<p>
Audacity е безплатен редактор за звук с отворен код.
Възможностите й включват запис и възпроизвеждане на звук,
импорт и експорт на формати WAV, AIFF, Ogg Vorbis, MP3 и др.
С нея можете да редактирате звуците чрез изрязване, копиране и вмъкване
(и да отменяте неограничен брой операции), да смесвате
писти и да прилагате ефекти върху записите.
В програмата е вграден редактор на обвиващи криви за амплитудата,
гъвкав режим за спектрограми и прозорец за честотен анализ на звук.
Вградените ефекти включват например ехо, промяна на темпото, премахване на шум; поддържат се и ефекти-приставки VST и LADSPA.
</p>

