<p>
Audacity е безплатен редактор за звук.
Възможностите й включват запис и възпроизвеждане на звук,
импорт и експорт на формати WAV, AIFF и MP3 и др.
С нея да редактирате звуците чрез изрязване, копиране и вмъкване
(и да отменяте неограничен брой операции), да смесвате
писти и да прилагате ефекти върху записите.
В програмата е вграден редактор на обвиващи криви за амплитудата,
гъвкав режим за спектрограми и прозорец за честотен анализ на звук.
Вградените ефекти включват усилване на басите, "уа-уа",
премахване на шум; поддържат се и ефекти-приставки VST.
</p>

<p>
Версията 1.0.0 на Audacity е най-стабилната, която сме издали досега,
и много хора намират, че уникалните й възможности и интуитивен интерфейс
я правят по-приятна за използване от доста други звукови редактори,
включително комерсиални.
</p>

<p>
Екипът на Audacity е издал и версия 1.1.0, която е с качество "бета" и
показва новите неща, по които активно се работи в момента. Тя съдържа
много нови възможности, но не е толкова изгладена и стабилна.
</p>

