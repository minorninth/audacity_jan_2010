1. Наистина ли е безплатна Audacity? Защо?<br>

</b></font>

<p>
Да, дори повече*. Audacity не просто се разпространява
безплатно, но с нея можете да правите почти каквото пожелаете.
<p>
Audacity се разпространява при условията на лиценза
<a target=_top href="http://www.gnu.org/copyleft/gpl.html"
>General Public License (GPL)</a>.
Свободни сте да я използвате за лични или комерсиални цели.
Можете да я раздавате или продавате. Изходният код на програмата
е достъпен безплатно в WWW и можете да го променяте за свои цели,
но извършените промени трябва също да бъдат разпространявани с
лиценз GPL.
<p>
Audacity е написана с използване на wxWidgets: библиотека,
разпространявана с още по-свободния лиценз LGPL. За повече
информация посетете
<a target=_top href="http://www.wxwidgets.org">wxwidgets.org</a>.
<p>
Авторите на Audacity са решили да я издадат при условията на
GPL по много причини. Някои от нас просто са си щедри. Някои
го правят по морални съображения, тъй като вярват, че всички
програми трябва да са със свободно достъпен код, докато други
смятат, че има нужда и от свободен, и от комерсиален софтуер.
<p>
Една причина Audacity да е безплатна е че така ще бъде по-популярна.
Много от нас предпочитат да виждат милиони потребители, които ползват
Audacity с удоволствие, отколкото хиляда души, които ни плащат. Повечето
потребители са по-дружелюбни, когато получават нещо безплатно.
<p>
Още една причина е, че така се окуражава сътрудничеството. Ако Audacity
беше shareware, щеше да е малко вероятно десетки хора от целия свят да дарят
код, поправки на грешки, документация и графика.
<p>
* <i>Забележка: на английски free означава и 'свободен', и 'безплатен', така че терминът е
двузначен: той може да означава както програма, разпространявана безплатно (но
на която нямате достъп до изходния код), така и програма, на която имате достъп
до изходния код и можете да го променяте (но това не значи задължително, че
всички копия от нея са безплатни). Audacity e "free" и в единия, и в другия смисъл.</i>
