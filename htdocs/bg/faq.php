5. Unix: Защо възниква грешка при зареждане на споделената библиотека libwx_gtk.so?<br></b></font>

<p><font size=+0>Това може да е причинено от два различни проблема:
<br>- Ако сте инсталирали wxWindows от RPM, стартирайте "ldconfig" (като root), за да
