все още не се инсталира, значи има непознат за нас проблем. Просто го инсталирайте
с параметър --nodeps.</font>

<p>
<a name="i5"></a>
<font size=+1><b>

5. Unix: Защо възниква грешка при зареждане на споделената библиотека libwx_gtk.so?<br></b></font>

<p><font size=+0>Това може да е причинено от два различни проблема:
<br>- Ако сте инсталирали wxWindows от RPM, стартирайте "ldconfig" (като root), за да
уведомите системата за новата библиотека.

<br><br>- Ако сте инсталирали wxWidgets от изходен код, тя може да е инсталирана в
/usr/local, без /usr/local/lib да е в пътя за библиотеки. Добавете /usr/local/lib във
файла "/etc/ld.so.conf" и стартирайте "ldconfig". Може да добавите "/usr/local/bin" към пътя за търсене,
така че програмите да могат да намират инструмента "wx-config".<br>

