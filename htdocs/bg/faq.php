2. Кой е разработил Audacity?<br>
</b></font>

<p>
Audacity е започната през 1999 г. от Доминик Мацони (Dominic Mazzoni)
по времето, когато е завършвал университета Карнеги Мелън в
Питсбърг, Пенсилвания, САЩ. Той работел по изследователски проект
заедно с консултанта си проф. Роджър Даненберг (Roger Dannenberg) и
на двамата е бил нужен инструмент за визуализация на алгоритми за
анализ на звук. С времето програмата прераснала в многоцелеви звуков
редактор и в разработването й се включили и други хора.
<p>
Днес Audacity се разработва чрез SourceForge - сайт, който помага
на хората по света да си сътрудничат в проекти за свободен софтуер.
За повече информация вижте <a href="http://www.sourceforge.net"
>sourceforge.net</a>. Десетки хора са вложили труд в Audacity и
работата постоянно се ускорява.
