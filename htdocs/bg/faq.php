<font size=+1><b>

1. Как се смесват две писти?
</b></font>
<p>

Audacity смесва автоматично. Всичко, което трябва да направите, е да
импортирате два клипа в един и същ проект, например чрез командата "Импорт на звук"
в менюто Проект, или като издърпате и пуснете два звукови файла върху Audacity. Освен това,
когато щракнете върху бутона Запис, Audacity автоматично създава нова писта.
<p>
За да съхраните смесените писти, можете да експортирате проекта, което ще смеси
автоматично всички писти, или да маркирате пистите и да използвате командата "Бързо смесване"
от менюто Проект.
<p>
<a name="a2"></a>
<font size=+1><b>
2. Мога ли да премахна вокала от запис?
</b></font>
<p>
При някои стерео записи е възможно да се премахнат вокалите заради
начина, по който записът е бил смесен в студиото. Често вокалите
са точно по средата между двата канала, докато другите инструменти
са изместени леко вляво или вдясно. Ако извадите левия канал от десния,
вокалите се унищожават взаимно, оставяйки само инструменала.
<p>

<b>Това работи само с някои записи!</b>
<p>
За да опитате горната операция с Audacity, импортирайте стерео запис,
после щракнете върху изскачащото меню за пистата (стрелката надолу до
името на пистата) и изберете "Разделяне на стерео писта".
Сега маркирайте долната писта (десния канал) и използвайте ефекта "Инвертиране"
(от менюто Ефект). Накрая използвайте изскачащите менюта на пистите, за да
ги превърнете в моно канали, и ги смесете с команда Бързо смесване. Ако имате
късмет, вокалът ще е изчезнал.
<p>
<a name="a3"></a>
<font size=+1><b>
3. Защо ако се опитам да запиша хармония със себе си,
пистите не се получават синхронизирани?
</b></font>
<p>
Това е нормално и става, защото има малко закъснение между момента,
когато Audacity започне възпроизвеждане, и когато звукът действително
стигне до високоговорителя. Audacity не се опитва да компенсира това
закъснение сама - трябва да го направите ръчно.
<p>
За да компенсирате закъснението, използвайте инструмента за изместване
във времето, като преместите една от пистите така, че те да се подравнят.
Забележете, че можете да ползвате инструмента, докато слушате записа.
<p>

<a name="a4"></a>
<font size=+1><b>
4. Защо с Audacity няма кодер за MP3?
</b></font>
<p>
За съжаление, алгоритъмът за кодиране или създаване на файлове
във формат в MP3 е патентован и <b>Audacity</b> не може да го
използва, без да бъде платена такса или да бъдат нарушени законите
в много страни.
<p>
Като компромис Audacity може да използва външни кодери за MP3, които
трябва да си свалите отделно. Ваша отговорност е да се уверите, че
не нарушавате лицензните ограничения, наложени върху тези кодери.
<p>
За повече информация вижте страницата за експорт във формат MP3 в
електронното ръководство към програмата.
<p>

<a name="a5"></a>
<font size=+1><b>
5. Мога ли да записвам звуков поток във формат RealAudio или друг?
</b></font>
</p><p>
Не и автоматично. Повечето формати за поточен звук, включително тези за
RealOne Player, Windows Media Player и Quicktime, са специално предназначени да не позволяват записване.
</p><p>
Все пак, почти винаги е възможно да се запише всичко, което компютърът ви
може да възпроизведе, като свържете изхода на звуковата карта с входа й
за микрофон или стерео входа.
</p><p>
Използвайте мини-кабел 1/8" стерео - 1/8" стерео. Включете единия му край
в жака за изход на звуковата карта (където обикновено бихте включили слушалки
или високоговорители - най-често е обозначен със зелено). Включете другия
край на кабела в жака line-in или този за вход от микрофон (обикновено обозначен с червено).
</p><p>
Сега Audacity може да записва всичко, което компютърът свири. Натиснете Record или Запис в Audacity, а после - Play в приемника за звуковия поток.
</p><p>

<a name="a6"></a>
<font size=+1><b>
6. Помощ! Натискам Запис, но се записва само тишина!
</b></font>
</p><p>
Audacity записва от подразбирания източник, избран в операционната система.
Ако имате няколко източника (вграден микрофон, външен микрофон, стерео вход,
музикален КД), трябва да изберете кой от тях се записва. Ако например искате
да записвате от микрофон, но входният източник е настроен на "line in",
Audacity ще запише само тишина.
</p><p>
В Windows можете да зададете входен източник, като щракнете с десния бутон
върху иконката с високоговорителя в системния панел (долу вдясно на екрана).
</p><p>
В Mac OS 9 можете да използвате панела Sound control. В Mac OS X можете да
ползвате панела Sound в System Preferences (във версия 10.2 и по-нова там
има страница Input).
</p><p>
Под Linux използвайте смесител като "xmixer" или "kmix".
</p><p>

<a name="a7"></a>
<font size=+1><b>
7. Как да разделя един файл на няколко писти?
</b></font>
</p><p>
Понякога може в един файл да са записани няколко песни. Ако искате да ги
запишете на КД като отделни писти, трябва да ги разделите в отделни файлове.
</p><p>
В Audacity има много начини да разделите запис на няколко писти. Ето някои от тях:
</p><p>
Начин 1: Export Selection [Експорт на маркираното]
</p><ul>
<li>Маркирайте частта от звука, съответстваща на първата писта.
</li><li>Изберете "Export Selection as WAV" [Експорт на маркираното като WAV] от менюто File [Файл], и я съхранете на диска.
</li><li>Повторете за останалите писти.
</li></ul>
<p>
Начин 2: Delete и Undo (изтриване и възстановяване)
</p><ul>
<li>Изтрийте всичко <i>освен</i> първата писта.
</li><li>Изберете "Export as WAV" [Експорт като WAV] от менюто File, и съхранете първата писта на диска.
</li><li>Изберете "Undo" [Отмяна] от менюто Edit [Редакция] колкото пъти е необходимо, за да върнете изтритата част от звука (Audacity поддържа неограничена отмяна).
</li><li>Повторете за останалите писти.
</li></ul>
<p>
Начин 3: Split и Export (разделяне и експорт)
</p><ul>
<li>Маркирайте областта за първата писта.
</li><li>Изберете "Split" [Разделяне] от менюто Edit, което ще премести маркираната област на нова писта в Audacity.
</li><li>Продължете по същия начин, докато разделите всички песни на отделни писти. Не забравяйте, че можете да анулирате грешните действия с Undo.
</li><li>Запишете всяка писта в отделен файл чрез "Export Selection as WAV". За да маркирате бързо писта, щракнете в етикета й (някъде в областта вляво от вълновата форма, под заглавието).
</li></ul>
<p>

