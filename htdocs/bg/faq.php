<font size=+1><b>
1. Windows: Какво да правя с файла с разширение .exe?<br>
</b></font>

<p>Изберете "Run from current location" от мрежовия навигатор. Така ще стартирате
инсталатора на Audacity, който ще инсталира самата програма и ще я включи в менюто
Start.
<p>
Ако вече сте свалили файла, щракнете двукратно върху него, за да стартирате
инсталатора.
<p>
Ако предпочитате да не ползвате инсталатора, можете да си свалите Audacity и във
формат ZIP, който да разкомпресирате с WinZip.

