<font size=+1><b>
1. Windows: Какво да правя с файла с разширение .exe?<br>
</b></font>

<p>Изберете "Run from current location" от мрежовия навигатор. Така ще стартирате
инсталатора на Audacity, който ще инсталира самата програма и ще я включи в менюто
Start.
<p>
Ако вече сте свалили файла, щракнете двукратно върху него, за да стартирате
инсталатора.
<p>
Ако предпочитате да не ползвате инсталатора, можете да си свалите Audacity и във
формат ZIP, който да разкомпресирате с WinZip.

<p>
<a name="i2"></a>
<font size=+1><b>
2. Windows: Как се деинсталира Audacity?<br>
</b></font>

<p><font size=+0>Audacity може да бъде деинсталирана чрез отваряне на прозореца
Add/Remove Programs в Control Panel. Изберете от списъка "Audacity" и щракнете върху
бутона "Add/Remove". Това стартира деинсталиращата програма на Audacity.
<p>
<a name="i3"></a> <font size=+1><b> 3. Mac: Какво да правя с файла с разширение .sit?<br> </b></font>

<p>
Трябва ви актуална версия на StuffIt Expander. Програмата е налице на
всички компютри Macintosh и обикновено е настроена по подразбиране във
всички мрежови навигатори за Mac. Ако Audacity не се разкомпресира
автоматично, изтеглете "audacity.sit" върху StuffIt Expander, за да го
разкомпресирате.
<p>
Няма нужда да инсталирате Audacity. Просто издърпайте папката й в папката
Applications или на друго място, където искате да бъде. За да я деинсталирате,
издърпайте папката й и я пуснете в кошчето.
<p>
