<font size=+1><b>

1. Как се смесват две писти?
</b></font>
<p>

Audacity смесва автоматично. Всичко, което трябва да направите, е да
импортирате два клипа в един и същ проект, например чрез командата "Импорт на звук"
в менюто Проект, или като издърпате и пуснете два звукови файла върху Audacity. Освен това,
когато щракнете върху бутона Запис, Audacity автоматично създава нова писта.
<p>
За да съхраните смесените писти, можете да експортирате проекта, което ще смеси
автоматично всички писти, или да маркирате пистите и да използвате командата "Бързо смесване"
от менюто Проект.
<p>
<a name="a2"></a>
<font size=+1><b>
2. Мога ли да премахна вокала от запис?
</b></font>
<p>
При някои стерео записи е възможно да се премахнат вокалите заради
начина, по който записът е бил смесен в студиото. Често вокалите
са точно по средата между двата канала, докато другите инструменти
са изместени леко вляво или вдясно. Ако извадите левия канал от десния,
вокалите се унищожават взаимно, оставяйки само инструменала.
<p>

<b>Това работи само с някои записи!</b>
<p>
За да опитате горната операция с Audacity, импортирайте стерео запис,
после щракнете върху изскачащото меню за пистата (стрелката надолу до
името на пистата) и изберете "Разделяне на стерео писта".
Сега маркирайте долната писта (десния канал) и използвайте ефекта "Инвертиране"
(от менюто Ефект). Накрая използвайте изскачащите менюта на пистите, за да
ги превърнете в моно канали, и ги смесете с команда Бързо смесване. Ако имате
късмет, вокалът ще е изчезнал.
<p>
<a name="a3"></a>
<font size=+1><b>
3. Защо ако се опитам да запиша хармония със себе си,
пистите не се получават синхронизирани?
</b></font>
<p>
Това е нормално и става, защото има малко закъснение между момента,
когато Audacity започне възпроизвеждане, и когато звукът действително
стигне до високоговорителя. Audacity не се опитва да компенсира това
закъснение сама - трябва да го направите ръчно.
<p>
За да компенсирате закъснението, използвайте инструмента за изместване
във времето, като преместите една от пистите така, че те да се подравнят.
Забележете, че можете да ползвате инструмента, докато слушате записа.
<p>

<a name="a4"></a>
<font size=+1><b>
4. Защо с Audacity няма кодер за MP3?
</b></font>
<p>
За съжаление, алгоритъмът за кодиране или създаване на файлове
във формат в MP3 е патентован и <b>Audacity</b> не може да го
използва, без да бъде платена такса или да бъдат нарушени законите
в много страни.
<p>
Като компромис Audacity може да използва външни кодери за MP3, които
трябва да си свалите отделно. Ваша отговорност е да се уверите, че
не нарушавате лицензните ограничения, наложени върху тези кодери.
<p>
За повече информация вижте страницата за експорт във формат MP3 в
електронното ръководство към програмата.

