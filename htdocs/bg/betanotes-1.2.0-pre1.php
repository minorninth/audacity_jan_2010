Нови възможности в Audacity 1.2.0-pre1:

  * Потребителски интерфейс:
    - вертикално мащабиране на пистите;
    - подобрен външен вид и позициониране на лентите с инструменти;
    - нови специализирани показалци за мишката;
    - напълно реализирани подлежащи на настройка клавишни комбинации;
    - намиране на пресечните точки на сигнал с нулата;
    - мащабиране на изгледа чрез колелцето на мишката;
    - многоинструментен режим (Multi-Tool mode);
    - усилване чрез обвиваща крива;
    - в надписите могат да се съхраняват маркирани области (както в
      Audacity 1.0.0).

  * Ефекти:
    - команда Repeat Last Effect [Повтаряне на последния ефект];
    - подобрена поддръжка за приставки VST;
    - за повечето ефекти вече има бутон за мостра (Preview);
    - компресор (Dynamic Range Compressor);
    - промяна на височината (без промяна на темпото);
    - промяна на темпото (без промяна на височината);
    - промяна на скоростта (променя и височината, и темпото);
    - повторение (полезно за създаване на зациклени звуци);
    - нормализиране на сигнал.

  * Вход/изход на звук:
    - 1-second preview command.
    - зациклено възпроизвеждане.

  * Вход/изход на файлове:
    - Audacity 1.2.0 отваря проектни файлове от всички предишни версии
      на Audacity от 0.98 до 1.1.3;
    - отваряне на няколко файла в един и същ диалогов прозорец;
    - използване на текстов файл със списък от звукови файлове за отваряне
      и отместванията им.

  * Обновено ръководство за потребители.


Поправени грешки в Audacity 1.2.0-pre1

  * Проектните файлове, съдържащи специални знакове, вече се четат.

  * Стържещите звуци, причинени от лошо изрязване (clipping), са отстранени.

  * Audacity вече не експортира дефектни файлове Ogg и не отрязва
    последните секунди на експортираните файлове в този формат.

  * Файловете MP3 с моно звук вече се експортират с коректна скорост.

  * Поправени са много некоректни резултати от прилагане на обвиваща крива.

  * Командата за експорт на надписи вече правилно презаписва
    съществуващи файлове.
  
  * Прозорецът за спектрален анализ показва верни номера на октави
    за нотите.

  * Отстранени са няколко утечки на памет.

  * Много други дребни корекции.
