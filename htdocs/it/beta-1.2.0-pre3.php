<h1>Audacity 1.2.0-pre3</h1>

<p>
Audacity 1.2.0-pre3 é una versione di test di Audacity, l'editor audio libero. Lo scopo di questa versione é di permettere agli utenti di testare e avere un'anteprima delle nuove caratteristiche che compariranno nella prossima versione stabile, la 1.2.0.
</p>

<p>
Per piacere riportate ogni errore trovato o commento a:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Il vostro contributo ci aiuterà a rendere la versione finale stabile e priva di errori!
Speriamo di rilasciare la nuova versione stabile nel Novembre 2003.
</p>
