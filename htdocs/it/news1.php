<?php BoxTop('2002-06-05: Versions 1.0.0 and 1.1.0 released!'); ?>

<p>
Il team di sviluppo di Audacity è lieto di annunciare la simultanea
pubblicazione delle versioni 1.0.0 e 1.1.0.
</p>

<p>
La versione 1.0.0 è la versione stabile di Audacity.
E' un buon, semplice editor audio per la registrazione, il mixaggio e
l'editing di traccie di qualità CD, su molte piattaforme incluse
Windows, Mac OS, e Linux.
</p>

<p>
La versione 1.1.0 è la versione beta di Audacity,
include il supporto di una migliore qualità audio, ulteriori formati file,
ulteriori effetti, ulteriori funzioni di editing, un interfaccia utente migliorata, e
un supporto per le lingue straniere.  Nonostante questa versione abbia molte nuove funzioni,
alcune di queste sono ancora sotto sviluppo e non sono state testate approfonditamente,
dunquè potrebbero avere qualche problema.
</p>

<p>
E' possibile avere nel proprio computer contemporaneamente entrambe le versioni di Audacity.
</p>

Nonostante questo sito sia in italiano, Audacity 1.0 è
disponibile solo in Inglese.  Audacity 1.1 (beta) supporta
molte lingue e se desideri avere una versione di Audacity in
lingua italiana, scarica la versione beta.  Altrimenti usa
Audacity in Inglese.

La documentazione potrebbe essere disponbile in italiano - controlla
la pagina di "Aiuto".

