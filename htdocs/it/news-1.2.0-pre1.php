Audacity 1.2.0-pre1 è una versione pubblica di test di Audacity, l'editor libero per l'audio.  Lo scopo di questa versione è di permettere agli utenti di testare le nuove funzionalità che saranno disponibili a partire dalle versione 1.2.0, che prevediamo e speriamo di distribuire per il prossimo mese.
