<h2>Problemi conosciuti</h2>

<p>
I seguenti sono i bug nella versione Audacity 1.2.0-pre1:
</p>

<ul>
<li>Il caricamento del plugin VST è stato disabilitato a causa di possibili problemi di licenza. Prevediamo di includere il supporto a VST nella versione finale di Audacity 1.2.0.</li>

<li>Il manuale utente è ancora in fase di costruzione.  Gli aggironamenti per  Audacity 1.2 sono completi al 75%.  
Pssono esserci pagine incomplete o non visibili.</li>

<li>Audacity può avere un crash se mentre l'audio è in fase di editing il programma viene utilizzato per la riproduzione o la registrazione.</li>

<li>(solo  per MacOS X?) Se viene selezionato Esci, e dopo si tenta di cancellare questa uscita, le operazioni di uscita e modifica delle preferenze non saranno più utilizzabili.</li>

<li>In certi sistemi operativi la barra del volume può essere azzerrata alla partenza del programma.</li>

<li>Audacity può importare e visualizzare i file MIDI ma non può riprodurli o modificarli.</li>

<li>Audacity ha un crash durante la mdofica del rate originale a + o - 12 volte.</li>

<li>La registrazione in full duplex in certi sistemi Linux può produrre un abbassamento della velocità di registrazione.</li>

<li>Solo in MacOS:  Il commando Find Zero Crossings ha come visualizzata scorciatoia da tastiera "Command-Z" ma la scorciatoia attuale è "Z".  Le scorciatoie per "l'aiuto in linea" e "Apri" sono in conflitto.</li>

<li>Solo in MacOS X: Per l'audio con amplitude 0, Audacity può visualizzare nulla (invece della linea piena).</li>
</ul>
