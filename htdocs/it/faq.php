5. Unix: Perchè ho un errore durante il caricamento
              della libreria libwx_gtk.so?<br></b></font>

<p><font size=+0>Questo può accadere per due problemi:
<br>- Se hai installato l'RPM di wxWindows, assicurati di eseguire "ldconfig" (da
