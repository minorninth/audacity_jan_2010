<font size=+1><b>
1. Audacity è realmente gratis?  Perchè?<br>

</b></font>

<p>

Si, in tutti i sensi.  Audacity non è solo distribuito gratis,
ma sei libero di fare ciò che vuoi con esso.
<p>
Audacity è distribuito sotto i termini della
<a href="http://www.gnu.org/copyleft/gpl.html"
>General Public License (GPL)</a>.
Sei libero anche di usare questo programma per usi personali
e commerciali.  Sei libero anche di copiarlo o rivenderlo.
Il codice sorgente di questo programma è liberamente disponibile
nel web, e puoi modificarlo secondo le tue volontà, tuttavia, tutte
le modifiche devono essere rilasciate secondo la licenza GPL.
<p>
Audacity è stato sviluppato con wxWindows, una libreria software che è
rilasciata sotto la meno restrittiva LGPL.
Per ulteriori informazioni, visita
<a href="http://www.wxwindows.org">wxwindows.org</a>.
<p>
Gli autori di Audacity hanno deciso di rilasciarlo sotto la
GPL per molte ragioni.  Alcuni di noi lo hanno fatto per
generosità, alcuni per ragioni morali perchè crediamo che
tutto il software dovrebbe essere libero mentre alcuni di noi
pensano ci sia la necessità sia di software libero che commerciale.
<p>
Una ragione per quì Audacity è libero è perchè così può godere di
maggiore popolarità. Molti di noi preferiscono sapere che ci sono
milioni di utenti che usano felicemente Audacity che non avere solo
qualche centinaia di utenti per un software commerciale.

<p>
Un altra ragione è che ciò incoraggia la collaborazione.  Se Audacity
fosse shareware, sarebbe improbabile che dozzine di persone avessero
contribuito al codice, ai bug, alla documentazione o alla grafica.

