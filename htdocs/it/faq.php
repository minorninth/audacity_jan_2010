<font size=+1><b>

1. Come fare il mix di due tracce??
</b></font>
<p>

Audacity esegue il  mix automaticamente.  Tutto quello che devi
fare è importare due traccie nello stesso progetto, lo puoi
fare usando il comando "Importa Audio" nel menu Progetto,
o semplicemente trascinando i due file audio all'interno di
Audacity.  Quando premi il bottone di registrazione, Audacity
crea automaticamente una nuova traccia.

<p>

Per salvare il mix, puoi usare l'esportazione progetto
che eseguira il mix di tutte le tue traccie o puoi selezionare
le tue traccie e usare il comando "Mix veloce" nel menu Progetto.

<p>
<a name="a2"></a>
<font size=+1><b>
2. Posso rimuovere la voce da una registrazione??
</b></font>
<p>

Con alcuni registratori stereo, è possibile rimuovere la voce
a causa del mix eseguito in fase di registrazione.
Spesso, la voce viene messa al centro della registrazione,
mentre gli altri strumenti sono messi all'esterno.  Se tu
dividi il canale destro da quello sinistro, la voce sarà
completamente eliminata,lasciano solo gli altri strumenti.
<p>

<b>Questo accade solo con alcune registrazioni!</b>
<p>
Per tentare questo effetto in Audacity, importa una registrazione
stereo, clicca nel pop-up della traccia (la freccia piccola
rivolta verso il basso vicino al nome della traccia) e seleziona
il comando "Dividi la traccia stereo".
Seleziona la traccia in basso (il canale destro) e usa l'effetto
"Inverti" (dal menu effetti).  Infine, usa il pop-up per trasformare
entrambe le traccie in mono, esegui il mix usando "Mix Veloce". Se
sei fortunato la voce sarà stata eliminata.

<p>
<a name="a3"></a>
<font size=+1><b>
3. Quando tento di registrare armonie perchè,
              ho sempre le due traccie fuori sincronia?
</b></font>
<p>
Questo è normale che accada perchè c'è sempre un ritardo tra  quando
Audacity inizia la riproduzione e quando il suono arriva ai tuoi
altoparlanti.  Audacity non cerca automaticamente di corregere questo
ritardo, devi farlo manualmente.
<p>
Per corregere questo ritardo, usa
Time Shift tool to slide
una delle traccie fino a quando saranno allineate.  E' possibile usare
questo strumento durante la registrazione.

<p>

<a name="a4"></a>
<font size=+1><b>
4. Perchè non distribuite Audacity con un encoder MP3??
</b></font>
<p>

Sfortunatamente l'algoritmo che permette la creazione di file MP3
è brevettato. <b>Audacity</b> non può includere un algoritmo
di esportazione MP3 senza chiedere denaro o senza violare la legge
in alcuni paesi.
<p>
Come compromesso, Audacity ha la facoltà di usare anche altri
encoders MP3 che puoi scaricare separatamente.
Spetta a te l'obbligo di usare questi encoders rispettando tutte le restrinzioni
che la licenza degli encoder MP3 impongono.
<p>
Per ulteriori informazioni, visita la pagina riguardante l'esportazione di MP3
all'interno della nostro aiuto in linea.

