<font size=+1><b>
1. Windows: cosa posso fare con il file .exe?<br>
</b></font>

<p>Dal browser, seleziona l'opzione "Esegui dalla posizione corrente".
Questo lancierà il programma di setup di Audacity, che installerà i file
di programma e crea una voce sul menu di avvio.
<p>
Se hai già scaricato il file, fai un doppio click su di esso e l'installazione
avrà inizio.
<p>
Se preferisci non usare il programma di installazione, puoi anche scaricare Audacity in
file zip che potrai decomprimere con Winzip.

