<font size=+1><b>
2. Chi sviluppa Audacity?<br>
</b></font>

<p>

Audacity è stato iniziato nel tardo 1999 da Dominic Mazzoni
mentre studiava alla Carnegie Mellon University, Pittsburgh, PA, USA.
Stava lavorando in un progetto di ricerca con il suo professore,
Roger Dannenberg, e loro necessitavano uno strumento che visualizzasse
gli algoritmi per l'analisi audio.  Questo programma si è tramutato presto
in un audio editor e altre persone iniziarono a contribuire con il loro aiuto.
<p>
Oggi, Audacity è sviluppato usando Sourceforge, un sito che
permette la collaborazione in progetti software.
Visita <a href="http://www.sourceforge.net"
>sourceforge.net</a> per ulteriori informazioni.
Dozine di persone hanno contribuito ad Audacity e il progresso è
in continua accelerazione.

