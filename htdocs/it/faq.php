<font size=+1><b>
2. Windows: Come disinstallare Audacity?<br>
</b></font>

<p><font size=+0>Audacity può essere disinstallato aprendo
la voce Aggiungi/Rimuovi Programmi nel pannello di controllo
Seleziona "Audacity" dalla lista e clicca il bottone "Aggiungi/Rimuovi".
Questo lancierà il programma di disinstallazione di Audacity.

<p>
<a name="i3"></a> <font size=+1><b> 3. Mac: cosa posso fare con il file .sit<br> </b></font>

<p>
Avrai bisogno di una versione recente di StuffIt Expander.  StuffIt Expander
è all'interno di tutti i  Macintosh e solitamente è configurato di default
su ogni web browser Mac.  Se Audacity non si decomprime automaticamente, trascina
"audacity.sit" su StuffIt Expander per eseguire la decompressione.
<p>
Non c'è bisogno di installare Audacity.  E' sufficiente trascinare la directory
di Audacity sulla directory delle applicazioni ovunque tu desideri collocarla.
Per la disinstallazione, è sufficiente trascinare l'intera directory sul cestino.

