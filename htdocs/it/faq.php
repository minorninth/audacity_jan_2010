<br><br>- Se hai installato wxWindows
dai sorgenti, potrebbe essersi installato in /usr/local, ma /usr/local/lib potrebbe
non essere nel path delle librerie. Aggiungi /usr/local/lib al file "/etc/ld.so.conf"
ed esegui "ldconfig". Puoi anche aggiungere "/usr/local/bin" al tuo path,
così i programmi potranno trovare l'utility "wx-config"<br>

