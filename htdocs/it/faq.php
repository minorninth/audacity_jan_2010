4. Ho trovato un bug!  Cosa devo fare?<br>
</b></font>
<p>

E' molto importante che un bug sia il più dettagliato
possibile.  Comunica le informazioni per riprodurre il
bug altrimenti sarà difficile la sua correzione.
<p>
