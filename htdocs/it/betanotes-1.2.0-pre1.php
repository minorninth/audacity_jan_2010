Nuove caratteristiche di Audacity 1.2.0-pre1:

  * Interfaccia utente
    - Zoom verticale delle traccie.
    - Migliore visibilità e posizionamento delle barre degli strumenti.
    - Nuovo cursore personalizzato.
    - Implementazione completa delle modifiche per le scorciatoie da tastiera.
    - Find zero-crossings.
    - La rotella del mouse può essere usata per aumentare o diminuire lo zoom.
    - Modo Multi-Tool.
    - Amplify using envelope.
    - Labels can store selections (come in Audacity 1.0.0).

  * Effetti
    - Ripetizione dell'ultimo effetto.
    - Supporto migliorato dei plug-in VST.
    - La maggior parte degli effetti ha un bottone di anteprima.
    - Compressore (Dynamic Range Compressor).
    - Cambia il Pitch (senza cambiare il tempo).
    - cambia il Tempo (senza cambiare il pitch).
    - Cambia la velocità (cambiare sia il pitch che tempo).
    - Ripetizione (utile per creare loops).
    - Normalizzazione (regolazione volume e DC bias).

  * Audio I/O
    - commando per l'anteprima di 1 secondo.
    - Riproduzione in loop.

  * File I/O
    - Audacity 1.2.0 apre i progetti creati con le versioni 0.98 fino alla 1.1.3.
    - Apre file multipli nella stessa finestra di dialogo.
    - Apre un file di testo per specificare la lista dei file audio da aprire.

  * Manuali per gli utenti aggiornati.


Bug corretti in Audacity 1.2.0-pre1

  * I file dei progetti con caratteri speciali non risultano più invalidi.

  * Rumori per diffetti di mixaggio sono stati corretti.

  * Audacity non esporta più file Ogg invalidi e non taglia più gli ultimi secondi dei file stessi.

  * I File MP3 ora sono esportati in maniera corretta.

  * Molte inesatezze di Envelope sono satte corrette.

  * L'esportazione delle etichette ora sovrascrive i file correttamente.
  
  * La finestra di "Plot Spectrum" visualizza correttamente i numeri di ottava per le note.

  * Molti problemi di memoria risolti.

  * Effettuate molte correzioni di bug segnalati.
