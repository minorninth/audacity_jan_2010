<p>
Audacity supporta i plug-in VST sulle piattaforme Macintosh e Windows.
Puoi trovare i Plug-ins per qualsiasi esigenza di filtraggio, equalizazzione, riverbero,
chorus, e altro!  Una lista di indirizzi dove scaricare o acquistare i Plug-ins
è riportata quì sotto.
</p>

<p>
Il supporto ai Plug-in di Audacity stà migliorando.  Per favore fateci sapere se
trovate un Plug-in VST  non funzionante in Audacity, e noi cercheremo
di farlo funzionare adeguatamente.
</p>

<p>
Per usare gli effetti dei Plug-in VST  in Audacity, create una cartella con nome
<b>VST</b> all'interno della directory di installazione di Audacity, e inserite
i vostri Plug-ins al suo interno!  la prossima volta che aprirete Audacity, i Plug-ins
saranno presenti nel menu Effetti.
</p>

