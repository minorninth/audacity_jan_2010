<h1>Audacity 1.2.0-pre1</h1>

<p>
Audacity 1.2.0-pre1 è una versione pubblica di Audacity, l'editor audio libero.  Lo scopo di questa versione è di permettere agli utenti di testare le nuove funzionalità che saranno disponibili a partire dalle versione 1.2.0.
</p>

<p>
Per favore spedite tutti i report dei bug o i commenti a:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
I vostri feedback ci aiuteranno a rilasciare la versione finale stabile e esente da bug!
Prevediamo di rilasciare la versione finale nel mese di settembre 2003.
</p>
