<p>
Audacity è un editor audio libero.
E' possibile registrare suoni, riprodurre, importare e esportare file WAV, AIFF, MP3 e altro.
Usalo per modificare i tuoi file di suono usando le funzioni Taglia, copia e incolla (con ripetizioni inlimitate
Undo), mixare le tue tracce, o applicare effetti alle tue registrazioni.
Ha un mdoficatore del volume integrato,
un spettrogramma personalizzabile e una
finestra per l'analisi di frequenza.
Gli effetti integrati includono l'eco, la modifica della velocità, la rimozione del rumore,
e supporta inoltre gli effetti VST e LADSPA tramite plug-in.
</p>


