<p>
Audacity è un editor audio libero.
E' possibile registrare suoni, riprodurre, importare e esportare file WAV, AIFF, MP3 e altro.
Usalo per modificare i tuoi file di suono usando Taglia, copia e incolla (con ripetizioni inlimitate
Undo), mixare le tue traccie, o applicare effetti alle tue registrazioni.
Ha un mdoficatore di amplitudine integrato,
un spectrogramma personalizzabile e una
finestra per l'analisi di frequenza.
Gli effetti integrati includono Bass Boost, Wahwah, la rimozione del rumore,
e supporta inoltre gli effetti VST tramite plug-in.
</p>

<p>
Audacity versione 1.0.0 è la versione più stabile che abbiano realizzato
