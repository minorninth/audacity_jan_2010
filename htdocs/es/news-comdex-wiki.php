noticias:

<p>
Audacity fue nominado como un posible programa de código abierto
que <a href="http://www.oreilly.com/">O'Reilly</a> podría enviar a
<a href="http://www.comdex.com/">COMDEX</a> en Las Vegas,
17-20 de noviembre. Haz <a href="http://www.oreillynet.com/contest/comdex/"
>clic aquí</a> para más información y para votar por Audacity
(registración gratuita es requerida en O'Reilly para votar). La votación
termina el 31 de octubre.
</p>

<p>
El <a href="http://audacityteam.org/wiki/">Audacity Wiki</a> es un sitio
web públicamente editable para cualquier cosa relacionada con Audacity.
Actualmente incluye consejos, documentación, y links, más un lugar para
pedido de agregado de funciones a Audacity. La mejor parte es que es fácil
para cualquier persona contribuir - simplemente haz clic en un link
al final de cada página para editar o agregar tu propio contenido. Esperamos
que te resulte un divertido y valioso recurso.
</p>

