<p>
Audacity es un editor de audio gratuito.
Puedes grabar sonidos, reproducir sonidos, importar y exportar archivos WAV,
AIFF, y MP3, y más.
Utilízalo para editar tus sonidos usando Cortar, Copiar y Pegar (con
ilimitados Deshacer), mezclar pistas, o aplicar efectos a tus grabaciones.
También posee un editor de envolvente de amplitud propio,
un modo espectograma ajustable a medida y una
ventana de análisis de frecuencia para aplicaciones de análisis de audio.
Efectos propios incluidos Bass Bost (Realzador de Graves), Wahwah, y
Removedor de Ruido, y también soporta efectos plug-in VST.
</p>

<p>
Audacity versión 1.0.0 es la versión más estable que hemos
lanzado hasta ahora, y mucha gente ha encontrado que sus capacidades
únicas y su interface intuitiva hacen que su uso sea más agradable
que el de otros editores de audio, incluyendo editores comerciales.
</p>

<p> Los desarrolladores de Audacity hemos lanzado también la versión 1.1.0,
la cual es un anticipo con calidad Beta de las nuevas cosas en las que
estamos trabajando activamente. Contiene realmente muchas nuevas funciones,
pero no es una versión tan pulida y estable.
</p>

