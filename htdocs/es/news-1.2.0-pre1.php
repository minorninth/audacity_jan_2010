<p>
Audacity 1.2.0-pre1 es un lanzamiento de prueba público del editor de
sonido gratuito Audacity. El propósito de este lanzamiento es permitir
a los usuarios previsualizar y probar las nuevas características que
aparecerán en nuestra próxima versión estable 1.2.0, la cual esperamos
lanzar el próximo mes.
</p>

<p>
Este lanzamiento agrega procesamiento de audio de calidad profesional,
y características nuevas importantes como la habilidad de acelerar,
desacelerar, y alterar el tono de una pista. Es la primer versión que
puede abrir archivos de proyecto guardados en el formato anterior Audacity 1.0
tanto como en el nuevo formato XML usado por Audacity 1.1 y superiores.
Audacity 1.2.0-pre1 soluciona muchos errores (bugs) de la última versión beta.
</p>

<p>
La página <a href="beta.php?<?php print "$langLinkStr"; ?>">Versiones Beta</a>
