<p>
Audacity soporta efectos Plug-in VST en las plataformas Macintosh y Windows.
Puedes encontrar Plug-ins que realicen todo tipo de filtros, ecualización,
reverb, chorus, y más! Encontrarás más abajo una lista de lugares para
descargar o comprar Plug-ins.
</p>

<p>
El soporte de Audacity para Plug-ins está mejorando. Por favor haznos
saber si encuentras un Plug-in VST que parezca no funcionar en Audacity,
y trataremos de arreglarlo.
</p>

<p>
Para utilizar un efecto Plug-in VST en Audacity, simplemente crea una
carpeta llamada <b>VST</b> en tu carpeta de Audacity, si es que todavía
no la tienes, y lleva tus Plug-ins allí! La próxima vez que abras
Audacity, los Plug-ins estarán en tu menú Efectos.
</p>

