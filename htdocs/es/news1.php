<p>
El Grupo de Desarrollo de Audacity está feliz de anunciar el lanzamiento
simultáneo de las versiones 1.0.0 y 1.1.0.
</p>

<p>
La versión 1.0.0 es la versión final y estable del Audacity original.
Es un buen y simple editor de audio apropiado para grabar, mezclar, y
editar pistas ordinarias con calidad CD de audio, en varias plataformas
incluyendo Windows, Mac OS, y Linux.
</p>

<p>
La versión 1.1.0 es el primer lanzamiento Beta de la próxima versión de Audacity,
la cual incluye soporte para audio de mayor calidad, más formatos de archivos de audio,
más efectos, más funciones de edición, una interface mejorada, y está
disponible en varios idiomas. Aunque esta versión tiene muchas capacidades nuevas y
útiles, algunas de estas funciones están todavía en desarrollo y no han
sido tan testeadas, y pueden tener problemas.
</p>

<p>
Es posible tener ambas versiones de Audacity en tu computadora al
mismo tiempo.
</p>

<p>
