<font size=+1><b>
4. Encontré un error!  ¿y ahora qué?<br>
</b></font>
<p>

Lo más importante cuando se reporta un error (bug) es ser lo más
específico posible. Danos la información suficiente como
para que podamos reproducir el error nosotros, de otra manera es
poco probable que podamos resolverlo.
<p>
Asegúrate de hacernos saber el sistema operativo que utilizas
(como Windows 98, MacOS 9.1, RedHat Linux 7.1, etc.)
y cualquier otra información sobre tu computadora que creas
que sea relevante.
<p>
Después, ¿Puedes reproducir el error? Si sucede en forma
consistente, dinos la secuencia exacta de eventos que causan
el error. Si recibes un mensaje de error, asegúrate de enviarnos
el texto exacto del mensaje de error.
<p>

Queremos eliminar todos los errores! Gracias por tomarte el tiempo
para ayudarnos a rastrearlos.

