1. Windows: ¿Qué hago con el archivo .exe?<br>
</b></font>

<p>
Si usas Internet Explorer, selecciona la opción "Ejecutar este
programa desde Internet". Esto lanza el programa de instalación
de Audacity, el cual instalará los archivos de programa de Audacity
y creará un enlace en el menú inicio.
<p>
Si ya has descargado el archivo, simplemente haz doble-click en él
para comenzar el instalador.
<p>
Si prefieres no utilizar el programa instalador, puedes también
descargar Audacity como un archivo ZIP, el cual puedes descomprimir
con muchos programas gratuitos descompresores de archivos ZIP.

