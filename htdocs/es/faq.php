<font size=+1><b>

1. ¿Cómo mezclo dos pistas?
</b></font>
<p>

Audacity mezcla automáticamente. Todo lo que tienes que hacer
es importar dos pistas dentro del mismo proyecto, lo cual
puedes hacer utilizando el comando "Importar Audio" en el menú
Proyecto, o simplemente arrastrando los dos archivos de audio
a Audacity. También, cuando presionas el botón para grabar,
Audacity genera una pista nueva automáticamente.

<p>
Para guardar tu mezcla, puedes o bien Exportar el
proyecto, lo cual va a mezclar automáticamente todas
tus pistas, o puedes seleccionar tus pistas y usar
el comando "Mezcla Rápida" en el menú Proyecto.

<p>
<a name="a2"></a>
<font size=+1><b>
2. ¿Puedo remover la voz en una grabación?
</b></font>
<p>
Con algunas grabaciones estéreo, es posible remover al vocalista
por el modo en el que las grabaciones fueron mezcladas en estudio.
A menudo, colocan las voces en el centro exacto de la grabación,
mientras que todos los demás instrumentos están un poco hacia un
lado o el otro. Si substraes el canal derecho del canal izquierdo,
las voces se cancelan completamente, dejando únicamente los otros
instrumentos. 
<p>

<b>Esto solamente funciona en algunas grabaciones!</b>
<p>
Para intentar esto en Audacity, debes importar una grabación estéreo,
luego abrir el menú de pista (haciendo click en la pequeña flecha al
lado del nombre de la pista) y seleccionar "Partir Pista Estéreo".
Ahora selecciona la pista de abajo (el canal derecho) y aplica el
efecto "Invertir" (en el menú Efectos). Finalmente, utiliza los
menues de pista de cada una de ellas para hacerlas canales Mono,
y luego mézclalas utilizando "Mezcla Rápida" (menú Proyecto). Si tienes
suerte, las voces desaparecerán. 

<p>
<a name="a3"></a>
<font size=+1><b>
3. Cuando trato de grabar armonías con mi propia voz,
¿por qué ambas pistas están fuera de sincronización?
</b></font>
<p>
Esto es normal y sucede porque hay un pequeño retraso entre
cuando Audacity comienza a reproducir sonido y cuando realmente
llega a tus parlantes. Audacity no trata de corregir automáticamente
este retraso, y debes hazerlo manualmente.
<p>
Para corregir este retraso, utiliza la "Herramienta de Traslado en Tiempo"
(uno de los cuatro botones de la esquina superior izquierda) para deslizar
una de las pistas hasta alinearlas. Nota que puedes utilizar esta
herramienta mientras estás escuchando la grabación.

<p>

<a name="a4"></a>
<font size=+1><b>
4. ¿Por qué Audacity no viene con un codificador MP3?
</b></font>
<p>

Desafortunadamente, el algoritmo para codificar o crear archivos MP3
está patentado, y <b>Audacity</b> no podría incluir un algoritmo de
exportación a MP3 sin cobrar o sin violar la ley en varios paises.
<p>
Como término medio, Audacity viene con la habilidad de utilizar
otros codificadores MP3, los cuales debes descargar por separado.
Depende de ti el asegurarte de que estás en cumplimiento con
cualquier restricción de licencia impuesta por codificadores MP3.
<p>
Para más información, lee la página sobre Exportar archivos MP3 en
nuestra "Ayuda Online".

