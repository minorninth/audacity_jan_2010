<a name="i2"></a>
<font size=+1><b>
2. Windows: ¿Cómo desinstalo Audacity?<br>
</b></font>

<p><font size=+0>Audacity puede ser desinstalado abriendo
"Agregar o quitar programas" en el Panel de Control (botón Inicio,
luego opción Configuración, y opción Panel de Control). Una vez abierto
"Agregar o quitar programas" selecciona "Audacity" de la lista
y presiona el botón "Agregar o Quitar...". Esto lanza el programa de
desinstalación de Audacity. 

<p>
<a name="i3"></a> <font size=+1><b> 3. Mac: ¿Qué hago con el
archivo .sit?<br> </b></font>

<p>
Necesitas una versión reciente de StuffIt Expander. StuffIt Expander
viene con todas las Macintosh y usualmente está configurado
directamente con todos los navegadores web de Mac. Si Audacity no se
descomprime automáticamente, arrastra "audacity.sit" a StuffIt
Expander para descomprimirlo.
<p>
No hay necesidad de instalar Audacity. Simplemente arrastra la
carpeta de Audacity a tu carpeta de Aplicaciones, o cualquier otro
lugar donde desees ponerla. Para desinstalar, sólo arrastra la
carpeta entera a la Papelera.

