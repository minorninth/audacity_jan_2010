1. ¿Realmente Audacity es Gratuito? ¿Por qué?<br>

</b></font>

<p>

Si, en casi todos los sentidos. Audacity no sólo es distribuido
gratuitamente, sino que también eres libre de hacer casi
cualquier cosa que desees con él. 
<p>
Audacity es distribuido bajo los términos de licencia
<a href="http://www.gnu.org/copyleft/gpl.html"
>General Public License (GPL)</a>.
Eres libre de utilizar este programa para propósitos personales
o comerciales. También eres libre de regalarlo o venderlo.
El código fuente de este programa está disponible en forma
gratuita en la Web, y eres libre de modificarlo para tu propio uso,
sin embargo cualquier cambio que hagas debe ser distribuido
también bajo la licencia GPL.
<p>
Audacity fue contruido usando wxWindows, una librería de software
la cual es distribuida bajo la menos restrictiva licencia LGPL.
Para más información, por favor visita
<a href="http://www.wxwindows.org">wxwindows.org</a>.
<p>
Los autores de Audacity decidieron la distribución bajo la licencia
GPL por muchas razones. Algunos de nosotros lo hacen por generosidad.
Algunos de nosotros lo hacemos por razones morales, porque
sentimos que todo software debería ser gratuito, mientras que otros
de entre nosotros creen en que hay una necesidad de ambos, software
gratuito y comercial, en el mundo.
<p>
Una razón para que Audacity sea gratuito es que de esa manera será
más popular. Muchos de nosotros preferirían ver un millón de
personas usando felizmente Audacity en forma gratuita que tener mil
personas pagándonos.
La mayoría de usuarios son más amigables cuando reciben algo en forma
gratuita.

<p>
Aún otra razón es que alienta a la colaboración. Si Audacity fuese
shareware, sería poco probable que docenas de personas alrededor del
mundo hubiesen aportado código, arreglos de errores, documentación, y
gráficos.

