2. ¿Quién desarrolló Audacity?<br>
</b></font>

<p>
Audacity fue comenzado a finales de 1999
por Dominic Mazzoni mientras era un estudiante
graduado en Carnegie Mellon University en Pittsburgh, PA, USA.
Estaba trabajando en un proyecto de investigación con su asesor,
Professor Roger Dannenberg, y necesitaron una herramienta que
les permitiese visualizar algoritmos de análisis de audio. Con
el tiempo, este programa evolucionó en un editor general de audio,
y otras personas comenzaron a ayudar.

<p>
Hoy, Audacity es desarrollado usando SourceForge, un sitio online
que permite a personas de todo el mundo colaborar en proyectos de
software gratuitos. Visita <a href="http://www.sourceforge.net"
>sourceforge.net</a> para más información.
Docenas de personas han aportado a Audacity,
y el progreso se está acelerando continuamente.

