<font size=+1><b>

5. Unix: ¿Por qué aparece un error mientras cargo
la librería compartida libwx_gtk.so?<br></b></font>

<p><font size=+0>Éste puede ser uno de dos problemas:
<br>- Si has instalado el wxWindows RPM, asegúrate de ejecutar "ldconfig"
(como root) así tu sistema sabe sobre tu nueva librería.

<br><br>- Si has instalado wxWindows
desde el código fuente, puede haber sido instalado en /usr/local, pero
/usr/local/lib puede no estar en tu path de librerias. Agrega /usr/local/lib al
archivo "/etc/ld.so.conf" y luego ejecuta "ldconfig". Tal vez también quieras
agregar "/usr/local/bin" a tu path, así esos programas pueden encontrar el
utilitario "wx-config".<br>

