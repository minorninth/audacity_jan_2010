<p>
Advarsel: den følgende utgaven av Audacity er av beta-kvalitet!
Den inneholder dusinvis med nye funksjoner, blant annet en ny verktøylinje,
automatisk omsampling, støtte for 32-bits lyd og kontroller for balanse og lydstyrke
på hvert spor.  Det støtter mange språk bortsett fra Engelsk, men verken programmet eller dokumentasjonen er ferdig og kan inneholde mange og omfattende feil.
</p>
<p>
Hvis du er interessert i å eksperimentere med de nye funksjonene oppfordrer vi deg til
å laste ned denne versjonen og prøve den.
Hvis du gjør det ville vi være takknemlige om du sendte oss dine kommentarer.
Fortell oss hva som virker, hva som ikke virker og hvilke nye funksjoner du kunne tenke deg.
Du kan ha både denne utgaven og Audacity 1.0 på din datamaskin samtidig.
</p>
<p>
NB!
Audacity 1.1.3 kan ikke lese eller skrive til prosjektfiler lagret av en annen versjon av Audacity, inkludert 1.1.1 og 1.1.2.  Du må eksportere all lyd fra prosjektet til standardformater som WAV, AIFF eller MP3 før du benytter deg av utgaven 1.1.3.
</p>