Forandringer i 1.2.2-pre1:

  * VU-Metre lagt til for både avspilling og opptak. Klikk på
    opptaksmeteret for å overvåke inndata uten å ta opp.
	
  * Eksporter flere - ny funksjon som lar deg eksportere flere
    filer samtidig, enten per spor eller delt basert på kommentarer.
	
  * Forsøk på å automatisk rette latens i opptak i full-duplex.
    (Dette virker ikke perfekt, og er ikke ennå støttet på alle
	systemer. Det vil bli bedre i senere versjoner.)

  * Rettet en alvorlig feil som kunne forårsake tap av data ved 
    lagring fulgt av gjenåpning og gjenredigering på et Audacity-
	prosjekt som inneholdt repetert eller duplisert data.

  * MP3-tag-dialogen vil bare komme fram første gangen du 
    eksporterer som MP3; etter det vil den ikke komme fram så lenge
	du har fylt inn minst én tag.

  * Du kan nå legge til en kommentar ved den løpende avspillings-
    posisjonen - i Prosjekt-menyen, eller med snarveien Ctrl+M.

  * Klikk på en kommentar vil nå velge alle sporene, noe som gjør 
    det lettere å bruke kommentarspor for å huske valg.

  * Windows: Rettet en feil som gav kræsj ved bruk av 
    tidssporets "Sett rate"-kommando.

  * Rettet en feil som forårsaket problemer med opptak på over 45 
    minutter på visse Windows-systemer.

  * Mac OS X: Forbedret støtte for Griffin iMic ved å rette en feil 
    som gjorde at den alltid tok opp i mono istedenfor stereo.

  * Lagt til støtte for Spill gjennom med programvare (spill av det 
    du tar opp samtidig som du tar det opp, eller mens du overvåker
	det vha. et VU-meter) - dette gjør det mulig, for eksempel, å 
	ta opp vha. én lydenhet mens du hører lyden spille gjennom en
	separat enhet. 

  * Unix/Linux: Rettet en frys som ble forårsaket av en "fanget mus"
    når lydenheten henger. (Audacity vil kanskje ikke svare, men den
	vil ikke lenger fryse hele X.)

  * Rettet en kosmetisk feil som gav en forvrengt bølgeform-visning 
    om du prøvde å åpne et Audacity-prosjekt lagret på en annen platform
	(f.eks. ved kopiering fra en Mac til en PC).

  * Rettet en feil som forårsaket ustabilitet ved innliming, oppdeling
    eller duplisering av kommentarspor. 

  * Du kan nå forandre skrifttypen på et kommentarspor ved å velge 
    "Skrifttype..." fra kommentarsporet sin lokalmeny.

  * Enkel utskriftsstøtte har blitt lagt til. Foreløpig skalerer den 
    hele prosjektet så det passer på nøyaktig én side. For å få best
	resultat, prøv å skrive ut med landskapsorientering.

  * Mac OS X og Windows: Audacity kommer nå med en nyere versjon (1.0.1)
    av Ogg Vorbis-omkoderen. Vorbis-kompresjon vil nå ha bedre kvalitet
	og mindre filstørrelser.

  * Rettet en feil som av og til forårsaket sammenbrudd hvis du brukte
    effekter for å dele opp spor.

  * Andre mindre feilrettinger og ytelsesforbedringer.

