<h2>Forandringer i Audacity 1.2.3</h2>
<ul>
<li><p>
		Windows: Rettet en feil som gjorde at opptak stoppet eller ble vist
		feil etter rundt 50 minutter på noen Windows-systemer. (Dette ble
		delvis rettet i Audacity 1.2.2, men virket fortsatt ikke på noen 
		systemer).
</p></li>
<li><p>
		Mac OS X: Rettet en større feil som fikk Audacity til å kræsje
		på tilsynelatende tilfeldige ganger på noen Mac-systemer, 
		spesielt ved avspilling eller opptak.
</p></li>
<li><p>
    Effektene Forandre tonehøyde og Forandre tempo har blitt oppgradert
	til en ny versjon av biblioteket SoundTouch av Olli Parviainen,
	med bedre hastighet og høyere kvalitet.
</p></li>
<li><p>
    libsndfile har blitt oppgradert til versjon 1.0.11. Audacity kan nå 
	importere filer fra Sound Designer II (SDII).
</p></li>
<li><p>
    Rettet en feil som fikk programmet til å kjøre sakte ved bruk av 
	Omhyllingskurveverktøyet.
</p></li>
<li><p>
    Shift-klikking på en Lyd av eller Solo-knapp vil nå skru lyden på 
	(eller solo av) på alle andre spor.
</p></li>
<li><p>
    Nyquist-innstikksmoduler kan nå akseptere strenger som inndata.
	I tillegg har en "Debug"-knapp blitt lagt til dialogene til
	Nyquist-effektene, som gjør at du kan se all utdataen produsert
	av Nyquist, for hjelp ved avlusing.
</p></li>
<li><p>
    Når lydfilen som er referert til ("aliased") av et Audacity prosjekt er
	borte, vil Audacity nå alltid spille stillhet. Før ville Audacity noen
	ganger gjenta den nyeste av lydene som tidligere hadde blitt spilt.
</p></li>
<li><p>
    VU-metrene vil nå alltid omstarte når lyd I/O (inn/ut) har stoppet.
</p></li>
<li><p>
    Nye eller oppdaterte oversettelser: Italiensk (it), Ungarsk (hu),
    Ukransk (uk), Spansk (es), Polsk (pl), Forenklet Kinesisk(zh),
    Norsk-Bokmål (nb), Fransk (fr), Russisk (ru).
</p></li>
</ul>

