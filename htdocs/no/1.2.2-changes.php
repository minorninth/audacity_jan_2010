<h2>Forandringer i Audacity 1.2.2</h2>
<ul>
<li><p><a href="onlinehelp-1.2/toolbar_meter.htm">Meterverktøy</a> lagt til
  for å overvåke volumnivå ved avspilling og opptak. Klikk på opptaksmeteret
  for å overvåke inndata uten å ta opp.</p></li>

<li><p>Eksporter flere - ny funksjon som lar deg eksportere flere filer 
    samtidig, enten per spor, eller delt opp basert på kommentarer.</p></li>

<li><p>Forsøk på å automatisk rette latens i opptak i full-duplex.
    (Dette virker ikke perfekt og er ikke ennå støttet på alle 
	systemer. Det vil forbedres i kommende versjoner.)</p></li>

<li><p>Rettet en alvorlig feil som kunne forårsake tap av data ved 
    lagring fulgt av gjenåpning og gjenredigering på et Audacity-
	prosjekt som inneholdt repetert eller duplisert data.</p></li>

<li><p>MP3-tag-dialogen vil bare komme fram første gangen du 
    eksporterer som MP3; etter det vil den ikke komme fram så lenge
	du har fylt inn minst én tag</p></li>

<li><p>Du kan nå legge til en kommentar ved den løpende avspillings-
    posisjonen - i Prosjekt-menyen, eller med snarveien Ctrl+M.</p></li>

<li><p>Klikk på en kommentar vil nå velge alle sporene, noe som gjør 
    det lettere å bruke kommentarspor for å huske valg.</p></li>

<li><p>Windows: Rettet en feil som gav kræsj ved bruk av 
    tidssporets "Sett rate"-kommando.</p></li>

<li><p>Rettet en feil som forårsaket problemer med opptak på over 45 
    minutter på visse Windows-systemer.</p></li>

<li><p>Mac OS X: Forbedret støtte for Griffin iMic ved å rette en feil 
    som gjorde at den alltid tok opp i mono istedenfor stereo.</p></li>

<li><p>Lagt til støtte for Spill gjennom med programvare (spill av det 
    du tar opp samtidig som du tar det opp, eller mens du overvåker
	det vha. et VU-meter) - dette gjør det mulig, for eksempel, å 
	ta opp vha. én lydenhet mens du hører lyden spille gjennom en
	separat enhet. </p></li>

<li><p>Unix/Linux: Rettet en frys som ble forårsaket av en "fanget mus"
    når lydenheten henger. (Audacity vil kanskje ikke svare, men den
	vil ikke lenger fryse hele X.)</p></li>

<li><p>Rettet en kosmetisk feil som gav en forvrengt bølgeform-visning 
    om du prøvde å åpne et Audacity-prosjekt lagret på en annen platform
	(f.eks. ved kopiering fra en Mac til en PC).</p></li>

<li><p>Rettet en feil som forårsaket ustabilitet ved innliming, oppdeling
    eller duplisering av kommentarspor. </p></li>

<li><p>Du kan nå forandre skrifttypen på et kommentarspor ved å velge 
    "Skrifttype..." fra kommentarsporet sin lokalmeny.</p></li>

<li><p>Enkel utskriftsstøtte har blitt lagt til. Foreløpig skalerer den 
    hele prosjektet så det passer på nøyaktig én side. For å få best
	resultat, prøv å skrive ut med landskapsorientering.</p></li>

<li><p>Mac OS X og Windows: Audacity kommer nå med en nyere versjon (1.0.1)
    av Ogg Vorbis-omkoderen. Vorbis-kompresjon vil nå ha bedre kvalitet
	og mindre filstørrelser.</p></li>

<li><p>Rettet en feil som av og til forårsaket sammenbrudd hvis du brukte
    effekter for å dele opp spor.</p></li>

<li><p>Zoom inn / Zoom ut vil nå deaktiveres på korrekt måte når de ikke
er tilgjengelige.</p></li>

<li><p>Sletter nå alltid midlertidige filer skapt av 
"Forhåndsvis"-funksjonen.</p></li>

<li><p>Andre mindre feilrettinger og ytelsesforbedringer.</p></li>
</ul>

