<h2>Kjente problemer</h2>

<p>
Her følger noen kjente feil i Audacity 1.2.1:
</p>

<ul>
  <li><p>Audacity kan importere og vise MIDI-filer, men de kan ikke spilles av eller redigeres.
    </p></li>
  <li><p>Juster tonehøyde-effekten kan krasje når den er brukt på et spor med en datapunktrate på 96 KHz.</p></li>
  <li><p>Bare Windows: Audacity krasjer når en bruker tidssporet sin "Sett rate"-funksjon.</p></li>
  <li><p>Linux: Innspilling i full duplex (avspilling på eksisterende spor samtidig med innspilling) gjør på noen systemer at mono-innspillinger høres saktere ut.</p></li>
  <li><p>MacOS X: Oppstart av Audacity kan få innstillingene til noen USB-lydenheter til å bli feil. Dette kan rettes ved å forandre innstillingene tilbake til det normale med Apples "Audio MIDI Setup"-program mens Audacity ennå kjører.</p></li>
  <li><p>MacOS X: Audacity kan ikke jobbe med filer eller mapper som ligger inni mapper med internasjonale tegn (aksent-tegn osv.) i navnene deres. Merk at filer med aksenter virker, og Audacity-prosjekter med aksenter virker. Bare filer inni mapper med aksenter i navnene vil skape problemer.
    </p></li>
  <li><p>Om Audacity-vinduet vises ikke riktig i språk som bruker ikke-vestlige alfabeter.</p></li>
</ul>

