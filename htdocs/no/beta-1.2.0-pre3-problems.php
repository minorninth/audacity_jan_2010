<h2>Kjente problemer</h2>

<p>
Her følger noen kjente feil i Audacity 1.2.3:
</p>

<ul>
  <li><p>Audacity kan importere og vise MIDI-filer, men de kan ikke spilles av 
  eller redigeres.
    </p></li>
  <li><p>Linux: Innspilling i full duplex (avspilling på eksisterende spor samtidig 
  med innspilling) gjør på noen systemer at mono-innspillinger får fordreid lyd. For 
  å jobbe rundt problemet, still inn Audacity til å ta opp i stereo.
    </p></li>
  <li><p>MacOS X: Audacity kan ikke jobbe med filer eller mapper 
  	som er inni mapper med internasjonale tegn (aksenter, æøå
	osv.) i navnene. Merk at filer med aksenter virker, og Audacity-
	prosjekter med aksenter virker. Bare filer inni mapper med aksenter 
	i navnene vil skape problemer.
    </p></li>
</ul>


