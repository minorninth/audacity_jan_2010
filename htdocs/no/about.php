<p>
Audacity er et fritt og gratis lydredigeringsprogram.
Det kan spille inn lyder, spille av lyder, importere og eksportere formatene WAV, AIFF og MP3 filer og mer.
Benytt det til å redigere dine lyder med funksjoner som Klipp, Kopier og Lim Inn (med ubegrensede angremuligheter), mikse spor sammen eller legge effekter på dine opptak.
Det har også innebygd redigering av amplitydeprofil,
en justerbar spektrogramfunksjon og et
vindu for frekvensanalyse.
Innebygde effekter inkluderer Bassforsterkning, Wahwah og Støyfjerner, 
og Audacity støtter også VST innstikksmoduler.
</p>

<p>
Audacity utgave 1.0.0 er den mest stabile versjonen som er utgitt,
og mange er av den mening at programmets unike funksjoner og intuitive
brukergrensesnitt gjør det lettere å bruke enn andre lydredigeringsprogrammer,
inkludert de som selges kommersielt.
</p>

<p>
Audacitys utviklere har også gitt ut utgave 1.1.0, en en beta-kvalitets smakebit på nye
funksjoner vi jobber aktivt med.
Denne utgaven inneholder mange, mange nye funksjoner, men er ikke fullt så velpolert
og stabil.
</p>

