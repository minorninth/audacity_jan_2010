Vi rettet en haug med feil i den sist utgitte testutgaven og vi vil publisere Audacity 1.2.0-pre3 en gang de neste dagene, sammen med VST Enabler, endelig. I mellomtida, her er noen andre nyheter:

<p>
Audacity ble nominert som et mulig åpen-kildekode-program som <a href="http://www.oreilly.com/">O'Reilly</a> kan sende til <a href="http://www.comdex.com/">COMDEX</a> i Las Vegas, 17.-20. november.  <a href="http://www.oreillynet.com/contest/comdex/"
>Klikk her</a> for mer informasjon og for å stemme på Audacity (du må registrere deg - gratis - hos O'Reilly for å stemme).
Du kan stemme fram til  31. oktober.
</p>

<p>
<a href="http://audacityteam.org/wiki/">Audacity Wiki</a> er en nettside som er mulig å redigere av alle, for alt som har med Audacity å gjøre. Den har foreløpig tips, dokumentasjon og linker, i tillegg til et sted for å be om nye programfunksjoner. Det beste er at det er lett for enhver å bidra - bare klikk på en lenke på bunnen av siden for å redigere eller legge til ditt eget materiale. Vi håper du vil synes at dette er en morsom og verdifull ressurs.
</p>

