<p><b>Audacitys Kunngjøringsliste-Policy</b></p>

<p>(Dette er listen som du blir med på ved å skrive inn epost-adressen din i boksen til venstre på nettsiden.)</p>

<p> Hvis du er interessert i å motta meldinger hver gang nye versjoner av Audacity kommer ut kan du gjerne skrive inn e-postadressen din i boksen til venstre, og klikke "Legg til". Du vil bli lagt til vår private e-postliste, hvor vi bare sender korte og heller sjeldne meldinger.
Disse meldingene vil vanligvis bare være på engelsk. Senere kan det bli mulighet for e-postlister i andre språk. 
</p>

