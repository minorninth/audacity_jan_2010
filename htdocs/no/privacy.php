<p>
Vi respekterer ditt privatliv.
</p>

<p> Under ingen omstendigheter vil Audacity-utviklerne eller administratorene for e-postlisten noen gang dele listen med utenforstående, uansett årsak. Vi vil ikke lagre noen annen informasjon om brukerne på denne listen (ikke en gang navnet ditt!) og vi vil aldri publisere e-postlisten på et offentlig tilgjengelig sted.
</p>

<p>
Hvis du ønsker å fjerne adressen din fra våre e-postlister, vennligst skriv inn e-postadressen i boksen til venstre og klikk "Fjern"-knappen. Dette vil fjerne deg fra listen vår øyeblikkelig; det kreves ingen bekreftelse fra din side og det blir ingen ventetid. Hvis det av en eller annen grunn ikke virker, vennligst send en e-post til <a href="mailto:dominic@audacityteam.org">dominic@audacityteam.org</a> så vil han prøve å hjelpe.
</p>

<p><b>Audacitys Epostliste-Policy</b></p>

<p>(Dette gjelder epostlistene 
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-help">audacity-help</a>, 
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-users">audacity-users</a>, 
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-devel">audacity-devel</a>, og 
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-translation">audacity-translation</a>.
</p>

<p>
Disse listene er <i>diskusjonslister</i>. Når du poster en beskjed i en av disse listene,
inkludert <a href="http://lists.sourceforge.net/lists/listinfo/audacity-help">audacity-help</a>,
vil den bli sendt til hundrevis av mennesker, som inkluderer både Audacity-utviklere og andre
medlemmer av publikum. Forskjellige nettsider, bl.a. Sourceforge, kan publisere arkiver av disse
listene på nettet. Arkivene vil normalt ikke inneholde epost-adressen din. Likevel, det er ingenting
som stopper folk fra å abbonnere på disse epostlistene og publisere deres egne arkiver, inkludert
epost-adresser. Hvis du er bekymret overfor privatlivet ditt, så bør du bruke en epostadresse som
ikke har så stor verdi for deg, eller en midlertidig en.
</p>

<p>
Hvis du har noen spørsmål om denne policyen og ikke er komfortabel med å sende dem ut 
til disse epostlistene, send en privat epost til lederen av Audacity-utviklingen på:
<a href="mailto:dominic@audacityteam.org">dominic@audacityteam.org</a>.
</p>

