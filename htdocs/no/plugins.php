<p>
Audacity 1.2 støtter <a href="http://www.ladspa.org/">LADSPA</a>- og
<a href="nyquist.php">Nyquist</a>-programtillegg rett fra pakken, og VST programtillegg om du bruker <a href="http://audacityteam.org/vst/">Audacity VST Enabler</a>.
</p>
<p>
Audacity 1.2.0 kommer allerede med noen LADSPA og Nyquist programtillegg i <b>Plug-ins</b>-mappen. Sett VST Enabler og andre programtillegg i denne mappen for å bruke dem med Audacity. Neste gangen du åpner Audacity vil programtilleggene være i Effekt-menyen (eller muligens Generer eller Analyser). Tilleggseffekter kommer alltid på bunnen av disse menyene, og innebygde effekter på toppen.
</p>

<p>
Audacity sin støtte for programtillegg er under forbedring. Vennligst si fra om du finner et VST-programtillegg som ikke ser ut til å virke i Audacity, så vil vi prøve å fikse det.
</p>

