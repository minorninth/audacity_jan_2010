<p>
Audacity är en gratis ljudeditor. Du kan spela in ljud, spela upp ljud, importera och exportera Wav, AIFF och MP3-filer och mycket mer. Använd programmet för att redigera dina ljud med Klipp ut, Kopiera och Klistra in (med oändlig Ångra-funktion, mixa ihop spår, eller lägga på effekter till dina inspelningar. Det har också en inbyggd envelope-editor där du kan styra ljudstyrkan, ett specialgjort spectrogramläge och ett fönster för ljudanalysprogram. Inbyggda effekter som Bass Boost, Wahwah och brusreducering, och programmet stöder också VST plugin-effekter. 
</p>

<p>
Audacity version 1.0.0 är den mest stabila version som vi släppt hittills, och många har funnit att dess unika funktioner och enkla gränssnitt gör det roligare att använda än andra ljudeditorer, inklusive kommersiella. 
</p>

<p>
Utvecklarna för Audacity har också släppt <a href="<?php print "beta.php?$langLinkStr"; ?>">version 1.2.0-pre1</a>, vilken är en betaversion med nya funktioner som vi aktivt arbetar med. Denna version innehåller många, många fler funktioner, men är inte finslipad och stabil än. 
</p>

