Vi har fixat några buggar i den sista testreleasen, och vi kommer att släppa  Audacity 1.2.0-pre3 inom några dagar, tillsammans med VST Enabler, till slut. Under tiden, här är några nyheter:

<p>
Audacity är nominerat som ett möjligt bidrag i klassen "open-source
program" som <a href="http://www.oreilly.com/">O'Reilly</a> skulle kunna skicka till <a href="http://www.comdex.com/">COMDEX</a> i Las Vegas,
17-20 november.  <a href="http://www.oreillynet.com/contest/comdex/"
>Klicka här</a> för mer information och för att rösta på Audacity (du måste registrera dig hos O'Reilly för att få rösta).
Röstning pågår till 31:a oktober.
</p>

<p>
<a href="http://audacityteam.org/wiki/">Audacity Wiki</a> är en allmän editerbar sajt för allt som har att göra med Audacity. Just nu innehåller den  tips, dokumentation, och länkar, plus en diskussion om funktioner som man saknar i programmet. Det bästa med detta är att det är enkelt för var och en att bidra med egna förslag och idéer - det är bara att klicka på en länk längst ner på sidan för att kuna ändra och lägga till egna åsikter. Vi hoppas att du skall sätta värde på denna möjlighet.
</p>

<p>
Vi kan nu erbjuda <a href="http://audacityteam.org/tshirt.php">Audacity T-Shirts</a> som ett tack till de personer som bidrar till utvecklingen av detta projekt.
</p>

<p>
Både <a href="http://audacityteam.org/wiki/">Wiki</a> och 
<a href="http://audacityteam.org/tshirt.php">T-Shirt erbjudande</a>
finns nu på en ny sajt, 
<a href="http://audacityteam.org/">AudacityTeam.org</a>,
som vi satt upp för extra Audacity material och frågor som rör Audacity development team, till skillnad från allmänna frågor kring Audacity.
</p>

<p>
Håll utkik efter Audacity 1.2.0-pre3.  Om du inte prövat en betaversion eller  en prerelease av Audacity nyss, titta gärna på våra uppdaterade  <a href="http://audacity.sourceforge.net/screenshots.php"
>Skärmdumpar</a>. 
</p>

