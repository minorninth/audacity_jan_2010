<h2>Kända problem</h2>

<p>
Kända buggar i Audacity 1.2.0:
</p>

<ul>
  <li><p>Audacity kan importera and visa MIDI files, men filerna kan inte spelas eller editeras.
    </p></li>
  <li><p>Inspelning i full duplex (spela upp spår samtidigt som man spelar in nya)på vissa Linux system kan orsaka att monoinspelningar låter långsamma.
    </p></li>
  <li><p>MacOS X endast: Audacity kan inte arbeta med filer och foldrar som är placerade inuti foldrar med internationella tecken (accenter, mm) i sina namn.  Observera att filer med accentuerade tecken fungerar, och Audacity projektfiler med Note that files with accentuerade tecken fungerar. Det är endast filer inuti foldrar med accentuerade tecken i deras namn som kan orsaka problem. 
    </p></li>
  <li><p>Effekten Ändra tonhöjd kan krascha om den används på ett spår med sampligsfrekvensen 96 KHz.</p></li>
</ul>
