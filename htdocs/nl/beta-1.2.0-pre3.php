<h1>Audacity 1.2.0-pre3</h1>

<p>
Audacity 1.2.0-pre3 is een publieke test vrijgave van de gratis geluidseditor Audacity.  Het doel van de vrijgave is de gebruikers kennis te laten maken met en het testen van de nieuwe mogelijkheden die in de navolgende stabiele versie 1.2.0. zijn ingebouwd.
</p>

<p>
S.v.p. opmerkingen of fouten rapporteren aan:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Jouw feedback helpt ons om de volgende versie stabiel en vrij van fouten te maken! We hopen de nieuwe stabiele versie in november 2003 vrij te kunnen geven.
</p>

