<p>
Audacity is door vrijwilligers in hun vrije tijd geschreven en ontwikkeld.  Als je Audacity waardeert en ons zou willen helpen, zijn hier enige tips:
</p>

<ul>

<li><b>Groet ons eens!</b>
<p>
Als je plezier beleeft aan Audacity, laat het ons eens weten!  Stuur een e-mail naar
<a href="mailto:audacity-help@lists.sourceforge.net"
>audacity-help@lists.sourceforge.net</a>
en laat ons eens weten dat je het programma waardeert.  Het maakt ons weer gelukkig als we weten dat mensen het programma waarderen.  Als je er behoefte aan hebt, kun je ons ook je opmerkingen of suggesties sturen - we zijn altijd bezig Audacity verder te ontwikkelen en te verbeteren en we waarderen je inbreng.
<p>

<li><b>Sluit je aan bij de Gebruikers Gemeenschap</b>
<p>
Deze mailinglijst is een plaats waar Audacity gebruikers met elkaar kunnen communiceren en hun ideeën kunnen delen.
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-users">Meldt je aan bij de Audacity-Gebruikers MailingLijst</a><br>


<li><b>Wordt een vertaler</b>
<p>
Als je Engels en een andere taal vloeiend beheerst, kun je andere mensen van je eigen taal helpen.  We hebben mensen nodig voor de vertaling van de menu's en dialogen van het programma, de documentatie en de website.
Ook hebben we mensen nodig die lid willen worden van de mailinglijst voor mensen die een vraag over Audacity willen stellen in hun eigen taal - je zou behulpzaam kunnen zijn in óf het beantwoorden van de vraag óf het vertalen van de vraag in het Engels en deze dan te plaatsen op één van de Engelse mailinglijsten.
<p>
<a href="http://audacity.sourceforge.net/translation/"
>Klik hier voor meer informatie over het vertalen.</a>
<p>

<li><b>Raporteren van fouten</b>
<p>
Als je een fout in Audacity ontdekt,
<a href="mailto:audacity-help@lists.sourceforge.net"
>laat het ons weten</a>.
Het meest behulpzaam is, als je de fout reproduceert en ons een gedetailleerde omschrijving stuurt over wat je deed op het moment dat de fout ontstond. Let er wel op dat je aan ons meldt welk besturingssysteem je gebruikt (bijv. Windows 2000, Mac OS 9, etc.).
<p>


<li><b>Wordt een beta-tester</b>
<p>
In navolging van de laatste stabiele versie van Audacity, hebben we altijd een beta versie van Audacity.  Deze versie bevat meestal vele nieuwe mogelijkheden welke nog niet zijn getest.  Als je zou willen helpen, download dan deze versie van Audacity en speel er eens mee.  Laat ons weten wat je bevalt en wat niet en vertel ons over hoe jij denkt hoe het verbeterd zou moeten worden.
Let op voor <i>regressions</i> - Dingen die vroeger werkten maar nu niet meer.
<p>

<li><b>Wordt een ontwikkelaar</b>
<p>
Als je al een ervaren C++ programmeur bent en je voelt je thuis in het gebruik van tools zoals CVS, zouden we je dolgraag verwelkomen bij het helpen ontwikkelen van Audacity.
Download a.u.b. de laatste broncode van CVS en stel het samen, en introduceer jezelf op de
<a href=http://lists.sourceforge.net/lists/listinfo/audacity-devel>
