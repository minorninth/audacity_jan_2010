<h2>Wijzigingen in Audacity 1.2.1</h2>

<ul>
  <li><p>De volgende vertalingen zijn toegevoegd of verbeterd: Fins,
    Frans, Hungaars, Italiaans, Japans, Noors, Pool, Russisch.

  <li><p>Fout hersteld die dataverlies kon veroorzaken bij het plakken van audio 
    van het ene project in het andere, nadat het eerste project opgeslagen en 
    gesloten werd.

  <li><p>Mogelijke crashfout hersteld bij openen of wijzigen van de grootte van 
    het Equalisatie venster, vooral bij gebruik van grote systeem lettertypen.

  <li><p>Niet toestaan van lagere percentages dan -100% in Wijzig  
    Pitch/Speed/Tempo effecten (dit hersteld een mogelijke crash).

  <li><p>Crashfout hersteld indein de tijdelijke folder bij opstarten niet 
    beschikbaar was.

  <li><p>Correct laden van in Audacity projectbestanden opgeslagen ID3 tags.

  <li><p>Op Linux en OS X, bewaren van lockfiles in de temp map in plaats van in
    de standaard map van de gebruiker. Dit hersteld problemen in lab omgevingen 
    waar gebruikers restricties hebben op of bij netwerk-gebonden standaard 
    directories.

  <li><p>Fout hersteld die voorkwam dat Nyquist effecten uitgevoerd konden 
    worden indien bepaalde regionale instellingen waren geactiveerd.

  <li><p>Fout hersteld in het Quick Mix commando die kon veroorzeken dat oude 
    tijdelijke bestanden niet verwijderd konden worden.

  <li><p>Linux: Endianness problemen in playback op PowerPC hersteld.

  <li><p>Linux: Compilatie problemen in Nyquist op MIPS hersteld.

  <li><p>Linux: Een meer recentere PortAudio v19 snapshot toegevoegd (hersteld
    compilatie problemen bij bouwen met de --with-portaudio=v19 optie).

  <li><p>Twee nieuwe Nyquist plug-ins: "Cross Fade In" en "Cross Fade Out."
  
  <li><p>Andere kleinere fouten hersteld.
</ul>
