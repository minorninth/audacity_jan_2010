<h2>Bekende Problemen</h2>

<p>
Het volgende zijn bekende fouten in Audacity 1.2.0-pre1:
</p>

<ul>
<li>VST plugin loading is uitgeschaekeld in deze vrijgave i.v.m. het werken aan
evetuele licensie problemen.  De bedoeling is om VST support in de
Audacity 1.2.0 final vrijgave weer te ondersteunen.</li>

<li>De gebruikershandleiding wordt nog aan gewerkt.  Updates voor Audacity 1.2
zijn voor ongeveer 75% compleet. Er kunnen enkele incomplete of vermiste paginas zijn.</li>

<li>Audacity zou kunnen crashen als audio bewerkt wordt terwijl het programma
een track afspeelt of opneemt.</li>

<li>(MacOS X alleen?) Als je Quit selecteert maar dan de Quit selectie afbreekt,
zullen de Quit en Voorkeuren commandos stoppen met functioneren.</li>

<li>De volumeregelaars in de mixer taakbalk kunnnen op sommige systemen tijdens het opstarten van het programma, een reset van het volume veroorzaken.</li>

<li>Audacity kan MIDI files importeren en laten zien, maar deze kunnen niet worden afgespeeld of bewerkt.</li>

<li>Audacity crasht indien wordt geprobeerd een sample rate meer dan 12
keer sneller of langzamer dan het origineel te converteren.</li>

<li>Opname in duplex (bestaande tracks afspelen tijdens opname) kan op sommige
Linux systemeneen verlangzaamd geluid tijdens de opname veroorzaken.</li>

<li>MacOS X alleen:  Het Zoek Zero Crossings commando heeft "Command-Z" als
snelkoppeling, maar behoort eigenlijk "Z" te zijn.  De snelkoppeling voor "Online Help" and "Open" conflict.</li>

<li>MacOS X alleen: Voor audio met zero amplitude, Audacity zou helemaal niets kunnen aangeven(i.p.v. een dunne rechte lijn).</li>
</ul>