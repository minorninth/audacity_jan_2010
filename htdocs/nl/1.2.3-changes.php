<h2>Wijzigingen in Audacity 1.2.3</h2>
<ul>
<li><p>
		Windows: Fout hersteld die na ongeveer 50 minuten foutieve 
                opnames of incorrecte weergave veroorzaakte op sommige Windows 
                systemen. (Dit was gedeeltelijk hersteld in Audacity 1.2.2, maar
                werkte nog steeds niet op sommige systemen.)
</p></li>
<li><p>
		Mac OS X: Fout hersteld die op willekeurige tijden Audacity liet
                crashen op Mac systemen, vooral tijdens playback of opnames.
</p></li>
<li><p>
    De Wijzig Pitch en Wijzig Tempo effecten zijn ge-upgrade om een
    nieuwe versie van de SoundTouch library door Olli Parviainen te gebruiken,
    met verbeterde snelheid en hogere kwaliteit.
</p></li>
<li><p>
    Libsndfile is ge-upgrade naar versie 1.0.11.  Audacity kan nu
		Sound Designer II (SDII) bestanden importeren.
</p></li>
<li><p>
    Fout hersteld die verorzaakte dat het programma te langzaam draaide tijdens
    het gebruik van de Envelope tool.
</p></li>
<li><p>
    Shift-klikken op een mute of solo knop bewerkstelligt nu un-mutes (of 
    un-solos) voor alle andere tracks.
</p></li>
<li><p>
    Nyquist plug-ins accepteren nu strings als input.  Ook is een "Debug"
    knop aan de Nyquist effecten dialoog toegevoegd, die je alle door Nyquist 
    geproduceerde output laat zien, als hulp bij het fouten herstellen.
</p></li>
<li><p>
    Als een door Audacity verwerkt audio bestand ("aliased") door een Audacity 
    project wordt vermist, zal Audacity nu altijd stilte afspelen. Daarvoor  
    speelde Audacity soms het meest afgespeelde audiobestand herhaaldelijk af.
</p></li>
<li><p>
    VU Meters zullen u altijd terugvallen zodra de audio I/O is gestopt.
</p></li>
<li><p>
    Nieuwe of gewijzigde vertalingen: Italiaasn (it), Hongaars (hu),
    Oekrainse (uk), Spaanse (es). Poolse (pl), Simpele Chinese (zh),
    Noors-Bokmal (nb), Frans (fr), Russisch (ru).
</p></li>
</ul>
