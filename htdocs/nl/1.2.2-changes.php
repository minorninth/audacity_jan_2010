<h2>Wijzigingen in Audacity 1.2.2</h2>
<ul>
<li><p><a href="onlinehelp-1.2/toolbar_meter.htm">Meter Toolbar</a> toegevoegd
  voor monitoren van volume levels tijdens playback en opname.  Klik op de
  opname meter om de input te monitoren zonder op te nemen.</p></li>

<li><p>Export Meerdere - nieuwe mogelijkheid die het mogelijk maakt meerdere 
    bestanden in een keer te exporteren. zowel als track of gesplitst gebaseerd 
    op labels.</p></li>

<li><p>Poging om latency automatisch te corrigeren in full-duplex opnames.
    (Het werkt niet perfect en wordt nog niet ondersteund op alle systemen.
    Verbeteringen in toekomstige versies verwacht.)</p></li>

<li><p>Serieuze fout hersteld die data verleis kon veroorzaken bijhet opslaan, 
    heropenen en opnieuw bewerken van een Audacity project welke herhaalde of 
    gedupliceerde datat bevat.</p></li>

<li><p>MP3 tags dialoog komt alleen tevoorschijn bij de eerste keer dat je als 
    MP3 exporteert; daarna zal het niet weer tevoorschijn komen zolang je 
    tenminste een tag hebt gevuld.</p></li>

<li><p>Je kunt nu een label toevoegen bij de huidige playback positie - in
    het Project menu, met de sneltoets Ctrl+M.</p></li>

<li><p>Kliken op een label selecteert nu alle tracks, wat het makkelijker maakt 
    om de label track te gebruiken voor het opnieuw oproepen van 
    selecties.</p></li>

<li><p>Windows: Crash hersteld in het Time Track "Set Rate" commando.</p></li>

<li><p>Fout hersteld die problemen veroorzaakte met opnames van meer dan 45 
    minuten op sommige Windows systemen.</p></li>

<li><p>Mac OS X: Improved support for the Griffin iMic by fixing a bug
    that was causing it to always record in mono instead of stereo.</p></li>

<li><p>Ondersteuning toegevoegd voor Software Playthrough (luisteren naar 
    opnames terwijl je deze nog aan het opnemen bent of aan het bekijken met 
    gebruik van de VU meter) - Dit maakt het bijvoorbeeld mogelijk, om opnames 
    via een audio apparaat te maken terwijl je ernaar kunt luisteren op een 
    separaat audio apparaat.</p></li>

<li><p>Unix/Linux: Vastloper hersteld dat veroorzaakt werd door de muis terwijl 
    een audio apparaat bleef hangen. (Audacity reageert dan wellicht niet meer 
    maar er volgt geen vastloper.)</p></li>

<li><p>Kosmetische fout hersteld die foutieve weergave van een waveform    
    veroorzaakte, als je een Audacity project wilde openen op een ander systeem 
    dan waar deze was opgeslagen. (bijvoorbeeld, een project kopieëren van een 
    Mac naar een PC).</p></li>

<li><p>Fout hersteld die instabiliteit kon veroorzaken bij plakken, splitsen of
    dupliceren van een label track.</p></li>

<li><p>Je kunt nu het lettertype van een label track wijzigen door kiezen van 
    "Font..." uit het label track's pop-up menu.</p></li>

<li><p>Basis print ondersteuning toegevoegd. Momenteel zorgt dit ervoor dat het 
    gehele project op een pagina past.  Probeer te printen in liggende positie 
    in plaats van de staande, voor beere resultaten.</p></li>

<li><p>Mac OS X en Windows: Audacity bevat een nieuwere versie van (1.0.1)
    de Ogg Vorbis encoder.  Vorbis compressie bevat nu hogere kwaliteit en 
    kleinere bestandsformaten.</p></li>

<li><p>Fout hersteld die zo nu en dan een crash veroorzaakte bij het toevoegen 
    van effecten tijdens het splitsen van tracks.</p></li>

<li><p>Zoom In / Zoom Uit worden nu correct uitgeschakeld als ze niet 
    beschikbaar zijn.</p></li>

<li><p>Altijd verwijderen van tijdelijke bestanden die door de "Preview" functie
    zijn gemaakt.</p></li>

<li><p>Andere kleinere fouten hersteld en uitvoering verbeterd.</p></li>
</ul>
