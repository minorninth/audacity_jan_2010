<p>
Audacity 1.2.0-pre1 is een publieke test vrijgave van de gratis Audacity geluids-editor. De bedoeling van de vrijgave is om gebruikers de kans te geven de nieuwe mogelijkheden, welke in onze volgende stabiele versie 1.2.0. aanwezig zullen zijn, die we volgende maand hopen vrij te geven, te bekijken en te testen.
</p>

<p>
Deze vrijgave voegt professionele audiokwaliteits bewerking toe en vele nieuwe mogelijkheden zoals de mogelijkheid de pitch van een track te versnellen, langzamer te maken en te veranderen. Het is de eerste vrijgave die het mogelijk maakt projecten te openen die hetzij opgeslagen zijn via het oudere 
Audacity 1.0 formaat hetzij via het nieuwere XML formaat dat wordt gebruikt door Audacity 1.1 en hoger.  Audacity 1.2.0-pre1 herstelt vele fouten van de laatste beta vrijgave.
</p>

