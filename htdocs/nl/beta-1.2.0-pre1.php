<h1>Audacity 1.2.0-pre1</h1>

<p>
Audacity 1.2.0-pre1 is een plubieke test vrijgave van het gratis audiobewerkingsprogramma Audacity.  Het doel van deze vrijgave is, om gebruikers de mogelijkheid te geven de nieuwe mogelijkheden, welke verwerkt zullen zijn in onze volgende stabiele versie 1.2.0., te bekijken en te testen.
</p>

<p>
A.u.b. melden van fouten of opmerkingen naar:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Jouw terugkoppeling helpt ons om de definitieve vrijgave stabiel en vrij van fouten te maken!
We hopen de nieuwe stabiele versie in September 2003 vrij te kunnen geven.
</p>
