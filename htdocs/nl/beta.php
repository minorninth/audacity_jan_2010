<p>
Waarschuwing: de navolgende versie van Audacity is van beta kwaliteit!
Het bevat vele nieuwe mogelijkheden, zoals nieuwe werkbalken,
automatische resampling, en ondersteuning voor 32-bit samples, maar waarschijnlijk bevat het ook nieuwe fouten.
</p>
<p>
Als je geinteresseerd bent in het experimenteren met de nieuwe mogelijkheden, nodigen wij je uit om deze versie te downloaden en gewoon eens uit te proberen.
Als je dit doet, stuur ons dan a.u.b. je ervaring met de werking hiervan. Laat ons weten wat werkt en wat niet en welke nieuwe mogelijkheden je zou willen hebben.
Je kunt zowel deze versie als Audacity 1.0 tegelijkertijd op je computer geinstalleerd hebben.  
</p>
<p>
Opmerking:
Deze versie kan geen bestanden die via Audacity 1.0 of ouder zijn opgeslagen, lezen of wegschrijven. Het gebruikt een nieuwe, op XML gebaseerde, bestandsformaat.
</p>
