<p>
Waarschuwing: de navolgende versie van Audacity is van beta kwaliteit!
Het bevat vele nieuwe mogelijkheden, zoals nieuwe werkbalken,
automatische resampling, ondersteuning voor 32-bit samples en "pan en gain" schuifregelaars voor elke muziektrack. Het programma ondersteunt niet alleen Engels maar ook meerdere andere talen. Het programma is echter nog niet geheel af, de documentatie is nog niet gereed en waarschijnlijk bevat het ook nog ernstige fouten.
</p>
<p>
Als je geïnteresseerd bent in het experimenteren met de nieuwe mogelijkheden, nodigen wij je uit om deze versie te downloaden en gewoon eens uit te proberen.
Als je dit doet, stuur ons dan a.u.b. je ervaring met de werking hiervan. Laat ons weten wat werkt en wat niet en welke nieuwe mogelijkheden je zou willen hebben.
Je kunt zowel deze versie als Audacity 1.0 tegelijkertijd op je computer geinstalleerd hebben.  
</p>
<p>
Opmerking:
Audacity 1.1.3 kan geen bestanden die via andere Audacity versies (zoals 1.1.1 of 1.1.2 of ouder) zijn opgeslagen, lezen of wegschrijven. Je moet al je audiobestanden exporteren in standaard formaten zoals WAV, AIFF of MP3 voordat je Audacity 1.1.3 gaat gebruiken.
</p>

