<p>
Het Audacity Ontwikkelaars Team is blij om de gelijktijdige vrijgave van de versies 1.0.0 en 1.1.0. aan te mogen kondigen.
</p>

<p>
Versie 1.0.0 is de laatste, stabiele versie van het originele Audacity.
Het is een goede, simpele geluidsbewerker, geschikt voor opnemen, mixen en
bewerken van gewone tracks van audio CD-kwaliteit, op vele systemen waaronder
Windows, Mac OS, en Linux.
</p>

<p>
Versie 1.1.0 is de eerste beta vrijgave van de volgende Audacity versie,
met daarin ondersteuning voor hogere kwaliteits audio, meer audio bestandsformaten, meer effecten, meer bewerkingsfuncties, een verbeterde gebruikers interface en ondersteuning van buitenlandse talen.  Hoewel deze versie vele nieuwe en bruikbare functies bevat,
zijn sommige van deze functies nog steeds in ontwikkeling en zijn ze nog niet zo vaak getest en daardoor zouden er problemen kunnen ontstaan.
</p>

<p>
Beide versies van Audacity kunnen tegelijkertijd op je computer zijn geïnstalleerd.
</p>

