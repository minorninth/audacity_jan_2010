Wijzigingen in 1.2.0:

  * Nieuwe cross-fade effecten.

  * Probleem herstelt, die een foute positie bij het inzoomen van samples 
    veroorzaakte.  Gevolg hiervan was, dat de drawing tool een andere sample 
    dan de onder de cursor geselecteerde sample, verplaatste.

  * Gebruik de id3v2.4 tags nog niet omdat ze nog niet door de meeste 
    afspelers worden ondersteund (Dit was in 1.2.0-pre2 hersteld, maar dook 
    per ongeluk weer op in 1.2.0-pre3.)

  * Correcte weergave van vertaalde berichten in de statusbalk.

  * Als de cursor on-screen is, zal de Zoom-in knop nu inzoomen op het gebied 
    rondom de cursor.

  * Mac OS X: Audio problemen op de Apple PowerMac G5 hersteld.

  * Linux/ALSA: Omleiding in gebruik ALSA's OSS emulation die ervoor zorgde 
    dat Audacity's playback cursor te snel bewoog.

  * Microsoft Windows: De Audacity broncode zou nu in Windows uit de box 
    gecompileerd moeten worden.

  * Vele nieuwe/ge-update vertalingen.

