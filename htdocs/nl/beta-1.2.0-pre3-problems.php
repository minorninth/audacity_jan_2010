<h2>Bekende Problemen</h2>

<p>
Het volgende zijn bekende problemen in Audacity 1.2.3:
</p>

<ul>
  <li><p>Audacity kan MIDI bstanden importeren en tonen, maar ze kunnen niet
    afgespeeld of bewerkt worden.
    <p></li>
  <li><p>Linux:  Opname in full duplex (afspelen van bestaande tracks tijdens 
    opnemen)kan op sommige Linux systemen mono opnames met storing veroorzaken.   
    Om dit probleem te voorkomen , dien je Audacity in te stellen om in 
    stereo op te nemen.
    <p></li>
   <li><p>MacOS X: Audacity kan niet met bestanden of mappen werken
    die geplaatst zijn in mappen met internationale characters
    (geaccentueerde markeringen e.d.) in de naam.  Bestanden met geaccentueerde
    characters werken wel en Audacity projecten met geaccentueerde
    characters werken ook. Het probleem ontstaat alleen bij bestanden in mappen 
    met geaccentueerde characters
    <p></li>
  </ul>