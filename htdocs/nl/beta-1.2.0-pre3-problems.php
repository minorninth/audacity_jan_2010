h2>Bekende Problemen</h2>

<p>
Het volgende zijn bekende problemen in Audacity 1.2.0-pre3:
</p>

<ul>
  <li>Audacity kan MIDI bstanden importeren en tonen, maar ze kunnen niet
    afgespeeld of bewerkt worden.
    <p>
  <li>Opname in full duplex (afspelen van bestaande tracks tijdens opnemen)
    kan op sommige Linux systemen de opnames verlangzaamd laten klinken.
    <p>
  <li>Alleen voor MacOS X: Audacity kan niet met bestanden of mappen werken
    die geplaatst zijn in mappen met internationale characters
    (geaccentueerde markeringen e.d.) in de naam.  Bestanden met geaccentueerde
    characters werken wel en Audacity projecten met geaccentueerde
    characters werken ook. Dit geldt alleen voor mappen met geaccentueerde
    characters
    <p>

   <li>Alleen voor Windows:
    De Windows installer neemt de plaats in van 1.0.0 installaties, 
    maar niet die van de 1.1.x beta installatie.
    <p>
</ul>
