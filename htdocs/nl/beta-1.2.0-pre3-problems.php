<h2>Bekende Problemen</h2>

<p>
Het volgende zijn bekende problemen in Audacity 1.2.0:
</p>

<ul>
  <li><p>Audacity kan MIDI bstanden importeren en tonen, maar ze kunnen niet
    afgespeeld of bewerkt worden.
    <p></li>
  <li><p>Opname in full duplex (afspelen van bestaande tracks tijdens opnemen)
    kan op sommige Linux systemen de opnames verlangzaamd laten klinken.
    <p></li>
  <li><p>Alleen voor MacOS X: Audacity kan niet met bestanden of mappen werken
    die geplaatst zijn in mappen met internationale characters
    (geaccentueerde markeringen e.d.) in de naam.  Bestanden met geaccentueerde
    characters werken wel en Audacity projecten met geaccentueerde
    characters werken ook. Dit geldt alleen voor mappen met geaccentueerde
    characters
    <p></li>
  <li><p>Het Wijzig Pitch effect zou kunnen crashen bij het gebruik ervan met
     een sample rate van 96 KHz.</p></li>
</ul>