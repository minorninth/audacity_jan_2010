Wijzigingen in 1.2.2-pre1:

  * VU Meters toegevoegd voor zowel playback en recording.  Klik op
    de recording meter om de input te kunnen zien zonder opnemen.

  * Export Multiple - nieuwe moelijkheid waardoor je meerdere bestanden ineens 
    kunt exporteren, of als track op zich of split gebaseerd op labels.

  * Poging om latency in full-duplex opnames automatisch te corrigeren.
    (Dit werkt nog niet geheel perfect en wordt nog niet op alle systemen 
    ondersteund.  Verbeteringen worden in toekomstige versies aangebracht.)

  * Serieuze fout hersteld die datverlies kon veroorzaken na opslaan en opnieuw
    laden en bewerken van een Audacity project waarin herhaalde of gedupliceerde
    data in voorkwam.

  * MP3 tags dialoog zal alleen verschijnen bij de eerste keer exporteren als 
    MP3; daarna zal deze niet weer verschijnen zolang je tenminste een tag 
    ingevuld hebt.

  * Je kunt nu een label toevoegen vanaf de huidige playback positie - in
    het Project menu, met de sneltoets Ctrl+M.

  * Klikken op een label selecteert nu alle tracks, waardoor het gemakkelijker 
    wordt om de label track te gebruiken om selecties opnieuw op te roepen.

  * Windows: crashfout hersteld in het Time Track "Set Rate" commando.

  * Fout hersteld die problemen op sommige Windows systemen veroorzaakte bij 
    opnames van meer dan 45 minuten.

  * Mac OS X: Verbeterde ondersteuning voor de Griffin iMic door herstel van een 
    fout die veroorzaakte dat constant mono in plaats van stereo werd opgenomen.

  * Toegevoegde ondersteuning voor Software Playthrough (luiseren naar je 
    opnames terwijl je nog aan het opnemen bent of aan het bekijken via de VU
    meter) - Dit maakt het bijvoorbeeld mogelijk om een opname via een 
    audioapparaat te maken terwijl je het kunt afspelen en beluisteren via een 
    ander, apart audioapparaat.

  * Unix/Linux: Bevriezen hersteld, da werd veroorzaakt door de muis terwijl het
    audioapparaat vastliep.  (Audacity reageert misschien niet maar het 
    veroorzaakt in ieder geval geen totale vastloper.)

  * Kosmetische fout hersteld waardoor een foute waveform te zien was als
    je een Audacity project wilde openen die op een ander platform opgeslagen 
    was (bijvoorbeeld een project kopieeren van een Mac naar een PC).

  * Fout hersteld die een onstabiel systeem kon veroorzaken bij het plakken, 
    splitsen or dupliceren van een label track.

  * Je kunt nu het lettertype van een label track wijzigen met kiezen "Font..."
    vanuit het label track's pop-up menu.

  * Basis printen ondersteuning toegevoegd.  Momenteel wordt het gehele project 
    zo gemaakt dat deze precies op een pagina past.  Probeer te printen in de 
    liggende optie in plaats van de staande, voor betere resultaten.

  * Mac OS X en Windows: Audacity bevat nu een nieuwere versie (1.0.1) van de 
    Ogg Vorbis encoder.  Vorbis compressie heeft nu hogere kwaliteit en een 
    kleiner besandsformaat.

  * Fout hersteld die af en toe een crash veroorzaakte bij het toevoegen van 
   effecten bij het splitsen van tracks.

  * Andere kleine fouten hersteld en verbeteringen aangebracht.

