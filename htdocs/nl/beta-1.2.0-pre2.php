<h1>Audacity 1.2.0-pre2</h1>

<p>
Audacity 1.2.0-pre2 is een publieke testvrijgave van de gratis Audacity audio
editor.  Het doel van deze vrijgave is, om gebruikers toe te staan de nieuwe functies die verwerkt zijn in onze volgende stabiele uitgave 1.2.0., te bekijken en te testen.
</p>

<p>
Rapporteer a.u.b. fouten of opmerkingen aan:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Jouw feedback helpt ons de volgende versie stabiel en foutenvrij te maken!
We hopen de nieuwe stabiele versie in oktober 2003 vrij te kunnen geven.
</p>
