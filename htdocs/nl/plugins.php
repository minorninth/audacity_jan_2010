<p>
Audacity 1.2.0 ondersteund <a href="http://www.ladspa.org/">LADSPA</a> en
<a href="nyquist.php">Nyquist</a> plug-ins van origine en VST plug-ins
met de optionele <a href="http://audacityteam.org/vst/">Audacity VST Enabler</a>.
</p>
<p>
Audacity 1.2.0 bevat al enkele samples van LADSPA en Nyquist plug-ins in de
<b>Plug-ins</b> map. Plaats de VST Enabler en andere plug-ins in deze map zodat je ze met Audacity kunt gebruiken. De eerstvolgende keer dat je Audacity opent, zullen de Plug-ins via het Effecten menu te benaderen zijn (of mogelijk Generate of Analyze).  Plug-in effecten verschijnen altijd onderin deze menu's
met de built-in effects bovenaan.
</p>

