<p>
Audacity 1.2 ondersteund <a href="http://www.ladspa.org/">LADSPA</a> en
<a href="nyquist.php">Nyquist</a> plug-ins uit zichzelf en VST plug-ins
met de optionele <a href="http://audacityteam.org/vst/">Audacity VST Enabler</a>.
</p>
<p>
Audacity 1.2.0 bevat reeds enkele LADSPA and Nyquist plug-ins samples in de
<b>Plug-ins</b> map.  Plaats de VST Enabler en andere plug-ins in deze map om ze met Audacity te gebruiken.  De eerstvolgende keer dat je Audacity opent, zullen de plug-ins in je Effect menu (of mogelijk Generate of Analyze) aanwezig zijn.  Plug-in effecten verschijnen in deze menus altijd onderin met de ingebouwde effecten bovenaan.
</p>

<p>
Audacity's Plug-in ondersteuning is aan vdrbetering onderhevig.  Laat het ons a.u.b. weten als je een VST Plug-in aantreft die schijnbaar in Audacity niet functioneert en dan proberen wij dit te herstellen. 
</p>

