<p>
Audacity ondersteund VST Plug-in effecten op de Macintosh en Windows systemen.
Er zijn Plug-ins te vinden die allerhande filtering kunnen doen, equalization, reverb, chorus en nog veel meer!  Een lijst met links waar je Plug-ins kunt downloaden of kopen zie je hieronder.
</p>

<p>
Audacity's Plug-in ondersteuning wordt altijd verbeterd.  Laat ons het a.u.b. weten als je een VST Plug-in ontdekt die schijnbaar in Audacity niet werkt, en dan proberen wij dat op te lossen. 
</p>

<p>
Om een VST Plug-in effect in Audacity te kunnen gebruiken, hoef je alleen maar in de Audacity map een nieuwe map aan te maken met de naam <b>VST</b>, als die map al niet aanwezig is, en drop je Plug-ins daarin!  De eerstvolgende keer dat je Audacity opent, zullen de Plug-ins beschikbaar zijn in je Effectenmenu.
</p>

