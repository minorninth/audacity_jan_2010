<h2>Változtatások az Audacity 1.2.3-as verziójában</h2>
<ul>
<li><p>
Windows: Javítva lett egy hiba, ami 50 percnél hosszabb felvételek esetén megállította vagy elrontotta a felvételt.
(Ez már részben javítva volt az 1.2.2-es verzióban is, azonban még nem m&#251;ködött minden rendszeren.)
</p></li>
<li><p>
Max OS X: Javítva lett egy nagy hiba, ami az Audacity véletlenszer&#251; leállását okozta néhány Mac rendszeren, f&#245;leg
lejátszás vagy felvételek készítésekor.
</p></li>
<li><p>
A Hangmagasság változtatása és a Tempó változtatása effektek megújultak az Olli Parviainen által készített SoundTouch könyvtár felhasználásával,
és ennek köszönhet&#245;en gyorsabbak és jobb min&#245;ség&#251;ek lettek.
</p></li>
<li><p>
A libsndfile frissítve lett az 1.0.11-es verzióra. Az Audacity most már képes a
Sound Designer II (SDII) fájlok importálására is.
</p></li>
<li><p>
Javítva lett egy hiba, ami a program lassulását okozta a Burkológörbe eszköz használatakor
</p></li>
<li><p>
Shift-Klikk a benyomott némítás vagy a szóló gombokon most már kikapcsolja a többi sáv némítását vagy szólózását.
</p></li>
<li><p>
A Nyquist b&#245;vítmények mostantól sztringeket is elfogadnak bemeneti adatként.
Ezenfelül minden Nyquist effekt ablak kapott egy "Debug" gombot, mellyel lehet&#245;ség nyílik az effekt kimenetének megtekintésére,
a hibakeresés megkönnyítése végett.
</p></li>
<li><p>
Ha mostantól egy Audacity projekten belül hiányzó fájl található, az Audacity csendet játszik be a hiányzó sáv tartalmaként.
Korábban el&#245;fordult, hogy az Audacity ilyenkor egy korábban használt hangsávot játszott le a hiányzó sáv helyett.
</p></li>
<li><p>
A monitor szintjelz&#245;i mostantól mindig nullázódnak, ha az audió ki/bemenet le lett állítva.
</p></li>
<li><p>
Új vagy frissített fordítások:
Olasz (it), Magyar (hu),
Ukrán (uk), Spanyol (es). Lengyel (pl), Egyszer&#251;sített kínai (zh),
Norsk-Bokmal (nb), Francia (fr), Orosz (ru).
</p></li>
</ul>
