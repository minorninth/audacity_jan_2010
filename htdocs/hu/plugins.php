<p>
Az Audacity 1.2 beépített <a href="http://www.ladspa.org/">LADSPA</a> and
<a href="nyquist.php">Nyquist</a> bõvítmény támogatással rendelkezik, valamint a VST bõvítményeket is
támogatja a kiegészítõ <a href="http://audacityteam.org/vst/">Audacity VST Enabler (engedélyezõ)</a> segítségevel.
</p>
<p>
Az Audacity 1.2.0 kiadástól kezdve mellékelten tartalmaz néhány LADSPA és Nyquist bõvítményt a
<b>Plug-ins</b> mappában. Tedd ide a VST Enabler és más bõvítményeket, amennyiben az Audacity-n belül szeretnéd használni azokat.
Az Audacity következõ futtatásakor az új bõvítmények már megtalálhatóak lesznek az Effekt (néhány esetben a Generálás vagy Analizálás) menüpont alatt.
A bõvítmények mindig a menü alján találhatóak meg, a beépített effektek pedig a tetején.
</p>

<p>
Az Audacity bõvítmény támogatása folyamatos fejlesztés alatt áll. Kérjük jelezd, amennyiben olyan VST bõvítményt találtál,
amelyik nem mûködik az Audacityvel és megpróbáljuk megoldani a problémát.
</p>
