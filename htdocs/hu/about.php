<p>
Az Audacity egy ingyenes audió szerkesztõ.
Képes hangfelvételre és -lejátszásra, WAV, AIFF, Ogg Vorbis, és MP3 fájlok importálására
sõt még többre is.
Használhatod a felvételeid szerkesztésére a Kivágás, Másolás, Beillesztés segítségével
(korlátlan visszavonási lehetõséggel), sávok keverésére, vagy akár a felvétel effektezésére is.
Ezenfelül beépített burkológörbe szerkesztõvel, könnyen testreszabható spektogram nézeti móddal és
frekvencia analízis üzemmóddal is rendelkezik, így az audió-jel analizálás sem okoz neki problémát.
A beépített effektek között megtalálható a Visszhang, Tempó módosítás, Zajszûrõ és
ezenfelül támogatja a VST és LADSPA bõvítmények használatát is.
</p>

