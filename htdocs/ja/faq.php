1. Windows: exeファイルはどのように使うのですか？<br>
</b></font>

<p>　ブラウザから、"このプログラムを上記の場所から実行する"を選びます。するとAudacityのセットアッププログラムが起動します。そうすればAudacityのプログラムファイルがインストールされ、スターとメニューに新しい項目ができるでしょう。
<p>
　もしあなたが既にそのファイルをダウンロードしてある場合は、それをダブルクリックすればインストールできます。
<p>
　もしあなたがインストーラーを使いたくないのであれば、ZIP圧縮ファイルとしてAudacityをダウンロードすることもできます。ZIPファイルはWinZipを使って解凍することができます。

