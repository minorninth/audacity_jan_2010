5. UNIX: 共通ライブラリlibwx_gtk.soを読み込むときにエラーが起こるのはなぜですか？<br></b></font>

<p><font size=+0>2つの原因が考えられます。
<br>- もしwxWindowsのRPMをインストールしたのであれば、あなたのシステムがそのライブラリを認識しているかどうか"ldconfig"でご確認ください。

<br><br>- もしあなたがwxWidgetsをソースからインストールしたのであれば、それは/usr/local内にインストールされたのかもしれません。しかし/usr/local/libは、ライブラリパスに設定されていないのでしょう。 "/etc/ld.so.conf"ファイル内に/usr/local/lib を追加してください。そして、"ldconfig"を実行してください。プログラムが"wx-config"ユーティリティを見つけることができるようするには、/usr/local/binも追加する必要があるかもしれません。<br>

