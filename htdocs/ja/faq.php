<font size=+1><b>

1. 2つのトラックをミックスするにはどうすればよいのですか？
</b></font>
<p>

Audacityは自動的にミックスします。あなたがしなければならないことは、プロジェクトメニューの中にある"インポートオーディオ"コマンドを使って、または単にAudacityにオーディオファイルをドラッグすることによって、２つのトラックを同じプロジェクトに取り込むことだけです。また、録音ボタンを押せば、Audacityは自動的に新しいトラックを生成します。

<p>

ミックスしたものを保存するには、プロジェクトをエクスポートすれば全トラックをミックスしたものが自動的にできますし、トラックを選択してプロジェクトメニューの中にある"クイックミックス"コマンドを使うこともできます。

<p>
<a name="a2"></a>
<font size=+1><b>
2. 録音したものからボーカルを除去することはできますか？
</b></font>
<p>

　ステレオで録音したものであれば、スタジオでどのように録音されたかにもよりますが、いくつかはボーカルを除去することができます。よく、ボーカルはちょうど中央に定位し、他の楽器は中央以外に定位するように配置されています。左チャンネルの波形から右チャンネルの波形を引き算すれば、他の楽器は残したまま、ボーカルだけが完全に消去できるでしょう。
<p>

<b>ただし、これは必ずしも成功するわけではありません。</b>
<p>
　これをAudacityで試みるには、ステレオのデータをインポートし、トラックポップアップメニュー(トラック名の横にある小さな下矢印)より"ステレオトラックを分割する"を選びます。次に、下のトラック(右チャンネル)を選び、"Invert"エフェクト(エフェクトメニューの中にある)を使って正負を反転します。最後に、トラックポップアップメニューを使って、両方のチャンネルをモノラルにし、それらをミックスします。もし運が良ければ、ボーカルは消え去ってしまうでしょう。


<p>
<a name="a3"></a>
<font size=+1><b>
3. 重ねて録音していくとき、２つのトラックが同期しないのはなぜですか？
</b></font>
<p>
　これは正常です。Audacityが再生をしてからスピーカーから音が出るまでの間に少し遅れがあるからです。Audacityは自動的にこの遅れを補正しようとはしませんので、手動で補正する必要があります。
<p>
　この遅れを補正するには、タイムシフトツールでひとつのトラックをスライドして整列させてください。このツールは録音したデータを再生しているときでも使うことができます。
<p>

<a name="a4"></a>
<font size=+1><b>
4. なぜAudacityにはMP3エンコーダーが付いていないのですか？
</b></font>
<p>

　不運なことに、MP3ファイルをエンコードするアルゴリズムと生成するアルゴリズムは、特許がとられているのです。ゆえに<b>Audacity</b>は、使用料を支払わずに、また多くの国の法律に違反せずに、MP3エンコーダーをつけることはできないのです。
<p>
　解決策として、Audacityには他のMP3エンコーダを使う機能がついています。
あなたはAudacityとは別々にダウンロードしなければなりません。MP3エンコーダーに課せられた制限に従わなければならいことを確認するのはあなたの責任です。

<p>
　さらに詳しいことは、オンラインヘルプに載っているMP3エクスポートのページをご覧ください。

<p>

<a name="a5"></a>
<font size=+1><b>
5. RealAudioや他のストリーミングオーディオを録音することはできますか？
</b></font>
</p><p>

　いいえ、自動的にはできません。RealOne Player、Windows Media Player、Quicktimeに送られてくるほとんどのストリーミングオーディオは、録音できないように設計されています。
</p><p>
　とは言え、サウンドカード上でオーディオ出力端子からオーディオ入力端子(又はマイク端子)へループさせれば、コンピューターで再生できるものは何でも録音することができます。
</p><p>
　1/8インチステレオミニ to 1/8インチステレオミニのケーブルを使ってください。一方をオーディオ出力端子(スピーカーをつないでいるところ、普通は緑色です)に接続してください。もう一方をオーディオ入力端子(普通は赤い色です)に接続してください。
</p><p>
　そうすればAudacityはコンピューターで再生するものは何でも録音できます。Audacityの録音ボタンを押してください。それから、ストリーミングオーディオの再生ボタンを押してください。

</p><p>

<a name="a6"></a>
<font size=+1><b>
6. 大変です、録音したのに何もとれていません！
</b></font>
</p><p>
　Audacityは、OSで設定されているデフォルトの入力ソースから録音します。もし複数の入力ソース(内臓マイク、外部マイク、Line-in端子、オーディオCDなど)を持っているのであれば、これらのうちひとつを選ばなければなりません。例えば、マイクから録音しようとしているのに、入力ソースがLine-inに設定されていれば、録音しても何もとれていないでしょう。
</p><p>
　もしWindowsをお使いでしたら、タスクバーのボリュームアイコン(通常は画面の右下の隅にある)を右クリックして入力ソースを選ぶことができます。
</p><p>
　もしMac OS 9をお使いでしたら、サウンドコントロールパネルを使ってください。Mac OS Xの場合は、System Preferenceの中にあるサウンドパネルを使ってください(10.2以上の場合はインプットタブがあります)。

</p><p>
　Linuxの場合は、"xmixer"や"kmix"などのミキサーを使ってください。
</p><p>

<a name="a7"></a>
<font size=+1><b>
7. ひとつのファイルを複数のトラックに分割するにはどうすればよいですか？
</b></font>
</p><p>
　ひとつのファイルに複数の曲を収録することもあるでしょう。もしがそれをCDに別々のトラックとして焼きたいのであれば、それぞれの曲に分割されたファイルを作る必要があります。
</p><p>
　Audacityを使って録音したものを複数のトラックに分割するにはたくさんの方法があります。ここにいくつかの方法を載せますので、お試しください。
</p><p>
方法1: 各セクションをエクスポートする
</p><ul>
<li>最初のトラックに相当する部分を選択します。</li>
<li>ファイルメニューから"Waveファイルとしてエクスポート"を選び、ハードディスクに保存します。
</li><li>残りのトラックも同様に繰り返します。
</li></ul>
<p>
方法2: 削除して元に戻す
</p><ul>
<li>最初のトラックに不要なものをすべて削除します。
</li><li>ファイルメニューから"Waveファイルとしてエクスポート"を選び、最初のトラックをハードディスクに保存します。
</li><li>編集メニューから"元に戻す"を選び、残りのトラックがすべて復元するまで元に戻し続けます。(Audacityは何度でも元に戻すことができます。)
</li><li>残りのトラックも同様に繰り返します。
</li></ul>
<p>
方法3: 分割してエクスポートする
</p><ul>
<li>最初のトラックに相当する部分を選択します。
</li><li>編集メニューから"分割する"を選び、選択した部分を別のトラックへ移動します。
</li><li>すべての曲が別々のトラックになるまでこれを繰り返します。もし間違えたら、いつでも元に戻すことができるということを忘れないでください。
</li><li>"Waveファイルとして選択範囲をエクスポート"を選び、各トラックを別々のファイルにエクスポートします。各トラックをすばやく選択するには、そのラベルをクリックしてください(波形の左部分でタイトルの下部分のエリアならどこでもかまいません)。
</li></ul>
