2. 誰がAudacityを開発したのですか？<br>
</b></font>

<p>
　Audacityは、アメリカのピッツバーグにあるCarnegie Mellon大学を出ているDominic Mazzoniによって、1999年の秋に作り始められました。彼はRoger Dannenberg教授とともに研究をしていましたが、彼らはオーディオ解析アルゴリズムを可視化するツールが必要となりました。その後、このプログラムは一般的なオーディオエディタとして開発され、他の人々も協力し始めました。
<p>
　今、Audacityは、世界中の人々が共同で作業することができるように、Sourceforgeというオンラインサイトを使って開発されています。さらに詳しいことは<a href="http://www.sourceforge.net">sourceforge.net</a>をご覧ください。多くの人々がAudacityに貢献しています。そして、絶えず進歩し続けています。

