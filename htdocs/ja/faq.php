1. Audacityは本当にフリーですか？　なぜ？<br>
</b></font>

<p>
　はい。Audacityは無料であるばかりでなく、どのような用途に使うことも自由です。
<p>
　Audacityは<a href="http://www.gnu.org/copyleft/gpl.html">General Public License (GPL)</a>のもとで配布されます。あなたは個人目的でも商用目的でもこのプログラムを使うことができます。あなたはそれを譲渡することも販売することもできます。このプログラムのソースコードはWeb上で自由に入手することが可能です。そしてあなたは用途に合わせて自由にソースを変更することができます。しかしながら、あなたが行った変更はすべて、GPLの規約に沿って公開されなければなりません。
<p>
　AudacityはwxWidgetsというLGPLのもとで配布されているソフトウェアライブラリーを使ってビルドされています。詳しくは、<a href="http://wxwidgets.org/">wxwidgets.org</a>をご覧ください。
<p>
　Audacityの製作者たちは、さまざまな理由からGPLのもとで公開することを決めました。私たちの中にはあまり寛大でない人もいれば、ソフトウェアは自由であるべきであるという信念をもつ人もいれば、また無料と商用のソフトウェアの両方が必要であると考える人もいました。
<p>
　Audacityが無料であるひとつの理由は、より人気のあるソフトになってほしいためです。私たちの多くは、お金を払ってAudacityを使っている1000人のユーザーよりも、無料でAudacityを使っている100万人のユーザーを見たいと思っています。ほとんどのユーザーは、無料で何かをもらったときは、より親密に感じるでしょう。
<p>
また別の理由は、共同作業を円滑にするためです。もしAudacityがシェアウェアだったとしたら、世界中の多くの人々がコード、バグの除去、文書、グラフィックスに貢献することはできそうにありません。

