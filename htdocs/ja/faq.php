4. バグを見つけました。どうすれば！？<br>
</b></font>
<p>

　バグを報告する際に最も重要なことは、できるだけ具体的に伝えることです。私たちがそのバグを再現できるよう、十分な情報をください。そうでなければ、そのバグを取り除くことはできそうにありません。
<p>
　ご使用のオペレーティングシステム(例えばWindows98, MacOS 9.1, RedHat Linux 7.1など)と、関係あると思われるあなたのコンピューターの情報を教えてください。
<p>
　それから、あなたはそのバグを再現することができますか？　もしバグがいつも起こるのであれば、バグを起こしている部分の正確なイベントの記録をください。もしエラーメッセージが表示されたら、どうかエラーメッセージの正確なテキストを送ってください。
<p>

　私たちはすべてのバグを踏み潰したいとおもっています。バグの追跡を手助けするために時間をとっていただければ幸いです。

