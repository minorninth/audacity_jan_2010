<h2>Audacity 1.2 変更点</h2>

　ここには、以前の完成版である Audacity 1.0.0 からの主な変更点が書かれています。ベータ版ユーザーの方は、<a href="<?php print "betanotes.php$langQueryStr"; ?>">1.1.0 からの変更点</a>をご覧ください。

<h3>プロ品質のオーディオ</h3>

<ul>
<li>
Audacityは、今では24ビットと32ビットの浮動小数点方式のサンプリング値を使っています。ひとつのプロジェクト内に、異なるサンプリングレートとフォーマットのトラックを混在させることができます。そしてAudacityは、必要があればいつでも自動的にサンプリングレートを変換します。すべての変換において、高品質なディザとリサンプリングが適用されます。
</li>

<li>
Audacityのオーディオ入出力が進歩しました。Audacityは、今では2つより多くのチャンネルを同時に録音することができます。バッファリングも進歩し、音飛びやメモリ不足の可能性はより少なくなりました。
</li>
</ul>

