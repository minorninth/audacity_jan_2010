<p>
　Audacity1.2は<a href="http://www.ladspa.org/">LADSPA</a>と<a href="nyquist.php">Nyquist</a>プラグインをサポートしています。また、オプションの<a href="http://audacityteam.org/vst/">Audacity VST Enabler</a>を使えば、VSTプラグインを使うことができます。
</p>

<p>
Audacity 1.2.0には、いくつかのLADSPAとNyquistプラグインが<b>Plug-ins</b>フォルダ内に付属しています。Audacityでさらに多くのプラグインを使うには、VST Enablerと追加するプラグインファイルをこのフォルダ内に入れてください。Audacityを再起動すると、それらのプラグインが"効果"メニュー(又は"製作"メニューか"解析"メニュー)に追加されているでしょう。プラグインエフェクトはいつもメニューの下に表示され、内蔵エフェクトが上に表示されます。
</p>

<p>
　Audacityのプラグインサポートは進歩しています。もし、Audacityでうまく動かないVSTプラグインを見つけましたら、どうか私たちに知らせてください。私たちはそれを直すようにするでしょう。
</p>

<p>
　ここにVSTプラグインをダウンロード又は購入できるサイトをいくつか紹介します。
</p>

