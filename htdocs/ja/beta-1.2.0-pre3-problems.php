<h2>既知の問題点</h2>

<p>
　Audacity1.2.0では、次に示すバグがまだ残っています。
</p>

<ul>
  <li><p>AudacityはMIDIファイルを読み込み表示することができますが、再生したり編集したりすることはできません。
    </p></li>
  <li><p>一部のLinux上では、再生と録音を同時にすると、録音が遅くなります。
</p></li>
  <li><p>Mac OS Xのみ：国際的な文字(アクセントマークなど)を含むフォルダ内にあるファイルやフォルダを開くことはできません。アクセントマークを含むファイルそのものは開くことができます。アクセントマークを含むフォルダの中を開こうとすると問題を引き起こすのです。
    </p></li>
  <li><p>96kHzのサンプリング周波数のデータでは、ピッチチェンジエフェクトをかけると異常終了することがあります。</p></li>
</ul>
