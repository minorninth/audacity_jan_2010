<p>
　Audacityはフリーのオーディオ編集ソフトです。録音、再生はもちろんのこと、WAV, AIFF, Ogg Vorbis, MP3などさまざまなファイル形式での読み込み・書き込みに対応しています。切り取り・コピー・貼り付けは何度でも元に戻すことができますし、複数のトラックをミックスしたり、録音したものにエフェクトを付加したりもできます。また、内蔵エンベロープエディタ、スペクトログラムモード、及び周波数解析ウィンドウを備えています。内蔵エフェクトには、エコー・テンポチェンジ・ノイズ除去なども備えています。さらにVSTやLADSPAのプラグインエフェクトを使うこともできます。
</p>

