　Audacity 1.2.0-pre2で見つかったバグをすべて直しましたので、2～3日のうちに1.2.0-pre3を公開する予定です。また、ついにVST Enablerに対応しました。他にもニュースがあります。

<p>
　Audacityは、ラスベガスにて11月17日～20日に行われる<a href="http://www.comdex.com/">COMDEX</a>に、<a href="http://www.oreilly.com/">O'Reilly</a>が送ることのできるオープンソースプログラムとしてノミネートされました。詳しいことは、またAudacityに投票するには、<a href="http://www.oreillynet.com/contest/comdex/">こちら</a>のページをご覧ください。投票は10月31日に終了します。
</p>

<p>
　<a href="http://audacityteam.org/wiki/">Audacity Wik</a>iは、Audacityに関することなら何でも書き込むことのできる公のウェブサイトです。現在、技術集、文書、関連リンク集、リクエスト場があります。誰でも簡単に貢献することができます。ページ下の方にあるリンクをクリックして、あなた自身のコンテンツを書き込んでください。面白くて役立つサイトだと感じていただければ幸いです。
</p>

