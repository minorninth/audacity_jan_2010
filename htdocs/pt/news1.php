<?php BoxTop('5 de Junho de 2002: Lançadas as versões 1.0.0 e 1.1.0'); ?>

<p>
O time de desenvolvimento do Audacity anuncia o lançamento da versão 1.0 e 1.1 simultaneamente.</p>

<p>
A versão 1.0 é a versão final e estável do Audacity original, um editor de som simples e fácil de usar, apto a gravar, mixar e editar sons com qualidade de CD. O programa está disponível, em inglês apenas, para Windows, MacOS e Linux.
</p>

<p>
A versão 1.1 é o primeiro preview da nova geração do Audacity, que incluirá suporte a alta-qualidade em som, uma interface mais aprimorada e suporte a diferentes linguagens, incluindo Português do Brasil. Esta versão está em desenvolvimento, e seu uso deve se restringir apenas a testes.
</p>

<p>
É possível instalar as duas versões do Audacity no mesmo computador.
</p>

