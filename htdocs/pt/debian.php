<p>
O Audacity é parte das distribuições <b>sid</b> e <b>woody</b> do Debian (em testes). Sua instalação é simples, apenas digite:
</p>
<xmp>$ apt-get update && apt-get install audacity</xmp>
<p>
Para quem ainda usa a versão Potato (Debian 2.2) o DEB pode ser baixado da <a href="http://sourceforge.net/project/showfiles.php?group_id=6235">área de arquivos do SourceForge</a>. VocÊ também pode acrescentar a seu arquivo 
/etc/apt/sources.list:
</p>
<xmp>
deb http://audacity.sourceforge.net potato/
</xmp>
<p><b>Aviso: </b>Os DEBs para a versão Potato do Debian já não estão sendo mais lançados.</p>

