<h2>Falhas relatadas:</h2>

<p>
As seguintes falhas já foram relatadas até agora no Audacity 1.2.0-pre3:
</p>

<ul>
  <li>O Audacity pode importar e exibir arquivos MIDI, mas não consegue tocar ou editá-los.
    <p>
  <li>Gravaçao em modo "full duplex" em alguns sistemas Linux pode tornar o som mais lento que o normal.    <p>
  <li>No MacOS X: O Audacity não trabalha com pastas nomeadas com caracteres não-ingleses (acentos, por exemplo). Os arquivos e projetos nomeados com estes caracteres funcionam normalmente.    <p>
   <li>Apenas em Windows: O instalador que acompanha a versão 1.2.0-pre3 sobregrava a versão 1.0 do programa, mas não substitui versões beta anteriores.
    <p>
</ul>
