<h2>Falhas relatadas:</h2>

<p>
As seguintes falhas já foram relatadas até agora no Audacity 1.2.1:
</p>

<ul>
  <li>O Audacity pode importar e exibir arquivos MIDI, mas não consegue tocar ou editá-los.
    <p>
  <li><p>O efeito de alteração do Pitch pode travar o sistema se executado em faixas com taxa de amostragem de 96 kHz.</p></li>
  <li><p>No Windows: O programa se encerra ao utilizar o comando "Ajustar Taxa" em uma faixa de áudio.</p></li>
  <li>Gravaçao em modo "full duplex" em alguns sistemas Linux pode tornar o som mais lento que o normal.    <p>
  <li>No MacOS X: O Audacity não trabalha com pastas nomeadas com caracteres não-ingleses (acentos, por exemplo). Os arquivos e projetos nomeados com estes caracteres funcionam normalmente.    <p>
 <li>No MacOS X: Ao iniciar, o Audacity pode não iniciar corretamente dispositivos de som USB. Este problema pode ser contornado executando o "Audio MIDI Setup" e re-ajustando os valores incorretos.   <p>
   <li>A caixa de diálogo "Sobre..." não exibe corretamente caracteres não-ocidentais. Não afeta a acentuação.    <p>
</ul>
