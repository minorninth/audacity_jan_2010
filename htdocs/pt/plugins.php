<p>
O Audacity aceita plug-ins de efeitos VST nas versões para Windows e Macintosh.
Você pode encontrar plug-ins que fazem todo tipo de filtragem, equalização, reverberação, coro e muito mais! 
p>

<p>
O suporte a plug-ins do Audacity está sendo melhorado. Se você conhece algum plug-in VST que não funciona como deveria no Audacity, escreva-nos e tentaremos corrigi-lo. 
</p>

<p>
Para usar um plug-in VST no Audacity crie uma pasta com o nome <b>VST</b> na pasta do Audacity e arraste os arquivos de plug-in para dentro dela. Na próxima vez que o programa for aberto os novos efeitos já estarão disponíveis no menu Efeitos.
</p>

