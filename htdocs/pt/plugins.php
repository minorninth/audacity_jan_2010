<p>
O Audacity aceita plug-ins de efeitos VST nas versões para Windows e Macintosh.
Você pode encontrar plug-ins que fazem todo tipo de filtragem, equalização, reverberação, coro e muito mais! 
p>

<p>
O suporte a plug-ins do Audacity está sendo melhorado. Se você conhece algum plug-in VST que não funciona como deveria no Audacity, escreva-nos e tentaremos corrigi-lo. 
</p>

<p>
Para usar um plug-in VST no Audacity 1.0 crie uma pasta com o nome <b>VST</b> na pasta do Audacity e arraste os arquivos de plug-in para dentro dela. Na próxima vez que o programa for aberto os novos efeitos já estarão disponíveis no menu Efeitos.
</p>

<p>
O Audacity 1.2.0 tem suporte nativo a plug-ins <a href="http://www.ladspa.org/">LADSPA</a> e <a href="nyquist.php">Nyquist</a> e a plug-ins VST com o pacote opcional <a href="http://audacityteam.org/vst/">"Audacity VST Enabler"</a> (Habilitador para VST do Audacity).
</p>
<p>
O Audacity 1.2.0 vêm com alguns plug-ins de demonstração LADSPA e Nyquist na pasta <b>Plug-ins</b>. Copie o "VST Enabler" e todos os plug-ins que adquirir ou copiar da Internet nesta pasta para poder utiliza-los. Na próxima vez que o Audacity for inicializado os novos plug-ins aparecerão no menu Efeitos (alguns efeitos se instalam no menu Gerar ou Analisar).  Os plug-ins novos aparecerão na parte inferior dos menus.</p>

