<h2>Falhas reportadas até agora</h2>

<p>
As seguintes falhas já foram detectadas e deverão ser corrigidas na próxima versão de testes:
</p>

<ul>
<li>O uso de plug-ins VST plugin foi desabilitado por problemas legais de licenciamento. O suporte a estes plug-ins deverá estar habilitado na versão final do Audacity 1.2.0.</li>

<li>O manual do usuário ainda está em construção. Neste momento a atualização está próxima de 75% concluída (em inglês). Somente após a conclusão do manual em inglês poderão haver versões em outras línguas. Não há ainda nenhum tradutor disponível para o manual em português do Brasil. Novos tradutores serão muito bem-vindos!</li>

<li>O programa pode falhar se houver tentativa de edição do áudio ao mesmo tempo da reprodução ou gravação de uma nova faixa de som.</li>

<li>(Apenas no MacOS X) Ao selecionar Sair (Quit), e depois clicar em Cancelar (Cancel), o comando Sair (Quit) e o menu Preferências (Preferences) param de funcionar.</li>

<li>Os controles de volume voltam a suas posições padrão a cada inicialização do programa.</li>

<li>O Audacity pode importar e exibir arquivos do tipo MIDI mas não é capaz de editá-los ou reproduzi-los.</li>

<li>O programa falha ao converter áudio a uma taxa que seja 12 vezes maior ou menor que a original.</li>

<li>Gravação em modo full-duplex (gravação e reprodução simultaneamente) em alguns sistemas rodando Linux faz com que o som seja gravado mais lentamente.</li>

<li>Apenas para MacOS X:  O comando "Find Zero Crossings" tem o atalho exibido nos menus como "Command-Z". No entanto, seu atalho é "Z". Os atalhos para Ajuda Online (Online Help) e Abrir (Open) estão em conflito.</li>

<li>Apenas para MacOS X: Para áudio com amplitude igual a zero, o programa exibe apenas uma fina linha vertical no lugar da onda sonora.</li>
</ul>
