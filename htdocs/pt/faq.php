<font size=+1><b>
3. When I try to record harmonies with myself,
              why are the two tracks out of sync?
</b></font>
<p>
This is normal and it happens because there is a small delay
between when Audacity starts playing sound and when it actually
reaches your speaker.  Audacity does not automatically try to
correct for this delay, and you must do it manually.
<p>
To correct this delay, use the
Time Shift tool to slide
one of the tracks over until they line up.  Note that you can
use the tool while you are listening to the recording.

<p>

<a name="a4"></a>
<font size=+1><b>
4. Por que o Audacity não salva arquivos em formato MP3?
</b></font>
<p>

Infelizmente,as bibliotecas de exportação para o formato comprimido MP3 são 

patenteadas e protegidas por leis de copyright (direitos de cópia). O Audacity não 

poderia ser entregue com um compressor de MP3 sem cobrar por isso (já que seríamos 

obrigados a licenciar cada cópia do compressor), ou violar a lei em alguns países, 

incluindo o Brasil.
<p>
Na falta de um compressor de código-fonte livre para a exportação neste formato, o 

Audacity ainda permite que sejam utilizadas bibliotecas de compressão de 

terceiros, que devem ser adquiridas separadamente.
<p>



<a name="a5"></a>
<font size=+1><b>
5. É possível salvar arquivos do RealAudio ou outros formatos de Fluxo Contínuo?
</b></font>
</p><p>

Não de maneira automática. Muitos destes formatos foram projetados para tornar 

impossível a gravação no seu computador, preservando assim os seus direitos 

autorais. Estes tipos incluem Windows Media Player - .wma, Quicktime - .mov e 

RealPlayer - .ra . 
</p><p>
Ainda assim, é possível a gravação de qualquer tipo de som que seu computador 

possa reproduzir, apenas adquirindo um cabo que permita que a saída de áudio de 

sua placa de som (normalmente marcada como "Line-out") seja conectada diretamente 

na sua entrada de áudio, marcada como "Line-in".
</p><p>
Agora, o Audacity deve poder gravar qualquer som enquanto este é reproduzido pelo 

seu computador.Apenas pressione "Gravar" no Audacity.
</p><p>

<a name="a6"></a>
<font size=+1><b>
6. Clicando "Gravar" nenhum som é gravado!
</b></font>
</p><p>
O Audacity grava a partir do dispositivo padrão selecionado para gravação em seu 

sistema operacional. Se seu sistema dispuser de múltiplos dispositivos de entrada 

de som, como microfones, CDs de áudio, etc, você deverá selecionar aquele ao qual 

o Audacity deve se conectar para capturar o som.
</p><p>
No Windows, é possível ajustar estas configurações a partir do Controle de Volume, 

na barra de tarefas, ao lado do relógio.
</p><p>
No MacOS, utilize o Sound Control Panel. Na versão X, utilize o Sound Panel, na 

pasta System Preferences. (testado apenas na versão em inglês. Caso alguém tenha 

tentado nas versões de OS em português, entre em contato conosco!)
</p><p>
No Linux, use um aplicativo de mixagem (mixer), como o "xmixer" ou "kmix".
</p><p>
<a name="a7"></a>
<font size=+1><b>
7. Como transformar um único som em diversas faixas?
</b></font>
</p><p>
Caso você tenha mixado um único projeto com múltiplas faixas e deseje gravá-las 

num CD como faixas separadas, será necessário exportar o projeto em cada faixa que 

se deseje gravar, criando assim um arquivo para cada faixa.
</p><p>
No Audacity há várias maneiras de se executar esta tarefa, por exemplo:
</p><p>
Método 1: Exportando a seleção
</p><ul>
<li>Selecione a área correspondente à primeira faixa. 
</li><li>Selecione "Exportar seleção como..." no menu "Arquivo" e salve.
</li><li>Repita o procedimento para cada faixa restante.
</li></ul>
<p>
Método 2: Apagar e Desfazer
</p><ul>
<li>Selecione e apague tudo <i>exceto</i> o conteúdo da primeira faixa a ser 

exportada.
</li><li>Selecione "Exportar como..." no menu "Arquivo" e exporte a faixa.
</li><li>No menu "Editar" selecione "Desfazer" até obter todo o áudio oiriginal. 

(O Audacity tem "desfazer" ilimitados)
</li><li>Repita o procedimento para cada faixa restante.
</li></ul>
<p>
Método 3: Separar e Exportar
</p><ul>
<li>Selecione a área correspondente à primeira faixa.
</li><li>Selecione no menu "Editar" - "Cortar em nova faixa" 
</li><li>Repita o procedimento para cada faixa restante. 
</li><li>Selecione as faixas recem-criadas e exporte uma a uma utilizando 

"Exportar seleção como..." 
</li></ul>
