<font size=+1><b>
2. Quem desenvolve o Audacity?<br>
</b></font>

<p>

O Audacity nasceu em 1999 quando Dominic Mazzoni era graduando 
da Carnegie Mellon University em Pittsburgh, EUA.
Trabalhando em um projeto de pesquisa com seu orientador, Professor
Roger Dannenberg, desenvolveu-se uma ferramenta que os permitia
visualizar algoritmos de análise de áudio. Com o tempo, o programa
se tornou um pequeno editor de áudio e outras pessoas começaram a 
desenvolvê-lo.
<p>
Hoje, o Audacity é desenvolvido utilizando os recursos da Sourceforge, 
um serviço online que permite que pessoas de todo o mundo contribuam 
em programas de código-fonte aberto. Veja mais em <a 

href="http://www.sourceforge.net"
>sourceforge.net</a>.
