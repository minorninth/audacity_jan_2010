<font size=+1><b>
3. When I try to record harmonies with myself,
              why are the two tracks out of sync?
</b></font>
<p>
This is normal and it happens because there is a small delay
between when Audacity starts playing sound and when it actually
reaches your speaker.  Audacity does not automatically try to
correct for this delay, and you must do it manually.
<p>
To correct this delay, use the
Time Shift tool to slide
one of the tracks over until they line up.  Note that you can
use the tool while you are listening to the recording.

<p>

<a name="a4"></a>
<font size=+1><b>
4. Por que o Audacity não salva arquivos em formato MP3?
</b></font>
<p>

Infelizmente,as bibliotecas de exportação para o formato comprimido MP3 são patenteadas e protegidas por leis de copyright (direitos de cópia). O Audacity não poderia ser entregue com um compressor de MP3 sem cobrar por isso (já que seríamos obrigados a licenciar cada cópia do compressor), ou violar a lei em alguns países, incluindo o Brasil.
<p>
Na falta de um compressor de código-fonte livre para a exportação neste formato, o Audacity ainda permite que sejam utilizadas bibliotecas de compressão de terceiros, que devem ser adquiridas separadamente.
<p>



<a name="a5"></a>
<font size=+1><b>
5. É possível salvar arquivos do RealAudio ou outros formatos de Fluxo Contínuo?
</b></font>
</p><p>

Não de maneira automática. Muitos destes formatos foram projetados para tornar impossível sua gravação no seu computador, preservando assim os direitos do autor. Estes tipos de arquivo incluem os do Windows Media Player, Quicktime e RealPlayer. 
</p><p>
Ainda assim, é possível a gravação de qualquer tipo de som que seu computador possa reproduzir, apenas adquirindo um cabo que permita que a saída de áudio de sua placa de som (normalmente marcada como "Line-out") seja conectada diretamente na sua entrada de áudio, marcada como "Line-in".
</p><p>
Agora, o Audacity deve poder gravar qualquer som enquanto este é reproduzido pelo seu computador.Apenas pressione "Gravar" no Audacity.
</p><p>

<a name="a6"></a>
<font size=+1><b>
6. Clicando "Gravar" nenhum som é gravado!
</b></font>
</p><p>
O Audacity grava a partir do dispositivo padrão selecionado para gravação em seu sistema operacional. Se seu sistema dispuser de múltiplos dispositivos de entrada de som, como microfones, CDs de áudio, etc, você deverá selecionar aquele ao qual o Audacity deve se conectar para capturar o som.
</p><p>
No Windows, é possível ajustar estas configurações a partir do Controle de Volume, na barra de tarefas, ao lado do relógio.
</p><p>
No MacOS, utilize o Sound Control Panel. Na versão X, utilize o Sound Panel, na pasta System Preferences. (testado apenas na versão em inglês. Caso alguém tenha tentado nas versões de OS em português, entre em contato conosco!)
</p><p>
No Linux, use um aplicativo de mixagem (mixer), como o "xmixer" ou "kmix".
</p><p>
<a name="a7"></a>
<font size=+1><b>
7. How do I split a single file into multiple tracks?
</b></font>
</p><p>
Sometimes you might end up with a recording of multiple songs that are
in a single file.  If you want to burn these songs onto a CD as separate
tracks, you need to create a separate file for each song.
</p><p>
There are lots of ways to split a recording into multiple tracks using
Audacity.  Here are a few methods you can try:
</p><p>
Method 1: Export Selection
</p><ul>
<li>Select the region of audio corresponding to the first track.
</li><li>Choose "Export Selection as WAV" from the File menu, and save it to
    disk.
</li><li>Continue for all remaining tracks.
</li></ul>
<p>
Method 2: Delete and Undo
</p><ul>
<li>Delete everything <i>except</i> for the first track.
</li><li>Choose "Export as WAV" from the File menu, and save the first track
    to disk.
</li><li>Select "Undo" from the Edit menu.  Keep undo-ing until you have the rest
    of your audio back.  (Audacity has unlimited undo.)
</li><li>Continue for all remaining tracks.
</li></ul>
<p>
Method 3: Split and Export
</p><ul>
<li>Select what you would like to be the first track.
</li><li>Choose "Split" from the Edit menu, which moves the selection into
    a separate track within Audacity.
</li><li>Keep doing this until all of the songs are in separate tracks.
    Don't forget that you can Undo at any point if you make a mistake.
</li><li>Now go back through and use "Export Selection as WAV" to export each
    track to a separate file.  To quickly select each track, click on its
    label (anywhere in the area to the left of the waveform, underneath
    the title).
</li></ul>
