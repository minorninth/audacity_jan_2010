1. Windows: O que faço com o arquivo .EXE?<br>
</b></font>

<p>De seu navegador, clique no arquivo com o botão esquerdo. Ao ser perguntado 

"Deseja abrir o arquivo ou salvá-lo no computador?" escolha "Abrir" (instruções 

para Internet Explorer 6). O programa de instalação será copiado para seu 

computador e aberto em seguida, instalando o Audacity em seu sistema. 
(Sistemas anti-vírus podem proibir a abertura de arquivos .EXE a partir de seu 

navegador. Neste caso, selecione a opção "Salvar" e abra o arquivo clicando duas 

vezes em seu ícone na pasta selecionada anteriormente como destino.)
<p>
É possível copiar o arquivo em formato compactado ZIP, apenas atente para o uso 

necessário do WinZip.
