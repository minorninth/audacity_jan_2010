<a name="a2"></a>
<font size=+1><b>
2. Posso remover os vocais de uma gravação?
</b></font>
<p>

Em algumas gravações é possível remover os vocais, devido à forma como estas foram 

mixadas. Normalmente, o vocal é mixado para estar exatamente no centro das faixas, 

enquanto os outros instrumentos são mixados para estarem um pouco mais à direita 

ou à esquerda dos canais. Subtraindo o canal direito do esquerdo, os vocais devem 

ser completamente apagados, sem prejuízo dos outros sons.
<p>

<b>Atenção, esta técnica só funciona em algumas gravações!!</b>
<p>
Para tentar, no Audacity, importe algum áudio estéreo, e clique na setinha abaixo, 

à esquerda da faixa selecionada. Selecione "Separar faixas de estéreo" (Split 

Stereo Track). Selecione a faixa mais abaixo - a da direita - e aplique o efeito 

"Inverter verticalmente" (Invert). Transforme todos os canais em mono e junte-os 

com o comando "Quickmix". Se a técnica funcionar, os vocais serão removidos.<p>



