5. Unix: Problemas ao carregar a biblioteca compartilhada 

libwx_gtk.so?<br></b></font>

<p><font size=+0>PRoblemas ao carregar esta biblioteca normalmente têm duas 

razões:
<br>- Se você instalou o RPM wxWindows, assegure-se de haver executado o 

