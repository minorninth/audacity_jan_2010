<h2>Known Problems</h2>

<p>
The following are known bugs in Audacity 1.2.1:
</p>

<ul>
  <li><p>Audacity can import and display MIDI files, but they cannot be played
    or edited.
    </p></li>
  <li><p>The Change Pitch effect may crash when used on a track with a
    sample rate of 96 KHz.</p></li>
  <li><p>Windows only: Audacity crashes when using the Time Track
    "Set Rate" command.</p></li>
  <li><p>Linux:  Recording in full duplex (play existing tracks while
    recording) on some systems causes mono recordings to sound
    slowed-down.</p></li>
  <li><p>MacOS X: Starting Audacity may cause the settings for some USB
    audio devices to be set incorrectly.  This can be fixed by changing
    the settings back to normal with Apple's "Audio MIDI Setup" program
    while Audacity is still running.</p></li>
  <li><p>MacOS X: Audacity cannot work with files or folders that
    are contained inside folders with international characters
    (accent marks, etc.) in their names.  Note that files with
    accented characters work, and Audacity projects with
    accented characters work.  Only files inside of folders
    with accented characters in their names will cause problems.
    </p></li>
  <li><p>The About window does not display correctly in languages with
    non-Western character sets.</p></li>
</ul>
