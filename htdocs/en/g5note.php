Note: Audacity 1.2.0-pre3 is apparently not compatible with the G5.
If you have a G5 or an audio device that is not supported in
version 1.2.0-pre3, <a href="http://audacityteam.org/mac/">see this page</a>
for more information.  We now have a test version of Audacity that should
work on all of these systems, but we are still working on a final build.
