<p>
Warning: the following version of Audacity is beta quality!
It contains dozens of new features, including a new toolbar,
automatic resampling, support for 32-bit samples, and pan and
gain sliders in every track.  It supports many languages other
than English.  But it is unfinished, the documentation is
incomplete, and it may contain serious bugs.
</p>
<p>
If you are interested in experimenting with the new features, we
encourage you to download this version and give it a try.
If you do, please send us feedback.  Let us know what works,
what doesn't, and what new features you would like to see.
You can have this version and Audacity 1.0 on your computer at
the same time.
</p>
<p>
Note:
Audacity 1.1.3 cannot read or write project files saved by any other
version of Audacity, including 1.1.1 or 1.1.2.  You must export all
of your audio to standard formats like WAV, AIFF, or MP3 before using
version 1.1.3.
</p>
