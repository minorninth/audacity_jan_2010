<td valign=top bgcolor=#eeeeee>
<p>
Audacity 1.0 supports VST Plug-in effects on the Macintosh and Windows platforms.
You can find Plug-ins that do all sorts of filters, equalization, reverb,
chorus, and more!  A list of places to download or purchase Plug-ins
is below.
</p>
<p>
To use a VST Plug-in effect in Audacity 1.0, just create a folder called
<b>VST</b> in your Audacity folder, if you don't already have one, and
drop your Plug-ins there!  The next time you open Audacity, the Plug-ins
will be in your Effects menu.
</p>
</td>
<td valign=top bgcolor=#d9d9d9>
<p>
Audacity 1.2.0 supports <a href="http://www.ladspa.org/">LADSPA</a> and
<a href="nyquist.php">Nyquist</a> plug-ins natively, and VST plug-ins
with the optional <a href="http://audacityteam.org/vst/">Audacity VST Enabler</a>.
</p>
<p>
Audacity 1.2.0 already comes with some sample LADSPA and Nyquist plug-ins in the
<b>Plug-ins</b> folder.  Put the VST Enabler and more plug-ins in this folder in
order to use them with Audacity.  The next time you open Audacity, the Plug-ins
will be in your Effect menu (or possible Generate or Analyze).  Plug-in effects
always appear at the bottom of these menus, with built-in effects at the top.
</p>

