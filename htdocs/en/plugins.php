<p>
Audacity 1.2 supports <a href="http://www.ladspa.org/">LADSPA</a> and
<a href="nyquist.php">Nyquist</a> plug-ins natively, and VST plug-ins
with the optional <a href="http://audacityteam.org/vst/">Audacity VST Enabler</a>.
</p>
<p>
Audacity 1.2.0 already comes with some sample LADSPA and Nyquist plug-ins in the
<b>Plug-ins</b> folder.  Put the VST Enabler and more plug-ins in this folder in
order to use them with Audacity.  The next time you open Audacity, the Plug-ins
will be in your Effect menu (or possible Generate or Analyze).  Plug-in effects
always appear at the bottom of these menus, with built-in effects at the top.
</p>

<p>
Audacity's Plug-in support is improving.  Please let us know if you
find a VST Plug-in that doesn't seem to work in Audacity, and we'll
try to fix it. 
</p>
