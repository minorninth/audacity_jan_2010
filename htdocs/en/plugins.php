<p>
Audacity supports VST Plug-in effects on the Macintosh and Windows platforms.
You can find Plug-ins that do all sorts of filters, equalization, reverb,
chorus, and more!  A list of places to download or purchase Plug-ins
is below.
</p>

<p>
Audacity's Plug-in support is improving.  Please let us know if you
find a VST Plug-in that doesn't seem to work in Audacity, and we'll
try to fix it. 
</p>

<p>
To use a VST Plug-in effect in Audacity, just create a folder called
<b>VST</b> in your Audacity folder, if you don't already have one, and
drop your Plug-ins there!  The next time you open Audacity, the Plug-ins
will be in your Effects menu.
</p>

