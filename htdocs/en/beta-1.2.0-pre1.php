<h1>Audacity 1.2.0-pre1</h1>

<p>
Audacity 1.2.0-pre1 is a public test release of the free Audacity audio
editor.  The purpose of this release is to allow users to preview and
test the new features that will appear in our next stable version 1.2.0.
</p>

<p>
Please report any bugs or comments to:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Your feedback will help us make the final release stable and bug-free!
We hope to release the new stable version in September 2003.
</p>
