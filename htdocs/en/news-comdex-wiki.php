We fixed a bunch of bugs in the last public test release, and we will be
releasing Audacity 1.2.0-pre3 in the next few days, along with the
VST Enabler, finally.  In the meantime, here's some other news:

<p>
Audacity was nominated as a possible open-source
program that <a href="http://www.oreilly.com/">O'Reilly</a> could
send to <a href="http://www.comdex.com/">COMDEX</a> in Las Vegas,
November 17-20.  <a href="http://www.oreillynet.com/contest/comdex/"
>Click here</a> for more information and to vote for
Audacity (free registration at O'Reilly required to vote).
Voting ends on October 31st.
</p>

<p>
The <a href="http://audacityteam.org/wiki/">Audacity Wiki</a> is a
publicly editable website for anything relating to Audacity.  It
currently includes tips, documentation, and links, plus a place for
feature requests.  The best part is that it's easy for anyone to
contribute - just click on a link at the bottom of any page to edit
or add your own content.  We hope you'll find this to be a fun and
valuable resource.
</p>

<p>
We now have <a href="http://audacityteam.org/tshirt.php">Audacity T-Shirts</a> available as a thank-you for people who contribute.
</p>

<p>
Both the <a href="http://audacityteam.org/wiki/">Wiki</a> and the
<a href="http://audacityteam.org/tshirt.php">T-Shirt Offer</a>
are hosted on a new website,
<a href="http://audacityteam.org/">AudacityTeam.org</a>,
which we've set up for extra Audacity content and topics related
to the Audacity development team, as opposed to Audacity in general.
</p>

