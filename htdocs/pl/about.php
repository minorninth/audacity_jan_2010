<p>
Audacity jest darmowym edytorem audio.
Możesz nagrywać i odtwarzać sygnały audio, importować i eksportować pliki w formatach WAV,AIFF, Ogg Vorbis,pliki MP3 i inne.
Używaj go do edycji dźwięków z wykorzystaniem wycinania,kopiowania i wklejania 
( z możliwością dowolnego cofania wykonanych operacji), do miksowania ścieżek oraz dodawania efektów do Twoich nagrań.
Program posiada także wbudowany edytor obwiedni/amplitudy, regulowany moduł do wyświetlania spektrogramów i analizy częstotliwościowej dla programów do analizy audio.
Posiada wbudowane efekty echa, zmiany tempa, usuwania szumu, a także umożliwia korzystanie z wtyczek efektowych VST i LADSPA.
</p>

