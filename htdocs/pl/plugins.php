<p>
Audacity 1.2 wspiera wtyczki efektowe <a href="http://www.ladspa.org/">LADSPA</a> i
<a href="nyquist.php">Nyquist'a</a> i pluginy VST 
z opcjonalnym <a href="http://audacityteam.org/vst/">Audacity VST Enabler'em</a>.
</p>
<p>
Audacity 1.2.0 zawiera kilka przykładowych efektów LADSPA i pluginó Nyquist'a w folderze <b>Pluginy</b>. Zainstaluj VST Enabler i więcej pluginów w tym folderze tak aby użyć ich w Audacity. Przy następnym uruchomieniu Audacity, pluginy znajdą się w menu Efekty (a niektóre w Generuj lub Analiza). Pluginy pojawiają się zawsze w dole tego menu z efektami.
</p>


<p>
Wspomaganie pluginów w Audacity jest ciągle ulepszane. Proszę powiadomić nas 
jeżeli znasz wtyczkę VST która nie pracuje w Audacity, a my spróbujemy się tym zająć.
</p>

