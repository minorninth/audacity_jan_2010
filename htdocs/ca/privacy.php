<p><b>Normes de funcionament de la llista d'avisos de novetats de l'Audacity</b></p>

<p>(És la llista a la que us afegiu quan escriviu la vostra adreça de correu a la capsa que hi ha a l'esquerra de les pàgines d'aquesta web)</p>

<p> Si esteu interessat en rebre informació cada cop que es llencin noves versions de l'Audacity, us convidem a què entreu la vostra adreça de correu electrònic a la casella de l'esquerra i cliqueu "Vull rebre avisos".  La vostra adreça s'afegirà a un llistat de correu que només utilitzarem per enviar-vos petits i infreqüents anuncis. (NOTA: generalment tots els anuncis es faran en anglès. No es descarta que en un futur hi hagi diferents llistats pels diferents idiomes).
</p>

<p>
Valorem la vostra privadesa.
</p>

<p> En cap circumstància, els desenvolupadors de l'Audacity i/o mantenidors del llistat de correu compartiran el llistat de persones amb terceres parts.  No emmagatzemem cap altra informació sobre els usuaris en el llistat (ni tan sols el vostre nom!) i mai no enviarem el llistat de direccions a cap lloc on es pugui fer públic.
</p>

<p>
Si voleu donar-vos de baixa de la llista, escriviu si us plau la vostra adreça de correu i pitgeu el botó "Vull deixar de rebre avisos".
Això hauria d'extreure immediatament la vostra adreça del llistat; no es requereix confirmació i no hi ha retard. Si per alguna raó això no funcionés envieu un missatge a  <a href="mailto:dominic@audacityteam.org">dominic@audacityteam.org</a>.
</p>

<p><b>Normes de funcionament de les llistes de correu de l'Audacity</b></p>

<p>(S'apliquen a les llistes <a href="http://lists.sourceforge.net/lists/listinfo/audacity-help">audacity-help</a>,
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-users">audacity-users</a>,
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-devel">audacity-devel</a>, i
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-translation">audacity-translation</a>).
</p>

<p>
Aquestes són <i>llistes de discussió</i>. Els missatges que envieu a aquestes llistes, incloent <a href="http://lists.sourceforge.net/lists/listinfo/audacity-help">audacity-help</a>,
s'enviaran a centenars de persones, entre les que s'inclouen els desenvolupadors de l'Audacity i altres usuaris. Diverses webs, incloent Sourceforge.net, poden publicar recopilacions dels missatges enviats a aquestes llistes. Les recopilacions no acostumen a incloure les adreces de correu dels qui envien els missatges, però com que es tracta de llistes públiques res no impedeix que algú altre s'hi subscrigui i es dediqui a publicar les seves pròpies recopilacions, amb adreces incloses. Si esteu preocupats per la privadesa hauríeu de fer servir adreces de correu temporals o dedicades només a aquest fi.
</p>

<p>
Si teniu alguna pregunta sobre aquestes normes, o si no us sentiu comfortables participant en les llistes de correu, envieu un misstge privat (en anglès) al desenvolupador en cap del projecte Audacity:
<a href="mailto:dominic@audacityteam.org">dominic@audacityteam.org</a>.
</p>

