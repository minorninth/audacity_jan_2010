<h2>Known Problems</h2>

<p>
Aquests són els problemes coneguts en l'Audacity 1.2.1:
</p>

<ul>
  <li><p>L'Audacity pot importar i mostrar fitxers MIDI, però no els pot interpretar o editar.
    </p></li>
  <li><p>L'efecte de canviar el to pot provocar problemes si es fa servir en una pista amb una resolució de 96 KHz.</p></li>
  <li><p>Només en Windows: L'Audacity es penja quan es fa servir la funció "Fixar resolució" de la pista de temps.</p></li>
  <li><p>Linux:  L'enregistrament en full duplex (interpretar pistes existents mentre s'enregistra) en alguns sistemes pot fer que els enregistraments monofònics sonin alentits.</p></li>
  <li><p>MacOS X: Quan s'engega l'Audacity pot ser que la configuració d'alguns dispositius d'àudio USB quedin mal ajustats. Això es pot arreglar restablint les configuracions amb el programa "Audio MIDI setup" d'Apple mentre l'Audacity està continua funcionant.</p></li>
  <li><p>MacOS X: L'Audacity no pot treballar amb fitxers o carpetes que estan dins de carpetes que tenen caràcters internacionals (accents, etc.) en els seus noms. Els fitxers amb accents funcionen, i els projectes Audacity amb accents també. Només els fitxers dins de carpetes amb noms amb accents tenen problemes.
    </p></li>
  <li><p>La finestra "Quant a l'Audacity" no es mostra correctament en idiomes que fan servir jocs de caràcters no occidentals.</p></li>
</ul>