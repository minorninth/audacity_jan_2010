<h2>Known Problems</h2>

<p>
Aquests són els problemes coneguts en l'Audacity 1.2.0:
</p>

<ul>
  <li><p>L'Audacity pot importar i mostrar fitxers MIDI, però no els pot interpretar o editar.
    </p></li>
  <li><p>L'enregistrament en full duplex (interpretant les pistes existents mentre s'enregistra) en alguns sistemes Linux, en modalitat monofònica, pot fer que  el so es vegi alentit.
    </p></li>
  <li><p>Només en Mac OS X: L'Audacity no pot treballar amb fitxers que es trobin dins de carpetes que tinguin caràcters internacionals (accents, etc.) en els seus noms. Els fitxers amb lletres accentuades funcionen, i els projectes Audacity amb lletres accentuades també funcionen. Només els fitxers que es trobin dins de carpetes amb noms amb accents poden causar problemes.
    </p></li>
  <li><p>L'efecte de canviar el to pot provocar problemes si es fa servir en una pista amb una resolució de 96 KHz.</p></li>
</ul>