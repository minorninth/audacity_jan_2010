<h2>Problemes coneguts</h2>

<p>
Aquests són els problemes coneguts de l'Audacity 1.2.3:
</p>

<ul>
  <li><p>L'Audacity pot importar i mostrar fitxers MIDI, però no els pot interpretar o editar.
    </p></li>
  <li><p>Linux:  L'enregistrament en full duplex (interpretar pistes existents mentre s'enregistra) en alguns sistemes pot fer que els enregistraments monofònics sonin distorsionats.</p></li>
  <li><p>Mac OS X: L'Audacity no pot treballar amb fitxers o carpetes que estan dins de carpetes que tenen caràcters internacionals (accents, etc.) en els seus noms. Els fitxers amb accents funcionen, i els projectes Audacity amb accents també. Només els fitxers dins de carpetes amb noms amb accents tenen problemes.
    </p></li>
</ul>