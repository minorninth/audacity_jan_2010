<p>
L'Audacity és un editor gratuït d'àudio. Podeu enregistrar sons, reproduir sons, importar i exportar arxius WAV, AIFF, i MP3, i molt més. Utilitzeu-lo per editar els vostres sons emprant les eines de Tallar, Copiar i Enganxar (amb il·limitada capacitat per desfer), barrejar pistes, o aplicar efectes a les vostres gravacions. També posseeix un editor envoltant d'amplitud propi, un mode espectrograma ajustable a mida i una finestra d'analisi de freqüència per aplicacions d'anàlisi d'àudio. Efectes propis inclosos el Bass Bost (Realçador de greus), Wahwah, i l'extractor de soroll, i també ofereix suport per efectes de pedaços VST. 

</p>

<p>
La versió 1.0.0 de l'Audacity és la versió més estable que hem
llençat fins ara, i molta gent ha trobat que les seves capacitats
úniques i la seva interfície intuïtiva fan que el seu ús sigui més agradable que el d'altres editors d'àudio, incloent-hi editors comercials.
</p>

<p> Els desenvolupadors de l'Audacity hem llançat també la versió 1.1.0,
que és un anticipi amb qualitat Beta de les noves coses en les que
estem treballant-hi activament. Conté realment moltes noves funcions,
però no és una versió tan polida i estable.
</p>

