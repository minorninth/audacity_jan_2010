<p>
L'Audacity és un programa lliure d'edició d'àudio. Amb ell podeu enregistrar sons, interpretar-los, importar i exportar fitxers WAV, AIFF, Ogg Vorbis i MP3, i moltes coses més. Podeu fer-lo servir per editar els vostres sons emprant les eines de retallar, copiar i enganxar (amb il·limitada capacitat per desfer), barrejar pistes o aplicar efectes als vostres enregistraments. 
També té un editor integrat de l'envolupant d'amplitud, un espectrograma totalment configurable i una finestra d'anàlisi de freqüències per aplicacions d'anàlisi d'àudio. Els efectes incorporats inclouen Eco, Canviar de temps, Reducció de sorolls, 
i suporta també els filtres i complements dissenyats per als sistemes VST i LADSPA.
</p>

