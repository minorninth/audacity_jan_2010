La versió 1.0.0 és la final versió estable de l'Audacity original.
És un bon i simple editor d'àudio apropiat per gravar barrejar i editar peces ordinàries de qualitat de CD d'àudio, en diverses plataformes incloent-hi el Windows, el Mac OS, i el Linux.
</p>

<p>
