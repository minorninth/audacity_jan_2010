<h2>Canvis de l'Audacity 1.2.1</h2>

<ul>
  <li><p>Han estat afegides o actualitzades les traduccions al: finlandès,
    francès, hongarès, italià, japonès, noruec, polonès, rus.

  <li><p>S'ha corregit un error que podia causar la pèrdua de dades en enganxar àudio d'un projecte a un altre si el primer projecte s'havia enregistrat i tancat.

  <li><p>S'ha corregit un error que es podia donar en obrir o redimensionar la finestra de l'equalitzador, especialment si es feien servir tipus de lletra grans.

  <li><p>No es permeten pecentatges inferiors al -100% en els canvis de to/velocitat/temps.

  <li><p>S'ha solucionat un error que es produïa quan el directori temporal no estava disponible en el moment d'arrencar.

  <li><p>Lectura correcta de les etiquetes ID3 emmagatzemades als fitxers de projectes Audacity.

  <li><p>En Linux i OS X, els fitxers de bloqueig (<i>lockfiles</i>) s'emmagatzemen al drectori temporal en comptes de fer-ho a la carpeta de dades de l'usuari. Això soluciona problemes en sistemes on les carpetes d'usuari no eisteixen o estan bloquejades.

  <li><p>S'ha corregit un error que impedia emprar els efectes Nyquist quan es trobaven activades determinades opcions de país.

  <li><p>S'ha corregit un error l'ordre de barreja rapida que podia fer que  no s'esborressin els fitxers temporals antics.

  <li><p>Linux: S'han corregit els probemes de codificació de dades (<i>endianness</i>) en sistemes PowerPC.

  <li><p>Linux: S'ha corregit un problema de compilació del Nyquist en sistemes MIPS.

  <li><p>Linux: Inclusió d'una versió més recent del PortAudio v19 que soluciona problemes de compilació amb l'opció --with-portaudio=v19.

  <li><p>Dos nous complements Nyquist: "Cross Fade In" i "Cross Fade Out."
  
  <li><p>Altres correccions d'errors menors.
</ul>
