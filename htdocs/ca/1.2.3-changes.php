<h2>Canvis a l'Audacity 1.2.3</h2>
<ul>
<li><p>
		Windows: Arreglat un error que feia que els enregistraments s'aturessin o es mostressin de manera incorrecta al cap de 50 minuts, en alguns sistemes Windows (Això es va resoldre parcialment a l'Audacity 1.2.2, però seguia sense funcionar en alguns sistemes.)
</p></li>
<li><p>
		Mac OS X: Arreglat un error que feia que l'Audacity es es pengés a l'atzar en sistemes Mac, especialment mentre s'enregistrava o s'escoltava un so.
</p></li>
<li><p>
    Els efectes de canviar el to i canviar el temps han estat actualitzats i ara fan servir una versió de la biblioteca  SoundTouch, d'Olli Parviainen,
    que ofereix més velocitat i millor qualitat.
</p></li>
<li><p>
    libsndfile s'ha actualitzat a la versió 1.0.11.  L'Audacity pot ara importar fitxers Sound Designer II (SDII).
</p></li>
<li><p>
    Corregida una errada que feia que el programa s'alentís en emprar l'eina d'envolupant.
</p></li>
<li><p>
    Quan es fa clic als botons "mut" o "solo" mentre es manté premuda la tecla de majúscules, treu el silenci (o la indicació "solo") de totes les altres pistes.
</p></li>
<li><p>
    Els connectors Nyquist poden ara aceptar l'entrada de cadenes de text.  També s'ha afegit un botó "Debug"
    als diàlegs d'efectes Nyquist, que us permet veure tota la informació de sortida produïda pel Nyquist, per ajudar a corregir errades.
</p></li>
<li><p>
    Quan un fitxer d'àudio referenciat ("aliat") a un projecte Audacity no es troba, l'Audacity interpretarà sempre silenci. Abans, l'Audacity acostumava a repetir el fitxer d'àudio interpretat més recentment.
</p></li>
<li><p>
    Els mesuradors de nivell (VU Meters) es reinicialitzen en aturar l'entrada o sortida d'àudio.
</p></li>
<li><p>
    Traduccions noves o actualitzades de: Italià (it), Hongarès (hu),
    Ucraïnès (uk), Espanyol (es), Polonès (pl), Xinès simplificat (zh),
    Norsk-Bokmal (nb), Francès (fr) i Rus (ru).
</p></li>
</ul>
