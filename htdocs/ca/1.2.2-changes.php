<h2>Canvis a l'Audacity 1.2.2</h2>
<ul>
<li><p>S'ha afegit una nova <a href="onlinehelp-1.2/toolbar_meter.htm">Barra de mesuradors de nivell</a>, que permet monitoritzar els nivells del senyal durant l'enregistrament i la reproducció.  Feu clic a la barra de mesuradors per visualitzar el nivell d'entrada sense enregistrar.</p></li>

<li><p>Exportació múltiple - nova funcionalitat que us permet exportar diversos fitxers al mateix temps, ja sigui per pista o a partir de 
    fragments delimitats per etiquetes.</p></li>

<li><p>El programa intentarà corregir automàticament la latència en
    enregistraments de tipus full-duplex. (Això encara no funciona
    perfectament, i no està suportat en tots els sistemes. Ho millorarem en 
    futures versions.)</p></li>

<li><p>S'ha arreglat un seriós error que podia provocar la pèrdua de dades
    quan es desava i es tornava a obrir un projecte Audacity que contingués
    dades repetides o duplicades.</p></li>

<li><p>El diàleg d'etiquetes MP3 només es mostra la primera vegada que exporteu a MP3; a partir d'aquí ja no apareix més, sempre i quan hi hagueu omplert com a mínim un camp.</p></li>

<li><p>Ara podeu afegir una etiqueta a la posició actual de reproducció, des del menú de Projecte o amb les tecles Ctrl+M.</p></li>

<li><p>Quan feu clic en una etiqueta es seleccionen totes les pistes, simplificant l'ús de les etiquetes per a recuperar seleccions.</p></li>

<li><p>Windows: S'ha arreglat un error en la funció "Fixar resolució" de la pista de temps.</p></li>

<li><p>S'ha arreglat un error que causava problemes amb enregistraments de més de 45 minuts en alguns sistemes Windows.</p></li>

<li><p>Mac OS X: S'ha millorat el suport per a l'iMic de Griffin, arreglant un error que feia que sempre enregistrés en mono en comptes d'estèreo.</p></li>

<li><p>S'ha afegit suport per a "playthrough" mitjançant software (escoltar el que s'està enregistrant mentre s'enregistra, o mentre s'ajusta el nivell d'entrada). Això fa possible, per exemple, enregistrar fent servir un dispositiu d'àudio mentre s'escolta el resultat en un altre dispositiu.</p></li>

<li><p>Unix/Linux: Arreglat el bloqueig del ratolí quan un dispositiu d'àudio es penja. Ara l'Audacity pot deixar de respondre, però ja no congela tot el sistema X.)</p></li>

<li><p>S'ha arreglat un error cosmètic que feia que les gràfiques d'ona es veiessin malament quan obríeu un projecte Audacity fet en una altra plataforma (per exemple, copiant un projecte d'un Mac a un PC).</p></li>

<li><p>S'ha arreglat un error que podia causar inestabilitat quan s'enganxava, es dividia o es duplicava la pista d'etiquetes.</p></li>

<li><p>Ara es pot canviar el tipus de lletra de la pista d'etiquetes mitjançant l'opció "Tipus de lletra..." del seu menú emergent.</p></li>

<li><p>S'ha afegit un suport bàsic a la impressió. Actualment s'escala tot el projecte per encabir-lo en una pàgina. Proveu amb l'orientació de paper apaisada per a obtenir millors resultats.</p></li>

<li><p>Mac OS X i Windows: L'Audacity ve amb la nova versió (1.0.1) del codificador Ogg Vorbis.  La compressió Vorbis tindrà ara una millor qualitat i permetrà obtenir fitxers més petits.</p></li>

<li><p>S'ha arreglat un error que ocasionalment podia provocar penjades quan s'aplicaven efectes a pistes creades a partir d'una divisió.</p></li>

<li><p>Les funcions de zoom (ampliació i reducció) es mostren ara desactivades quan no estan disponibles.</p></li>

<li><p>Els fitxers temporals creats per la funcionalitat "Audició preliminar" s'esborren sempre.</p></li>

<li><p>Altres errors menors i millores de funcionament.</p></li>
</ul>
