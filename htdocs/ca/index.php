<p>
L'equip de desenvolupadors de l'Audacity està content d'anunciar la realització automàtica d les versions 1.0.0 i 1.1.0.
</p>

<p>
La versió 1.0.0 és la versió final estable de l'Audacity original.
És un bon i simple editor d'àudio capaç de gravar, barrejar i editar les peces ordinàries de qualitat d'àudio, en diverses plataformes incloent-hi el Windows, el Mac l'OS, i el Linux.
</p>

<p>
La versió 1.1.0 é la primera versió beta de la següent versió de l'Audacity, que permet l'ús de l'àudio d'alta qualitat, més fitxers de formats d'àudio, més efectes, més funcions d'edició, una interfície millorada d'usuari, i permet l'ús de llengües estrangeres. Aquesta versió té moltes noves característiques, algunes d'aquestes encara estan sota desenvolupament i no han estat gaire provades de manera que poden presentar problemes.
</p>

<p>
És possible de tenir les dues versions de l'Audacity en el vostre ordinador al mateix temps.
</p>

