  * Экспортируемые монофонические MP3-файлы сохраняются с правильной скоростью.

  * Исправлена ошибка, вызывающая неправильную обработку огибающей.

  * Корректно переписываются существующие файлы по команде «Экспортироавть заметки».
  
  * В окне функции «Нарисовать график спектра» отображаются правильные номера октав для нот.

  * Устранено несколько ошибок, вызывающих утечку памяти.

  * Исправлено несколько других незначительных ошибок.
