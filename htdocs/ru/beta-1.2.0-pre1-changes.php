</li>

<li>
Средствами Audacity теперь можно найти нулевые точки, что позволяет вырезать области и создавать петли без слышимых щелчков. Нажмите "Z", чтобы сместить границы выделенной области до ближайших нулевых точек.
</li>
</ul>


<h3>Модули</h3>

<ul>
<li>
В Linux-версии Audacity теперь поддерживаются модули <a href="http://www.ladspa.org/">LADSPA</a>.
</li>

<li>
В Audacity 1.2 добавлен язык цифровой обработки сигнала под названием
<a href="nyquist.php">Nyquist</a>, позволяющий пользователям писать для программы новые эффекты на LISP-подобном языке.
</li>
</ul>


<h3>Импорт и экспорт файлов</h3>

<ul>
<li>
Файлы проектов Audacity 1.2 используют новый формат на основе XML. Audacity 1.2 автоматически откроет и преобразует в новый формат файлы, созданные более старыми версиями программы.
</li>

<li>
Audacity 1.2 использует <a href="http://www.underbit.com/products/mad/">libmad</a>
для быстрого декодирования MP3-файлов. Библиотека <a href="http://www.zip.com.au/~erikd/libsndfile/">libsndfile</a> Эрика де Кастро Лопо (Erik de Castro Lopo) обеспечивает улучшенную совместимость с другими форматами несжатого звука.
</li>

<li>
В диалогах импорта и открытия теперь можно выбрать несклько файлов, чтобы добавить их в один проекто. Новый формат "LOF" позволяет Audacity сразу открыть группу файлов, перечисленных в текстовом файле с расширением LOF.
</li>
</ul>


<h3>Улучшенный пользовательский интерфейс</h3>

