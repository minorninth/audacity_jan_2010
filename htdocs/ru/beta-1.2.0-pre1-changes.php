<ul>
<li>
Новые панели редактирования и микшера для быстрого доступа к часто используемым функциям.
</li>

<li>
Новый инструмент рисования для подстрйоки отдельных сэмплов, работающий при максимальном масштабировании. Добавлен новый многоинструментальный режим, переключающий инструменты в зависимости от контекста (положения мыши относительно дорожки). 
</li>

<li>
Много новых горячих клавиш и возможность их настройки.
</li>

<li>

Новые команды:
 <ul>
  <li>Воспроизведение петли. Нажмите "L" или удерживайте нажатой клавишу Shift, начиная воспроизведение выделенной области.</li>
  <li>Нажмите "1", чтобы воспроизвести по полсекунды звука с обеих сторон курсора.</li>
 </ul>
</li>

<li>
Можно масштабировать отображение проекта по прокрутке колеса мыши.
</li>

<li>
Отображение дорожек по вертикали можно увеличить щелчком или перетаскиванием по вертикальной линейке. Комбинацией Shift+щелчок или щелчком правой кнопкой мыши отображение уменьшается.  
</li>

<li>
Линейка и статусная панель могут отображать время в разных единицах измерения, включая секунды, сэмплы и видеокадры.
</li>

<li>
Интерфейс Audacity может быть переведён с английского на другие языки. Вы можете <a href="translation/">перевести Audacity</a> на свой родной язык.
</li>
</ul>
