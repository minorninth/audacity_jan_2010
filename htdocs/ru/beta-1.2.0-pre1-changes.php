<h3>Эффекты</h3>

<ul>
<li>
Три новых эффетка меняют высоту тона и темп дорожек:
 <ul>
  <li>&laquo;Изменить высоту тона&raquo;&nbsp;&mdash; повышает или понижает высоту тона выделенной области звука, не изменяя скорости воспроизведения.</li>
  <li>&laquo;Изменить темп&raquo;&nbsp;&mdash; ускоряет или замедляет воспроизведение выделенной области, не изменяя высоты тона.</li>
  <li>&laquo;Изменить скорость&raquo;&nbsp;&mdash; одновременно изменить и высоту тона, и скорость воспроизведения подобно тому, как это происходит с виниловой пластинкой или магнитофонной кассетой, когда меняют скорость их воспроизведения.</li>
 </ul>
</li>

<li>
В диалоге большинства эффектов теперь есть кнопка &laquo;Прослушать&raquo;, позволяющая прослушать, как будут звучать первые три секунды области звука, к которой применил дданный эффект. Кроме того, добавлен пункт меню, при помощи которого можно применить последний эффект с теми же настройками без повторного открытия диалога эффекта.
</li>

<li>
Другие новые эффекты:
 <ul>
  <li>Компрессор, дял сжатия динамического диапазона.</li>
  <li>Повторить, для физического создания повторов-петель выделенной области.</li>
  <li>Нормализовать, для подстройки уровня звука и исправления отклонения от оси.</li>
 </ul>
</li>
</ul>


<h3>Новые функции редактирования</h3>

<ul>
<li>
С помощью инструмента редактирования огибающей, предназначенного для плавной смены громкости дорожек, теперь можно делать дорожки громче, чем в оригинала (равно как и тише).
</li>

<li>
Новая "Дорожка времени" внешне похожа на огибающую, однако, с её помощью плавно изменяется скорость воспроизведения дорожки.
</li>

<li>
У каждой дорожки теперь есть собственный регулятор усиления и панорамирования, что сильно упрощает жизнь при сведении (а точнее, делает его возможным).
