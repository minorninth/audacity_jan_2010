<h2>Изменения в Audacity 1.2</h2>

Основные изменения относительно предыдущей стабильной версии Audacity (1.0.0):

<h3>Звук профессионального качества</h3>

<ul>
<li>
Теперь Audacity может записывать и редактировать звук с битовой глубиной 24-бит и 32-бит (с плавающей точкой). В проекте могут быть дорожки с расной частотой сэмплирования и битовой глубиной; Audacity может автоматически преобразовывать их в нужный формат по необходимости. Для всех преобразований используется высококачественный дитеринг и пересэмплирование.  
</li>

<li>
Audacity может записывать одновременно более двух каналов. Буфферизация также улучшена, что позволяет избежать &laquo;проглатывания&raquo; звука и переполнения буфера.
</li>
</ul>

