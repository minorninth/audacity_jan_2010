    - Функция выравнивания/группового выравнивания
    - Сохранение позиции курсора
    - Журналирование работы над проектом
  * Эффекты
    - Эффекты разделены на три группы/меню: &laquo;Создание&raquo;, 
      &laquo;Эффект&raquo; и &laquo;Анализ&raquo;
    - Через меню &laquo;Создание&raquo; доступны функции создания 
