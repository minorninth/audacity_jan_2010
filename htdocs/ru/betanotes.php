Известные ошибки в Audacity 1.1.3:
  - Файл с именем "Audacity-Commands.xml" помещается в домашний 
    каталог пользователя или любой другой. Этот файл необходим 
    для запуска Audacity 1.1.3. Приносим извинения за причинённое 
    неудобство и обещаем в следующей версии обеспечить файлу 
    более подобающее &laquo;место жительства&raquo;

Новые возможности Audacity 1.1.3:
  * Пользовательский интерфейс
    - Появилась панель микшера, через которую можно управлять 
      уровнем сигнала на входе, выходе, а также выбирать источник 
      записи
    - Для каждой дорожки теперь есть свой регулятор громкости и 
      баланса

  * Работа с файлами
    - Используется новый улучшенный формат проектных файлов. 
      К сожалению, прежние версии формата, включая 1.1.1, временно 
      не поддерживаются
    - Блочные файлы (сохраняемые в каталогах с проектами Audacity) 
      теперь имеют стандартный формат AU. Несмотря на то, что в них 
      используется метаинфорфация, такие файлы можно открывать и 
      обрабатывать в других аудиоредакторах
    - Исправлен ряд ошибок при чтении и записи аудиофайлов с 
      битностью больше 16
    - Импорт RAW-файлов снова работает &nbsp;&mdash; поддерживается 
      большее количество форматов, хотя и, временно, ценой чуть 
      более &laquo;бедного&raquo; графического интерфейса.
      Алгоритмы автоматического определения стали чуть аккуратнее 
      по сравнению с алгоритмами в версии 1.0

  * Запись и воспроизведение
    - Полностью переписаны алгоритмы записи и воспроизведения, 
      результатом чего стало понижение латентности и обеспечение 
      ничтожной возможности недозаполнения буфера при записи.

  * Пересемплирование
    - Улучшены алгоритмы пересемплирования с возможностью выбора 
      лучшего качества микширования за счёт ухудшения воспроизведения 
      в режиме реального времени.

    - Пробная поддержка дорожек времени, при помощи которых можно 
      гибко менять скорость воспроизведения аудиодорожки на всём её 
      протяжении

  * Большое число исправленных ошибок

Новые возможности Audacity 1.1.2:
  * Пользовательский интерфейс
    - Исправлена ошибка в версии для Windows с именованием 
      пунктов меню дорожек &laquo;Имя...&raquo; и &laquo;Разделить стереодорожку/ 
      Создать стереодорожку&&raquo;
  * Эффекты
    - Добавлена поддержка Nyquist для Windows-версии (добавлены 
      модули, написанные на Nyquist &nbsp;&mdash; интерпретируемом 
      функциональном языке, основанном на Lisp)


Известные ошибки в Audacity 1.1.1:

  * Попытка запустить тест на производительность под Windows 
    может привести  к падению программы. Для большинства 
    пользователей эта  функция не представляет особого 
    значения &nbsp;&mdash; она больше полезна для разработчиков, 
    тестирующих производительность  программы на различных компьютерах 
    и под различными операционными системами

  * Mac OS X: часть данных со звуковых дорожек может быть 
    потеряна, если вы будете удерживать кнопку мыши над меню 
    слишком долго после начала сеанса записи. Постарайтесь не 
    нарушать мерное течение записи звука, чтобы получить как 
    можно более адекватный вашим ожиданиям результат

