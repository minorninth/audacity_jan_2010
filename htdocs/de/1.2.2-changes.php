<h2>&Auml;nderungen in der Version Audacity 1.2.2</h2>
<ul>
<li><p>Die <a href="onlinehelp-1.2/toolbar_meter.htm">Meter Toolbar</a> wurde neu entwickelt um die
Lautst&auml;rke-Levels zu beobachten, w&auml;hrend der Aufnahme und Wiedergabe. Klicke auf 
die Balken um das ganze zu beobachten, ohne etwas aufzunehmen.</p></li>

<li><p>Export Multiple - Eine neue M&ouml;glichkeit die dir erlaubt, verschiedene
Dateien auf einmal zu speichern, entweder per Lied oder getrennt nach den Labeln.</p></li>

<li><p>Die Aufnahmelatenz bei Full-duplex Aufnahmen wird automatisch angepasst.
    (Das arbeitet noch nicht immer perfekt und wird noch nicht von allen Systemen
unterst&uuml;tzt. In der Zukunft wollen wir das noch verbessern.)</p></li>

<li><p>Du konntest deine Daten verlieren, wenn du ein Audacity Projekt gespeichert hast,
das ganze wieder geladen und neu bearbeitet, besonders wenn du Teile der Musik wiederholt hattest.
Dieser Fehler wurde beseitigt.</p></li>

<li><p>Der MP3 Tags Dialog &ouml;ffnet sich nur noch beim ersten Abspeichern einer MP3
Datei. Danach erscheint er nur noch, wenn du mindestens einen Tag gef&uuml;llt hast..</p></li>

<li><p>Man kann nun einen Label (Hinweis) auf die aktuelle Position
setzen, wenn man Ctrl+M dr&uuml;ckt im Projekt Men&uuml;.</p></li>

<li><p>Wenn man auf den Hauptlabel (Links neben der Waveform) klickt, wird die gesamte Datei markiert, damit es
einfacher ist, eine Auswahl erneut zu treffen.</p></li>

<li><p>Windows: Fehler im Time Track "Set Rate" Kommando beseitigt (f&uuml;hrte zu Systemzusammensturz).</p></li>

<li><p>Man hatte in machen Wind-Versionen Probleme Aufnahmen &uuml;ber 45 Minuten zu machen. Dies wurde beseitigt.</p></li>

<li><p>Mac OS X: Verbesserte Unterst&uuml;tzung f&uuml;r die Griffin iMic (Der Fehler der immer zu Mono Aufnahmen 
f&uuml;hrte wurde beseitigt).</p></li>

<li><p>Unterst&uuml;tzung f&uuml;r "Software Playthrough" neu entwickelt. (Man kann nun
anh&ouml;hren was man aufnimmt, w&auml;hrend man aufnimmt oder auch &uuml;ber den VU Meter).
</p></li>

<li><p>Unix/Linux: System Freeze wird nun vermieden, wenn Audioausgabe über die Maus gesteuert wurde.
  (Audacity war wahrscheinlich nicht verantwortlich, aber es gibt nun keinen System Freeze mehr in X.)</p></li>

<li><p>Der Fehler in der Darstellung der Waveform wurde beseitigt, der auftrat, wenn man ein Projekt zwischen verschiedenen 
Betriebssystemen wechselte (zum Beispiel von Mac nach Windows).
    </p></li>

<li><p>Ein Fehler wurde beseitigt, der das ganze instabil machte, wenn man einen Label versuchte aufzuteilen, einzufügen oder
zu verfielfältigen.</p></li>

<li><p>Man kann nun die Schriftart eine Labelmarkierung &auml;ndern indem man "Schriftart.." im Label Pop-Up Men&uuml;
aussucht.</p></li>

<li><p>Grunds&auml;tzlicher Druckoutput wird nun angeboten. Zur Zeit wird das ganze Projekt
immer auf einer kompletten Seite ausgedruckt (immer 1 Seite).</p></li>

<li><p>Mac OS X und Windows: In Audacity ist nun die neuere Version (1.0.1)
    des Ogg Vorbis Encoders enthalten.  Die Vorbis Kompression hat nun eine h&ouml;here Qualit&auml;t und kleinere
Dateigr&ouml;ssen.</p></li>

<li><p>Ein fehler wurde beseitigt, der zum Absturz f&uuml;hrte, wenn man versuchte Effekte in einem
aufteilten Track hinzuzuf&uuml;gen.</p></li>

<li><p>Zoom In / Zoom Out sind nun nicht mehr aktivierbar, wenn sie nicht funktionieren.</p></li>

<li><p>Die temporären Dateien die in der Vorausschau erzeugt werden, werden nun automatisch gel&ouml;scht..</p></li>

<li><p>Weitere kleinere Fehler und andere Verbesserungen.</p></li>
</ul>

