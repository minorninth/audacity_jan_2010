<p>
Audacity unterstützt VST Plugins für Macintosh und Windows-Betriebssysteme.
Es stehen eine große Anzahl von Plugins für verschiedenste Anwendungsfälle zur Verfügung.
Sie finden hier eine Liste mit Links zu Downloadseiten oder Kaufmöglichkeiten.
</p>

<p>
Die Plugin-Unterstützung ist noch nicht vollends ausgereift.
Wenn Sie ein VST Plugin finden, das nicht unterstützt wird, teilen Sie uns das bitte
im <a href="http://audacity.fuchsi.de/">Audacity-Forum</a> mit, damit
wir den Fehler beheben können.
</p>

<p>
Um die VST Plugins nutzen zu können, genügt es, ein Verzeichnis
mit dem Namen <b>VST</b> im Audacity-Prgrammverzeichnis zu erstellen (falls nicht schon vorhanden)
und die Plugins hineinzukopieren/verschieben.
Beim nächsten Programmstart erscheinen die Plugins im "Effekte"-Menü.
</p>

