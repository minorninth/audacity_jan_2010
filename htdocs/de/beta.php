<p>
Achtung: die folgende Version von Audacity befindet sich noch im Beta - Stadium!
Sie enthält eine Menge neuer Funktionen wie automatisches Resampling, 
eine neue Werkzeugleiste und 32-bit Sample - Unterstützung, kann aber auch einige neue Fehler enthalten!
</p>
<p>
Wenn du gerne die neuen Möglichkeiten ausprobieren möchtest,
laden wir dich ein, diese Version herunterzuladen. Wenn du das Programm testest, 
freuen wir uns schon auf deine Meinung und Verbesserungsvorschläge! 
Lass uns wissen, was bei dir funktioniert und was nicht - oder was noch fehlt.
Du kannst beide Versionen gleichzeitig auf deinem Computer installieren.
</p>
<p>
Hinweis:
Diese Version kann keine Projektdateien von Audacity 1.0 oder älter lesen.
Es wird ein neues XML-basiertes Dateiformat für die Projektdatei verwendet!
</p>
