<p>
Achtung: Die folgende Version von Audacity befindet sich noch im Beta-Stadium!
Sie enthält eine große Anzahl neuer Funktionen wie automatisches Resampling,
eine neue Werkzeugleiste und 32-bit Sample-Unterstützung, kann aber natürlich auch einige
neue Fehler enthalten!
</p>
<p>
Wenn Sie gerne die neuen Möglichkeiten ausprobieren möchten,
laden wir Sie ein, diese Version herunterzuladen. Wenn Sie das Programm testen,
freuen wir uns schon auf Ihre Meinung und Verbesserungsvorschläge!
Lassen Sie uns im <a href="http://audacity.fuchsi.de/">Audacity-Forum</a> wissen, was bei Ihnen funktioniert und was nicht - oder was noch fehlt.
Sie können beide Versionen gleichzeitig auf Ihrem Computer installieren.
</p>
<p>
Hinweis:
Diese Version kann keine Projektdateien von Audacity 1.0 oder älter lesen.
Es wird ein neues XML-basiertes Dateiformat für die Projektdatei verwendet.
</p>
