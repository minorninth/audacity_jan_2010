<?php BoxTop('June 5 2002: Versions 1.0.0 and 1.1.0 released!'); ?>

<p>
Das Audacity Entwicklerteam freut sich, die Erscheinung der Versionen 1.0.0 und 1.1.0 ankündigen zu dürfen.
</p>

<p>
1.0.0 ist die fertige, stabile Version des originalen Audacity. 
Das Programm ist ein guter, einfacher Audio Editor für Aufnahmen, 
Mixes und die Bearbeitung normaler Stücke in CD-Qualität für viele Plattformen 
wie Windows, Mac und Linux.
</p>

<p>
Audacity 1.0 ist nur in Englisch erhältlich. 
Diese Version besitzt noch keinen Sprachsupport und wird auch keinen mehr bekommen. 
Wer das Programm auf Deutsch nutzen möchte, soll die Beta 1.1.0 versuchen (noch nicht stabil!).
</p>

<p>
Um Audacity auf Deutsch nutzen zu können müssen folgende Schritte durchgeführt werden:

<ol start="1" type="1">
<li>Starte das Programm</li>
<li>Wähle "Preferences" im Menü "File"</li>
<li>Wähle "Interface" an</li>
<li>Ändere "en" nach "de"</li>
<li>Beende Audacity und starte es erneut - die Oberfläche ist nun Deutsch</li>
</ol>

Die Windowsversion findet die Sprachdatei nicht, wenn das Programm über einen Link (auf dem Desktop oder im Startmenü) gestartet wird. Um die Sprache einzustellen muß es also "direkt" gestartet werden. Dazu mußt du mit deinem Dateimanager (Windows Explorer) in das Programmverzeichnis wechseln (meist C:\Prorgamme\Audacity 1.1) und das Programm dort durch einen Doppelklick starten. Dieser Fehler wird in Version 1.1.1 behoben sein.
</p>

<p>
1.1.0 ist die erste Beta der nächsten Audacity Version
welches Stücke in höherer Qualität, mehrere Formate, Effekte und Funktionen (u.a. verschiedene Sprachen) erlaubt/bietet.
Da diese Version viele neue Funktionen hat und einige noch stark in der Entwicklungsphase sind, kann es bei einigen Benutzern zu Problemen kommen.
</p>

<p>
Es ist ohne weiteres möglich, beide Versionen von Audacity gleichzeitig installiert zu haben.
</p>

