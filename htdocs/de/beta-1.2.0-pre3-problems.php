<h2>Bekannte Probleme</h2>

<p>
Dies sind dies uns bisher bekannten Probleme / Fehler in Audacity 1.2.2:
</p>

<ul>
  <li><p>Audacity kann MIDI Dateien laden und anzeigen, aber nicht abspielen bzw. &auml;ndern    </p></li>
  <li><p>Linux:  Aufnahmen im Full Duplex Modus (Abspielen eines Lides w&auml;hrend der gleichzeitigen Aufnahme)
     f&uuml;hrt bei manchen Systemen zu Monoaufnahmen bzw. es h&ouml;rt sich so an, als w&auml;hre der Takt zu langsam
</p></li>
  <li><p>MacOS X: Audacity kann nicht mit Daten arbeiten, die Internationale Zeichen beinhalten (Hochkomma usw.) 
wenn diese Dateien <b>innerhalb</b> von Ordner liegen. Einzelzugriff auf Dateien oder Ordner mit Hochkomma funktionieren.
    </p></li>
</ul>