<h2>&Auml;nderungen in Audacity 1.2.1</h2>

<ul>
  <li><p>Folgende &Uuml;bersetzungen wurden hinzugef&uuml;gt: Finnisch,
    Franz&ouml;sisch, Ungarisch, Italianisch, Japanisch, Norwegisch, Polnisch, Russisch.

  <li><p>Der Fehler des Datenverlustes wurde beseitigt, wenn man Daten aus einem Projekt in ein anderes eingef&uuml;gt hatte
und das erste Projekt wurde vorher geschlossen.

  <li><p>Wenn man das Equalizer Fenster &ouml;ffnete oder &auml;nderte und dabei Grosse Symbolschriftarten auf dem Betriebssystem hatte
konnte es zu einem Systemzusammenbruch kommen. Dies wurde beseitigt.

  <li><p>Prozents&auml;tze unter -100% im Bereich des Pitch/Speed/Tempo Effektes werden nicht mehr akzeptiert.


  <li><p>Fehler eines Zusammenbruches beseitigt, wenn das Temp Verzeichnis beim &ouml;ffnen nicht
verf&uuml;gbar war.

  <li><p>Richtiges laden der ID3 Tags die in Audacity Projekt Dateien gespeichert werden.

  <li><p>On Linux und OS X: Die Lockfiles werden jetzt im Temp Verzeichnis gespeichert, nicht mehr
im Home Verzeichnis. Dies f&uuml;hrte in der Vergangenheit zu Problemen bei beschr&auml;kten Zugriff auf dieses Verezichnis.

  <li><p>Einige Nyquist effects funktionierten nicht, bei bestimmten Einstellungen je nach Region. Dies wurde beseitigt

  <li><p>Fehlerbeseitigung im Quick Mix. Ältere Temp Dateien werden nun gelöscht.

  <li><p>Linux: Beseitigung endianness Probleme beim Abspielen auf PowerPC.

  <li><p>Linux: Beseitigung des compilation Problems in Nyquist auf MIPS.

  <li><p>Linux: Eine neuere PortAudio v19 Snapshot wurde eingebaut (Beseitigt Compilations
    Probleme wenn man mit der --with-portaudio=v19 Option).

  <li><p>2 neue Nyquist Plug-Ins: "Cross Fade In" und "Cross Fade Out."
  
  <li><p>Weitere kleinere Fehlerbeseitigungen.
</ul>
