<p>
Audacity ist ein freier Audio - Editor.
Du kannst mit ihm Musik aufnehmen, abspielen, Audiodaten wie WAV, AIFF, MP3 im- und exportieren und vieles mehr.
Gängige Operationen wie Ausschneiden, Kopieren und Einfügen (unbegrenztes Rückgängigmachen) sind gleichermaßen möglich,
wie das Mixen und Hinzufügen von speziellen Klangeffekten, wie Echo, oder Geschwindigkeitsveränderungen.
Das Programm besitzt unter anderem ein Werkzeug um die Amplituden einfach zu verändern (fade-in und -out) 
und ein einstellbares Frequenzanalysetool.
Die eingebauten Effekte sind zur Zeit "Bass Boost", "Wahwah" und ein Lärm- bzw. Rauschfilter; 
es können aber auch beliebige VST-Plugins gechrieben und verwendet werden.
</p>

<p>
Audacity 1.0.0 ist im Moment die einzige stabile Version und viele Benutzer finden die einzigartigen Möglichkeiten
 und die intuitive Oberfläche erfrischender und brauchbarer als so manches kommerzielles Programm.
</p>

<p>
Audacity 1.0 ist <b>nur in Englisch erhältlich</b>. 
Diese Version besitzt noch keinen Sprachsupport und wird auch keinen mehr bekommen. 
Wer das Programm auf Deutsch nutzen möchte, soll die Beta 1.1.0 versuchen (noch nicht stabil!).
</p>

<p>
Um Audacity auf Deutsch nutzen zu können müssen folgende Schritte durchgeführt werden:

<ol start="1" type="1">
<li>Starte das Programm</li>
<li>Wähle "Preferences" im Menü "File"</li>
<li>Wähle "Interface" an</li>
<li>Ändere "en" nach "de"</li>
<li>Beende Audacity und starte es erneut - die Oberfläche ist nun Deutsch</li>
</ol>

Die Windowsversion findet die Sprachdatei nicht, wenn das Programm über 
einen Link (auf dem Desktop oder im Startmenü) gestartet wird. Um die Sprache 
einzustellen muß es also "direkt" gestartet werden. Dazu mußt du mit deinem 
Dateimanager (Windows Explorer) in das Programmverzeichnis wechseln 
(meist C:\Prorgamme\Audacity 1.1) und das Programm dort durch einen 
Doppelklick starten. Dieser Fehler wird in Version 1.1.1 behoben sein.
</p>

<p>
Die Version 1.1.0 ist zur Zeit noch im Beta - Stadium und soll lediglich eine Vorschau auf 
die zahlreichen zukünftigen Funktionen des Programmes sein.
Vor dem Einsatz mit wichtigen Dateien ist dringend abzuraten!
</p>

