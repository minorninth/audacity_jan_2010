<h2>Änderungen in Audacity 1.2.3</h2>
<ul>
<li><p>
		Windows: Fehler beseitigt, der in Windows bei Aufnahmen über 50 Minuten
		auftrat. Die Aufnahmen wurden gestoppt oder nicht korrekt angezeigt.
		(Dies war teilweise schon in Audacity 1.2.2. beseitigt, aber nicht auf
		allen Systemen.)
</p></li>
<li><p>
		Mac OS X: Fehler beseitigt, der zu einem Programmabsturz führte.
		Dies geschah hauptsächlich während des Abspielens oder von Aufnahmen.
</p></li>
<li><p>
    Die Effekte "Tonhöhen Änderung" und "Tempo Änderung" wurden auf eine neuere Version der
    SoundTouch library von Olli Parviainen umgestellt, nun funktionieren Sie schneller und mit besserer Qualität.
</p></li>
<li><p>
    libsndfile ist auf Version 1.0.11 umgestellt worden. Audacity kann nun Sound Designer II (SDII) Dateien importieren.
</p></li>
<li><p>
    Fehlerbeseitigung im Bereich des Envelope-Tools. Das Programm wurde dadurch langsamer.
</p></li>
<li><p>
    Shift-clicking auf einen Mute oder Solo Knopf "un-mutes" (oder "un-solos")
    alle anderen Lieder.
</p></li>
<li><p>
    Nyquist Plugins akzeptieren nun Zeichenfolgen als Eingabe. Ebenfalls wurde eine
	"Debug" Option in die Nyquist Effekt Dialoge eingebaut, der bei Problemen helfen kann schneller die
Ursache zu finden.
</p></li>
<li><p>
    Wenn eine Audiodatei fehlt, die in einem Audacity Projekt verwendet wird, wird nun
	"Stille" abgespielt. (In den Audacity Versionen vor 1.2.3 wurde immer das vorherige Lied abgespielt). 
</p></li>
<li><p>
    Der VU Meters wird nun immer neu initialisiert, wenn die Audiowiedergabe gestoppt wird.
</p></li>
<li><p>
    Neue oder geänderte Übersetzungen: Italian (it), Hungarian (hu),
    Ukrainian (uk), Spanish (es). Polish (pl), Simplified Chinese (zh),
    Norsk-Bokmal (nb), French (fr), Russian (ru).
</p></li>
</ul>
