<h1>Audacity 1.2.0-pre1</h1>

<p>
Audacity 1.2.0-pre1 ist die aktuelle Testversion des freien Audioeditors. Mit Hilfe dieser Version ist es möglich, die neuen Funktionen der in voraussichtlich einem Monat erscheinenden Stable Version 1.2.0 kennen zu lernen und das Programm zu testen.
</p>

<p>
Sollten Fehler auftreten, sende bitte eine Nachicht an:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Rückmeldungen helfen uns, die endgültige Version von Audacity 1.2.0 fehlerfrei und stabil zu machen. Wir hoffen, diese Final Version im September 2003 freizugeben.
</p>
