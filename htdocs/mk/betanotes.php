Познато во Audacity 1.1.3:
  - Става датотека "Audacity-Commands.xml" во вашиот дома именик
    или некаде на вашиот компјутер.  Датотеката е потребна за
    Audacity 1.1.3 да работи.  Се извинуваме за полнењето на вашиот компјутер
    и ветуваме дека ќе ја ставиме датотеката на убаво место во следните изданија.

Ново во Audacity 1.1.3:
  * Кориснички интерфејс
    - Новата Микс алатка ви дозволува да ја контролирате излезната
      и влезната јачина како и влезниот уред директно
      од Audacity.
    - Секоја нумера сега си има сопствени гејн и панорамски контроли.

  * Датотечен В/И
    - Користи подобрен формат за проекти.  (За жал читањето на
      претходните формати, вклучително и 1.1.1, не е поддржано.)
    - Блоковите (зачувани во Audacity проект именикот) сега
      го користат стандардниот AU формат.  Бидејќи некои Audacity
      мета-информации се во оваа датотека, таа сега може да биде читана
      од многу други популарни аудио програми исто така добро.
    - Поправени некои бубачки кои се однесуваа на читање/снимање
      нумери со повеќе од 16-битна рата.
    - Внеси RAW повторно работи, со поедноставен GUI (изглед)
      но со подршка за повеќе формати.  Автодетекцискиот
      алгоритам е многу поспособен од оној
      во 1.0.

  * Аудио В/И
    - Комплетно е препишан аудио В/И, со помало доцнење
      и минимални шанси за паѓање на меѓумеморијата при
      снимање.

  * Прератирање
    - Користи висококвалитетен прератирачки алгоритам, со
      опција за подобар квалитет при миксување отколку при реално времнски
      плејбек

    - Подготвителна подршка за Временски нумери, за менување на
      брзината временски.

  * Многу отстранети бубачки и нови можности

Ново во Audacity 1.1.2:
  * Кориснички интерфејс
    - Поправени бубачки во Windows верзијата, за Нумера мени командите
	  "Име..." и "Подели стерео нумера"/"Направи стерео нумера".
  * Ефекти
    - Nyquist подршка за Windows (подршка на додавки пишувани
