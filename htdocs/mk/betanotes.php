    - Вертикалното менување на стерео нумерите е позабавно
    - Местење селекција со клик-влечење на селекциската рамка
    - Поттекстот на копчињата е чувствителен на вклучување/исклучување
    - Подобрена зум функционалност (ги центрира регионите)
    - Повеќе начини за приказ на позицијата на покажувачот и селекцијата
    - Залепи за селкција опција
    - Влечење нумери горе долу
    - Порамни и порамни во група функции
    - Покажувач сними/врати
    - Прозорец за работна историја
  * Ефекти
    - Ефектите се поделени во три менија: Генерирај, Ефект и
      Анализирај
    - Генерирај менито ви дозволува генерирање тишина, шум и тон
    - Nyquist подршка (подршка на додавки напишани во Nyquist,
      интерпретирачки функциски јазик базиран на Lisp)
  * Локализација
    - Овозможува подршка на локлизација
    - Повеќе достапни јазици
    - Избор на јазик при прво поставување
  * Mac OS X
    - Подршка за повеќе аудио уреди
    - Подрчка за full-duplex (свирење при снимање)
    - Подршка за MP3 експорт користејќи LameLib Carbon
  * Unix
    - Audacity сега има man страна (ги опишува команднолиниските
      наредби и како да поставите патека за барање)
  * Датотечни формати
    - Употреба на libsndfile 1.0, што отстранува некои бубачки
      и ги подобрува преформансите
  * Барање на датотеки:
    - Во Windows и Mac OS, Audacity сега ги бара
      преводите во "Languages" именикот и сите додавки
      во "Plug-ins" именикот, соодветни за програмот.
    - Во Unix, Audacity ги бара преводите во
      <prefix>/share/locale и се друго во
      <prefix>/share/audacity и на секоја друга патека во
      AUDACITY_PATH варијаблата

