Познати бубачки во Audacity 1.1.1:

  * Не ја стартувајте Брзинар командата во Windows - може да падне.
    Тоа е бескорисна можност за повеќето корисници - наменета е
    за развивачи, да ја проценат брзината на програмот на различни
    компјутери и оперативни системи.

  * Mac OS X: некои аудио можат да бидат изгубени ако го држите увцето
    притиснато подолго над менито Снимај.  Обидете се да го оставите Audacity
    сам при снимање за подобри резултати.

Ново во Audacity 1.1.1:

  * Кориснички интерфејс
    - Алатките се појавуваат во статусната линија.
    - Вертикалниот покажувач дозволува свири/сними
    - Копче Пауза
