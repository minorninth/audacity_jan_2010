<font size=+1><b>

1. Како да смиксам две нумери заедно?
</b></font>
<p>

Audacity миксува автоматски. Се што треба да направите е
да внесете две нумери во ист проект, што можете да го направите
користејќи ја „Внеси аудио“ командата во Проект
меито или едноставно довлечувајќи две аудио нумери во
Audacity.  Кога ќе го стиснете копчето Снимај Audacity,
исто така, создава нова нумера автоматски.

<p>

За да го нимите вашиот микс, можете да го Експортирате
проектот, што автоматски ќе ги измиксува сите ваши
нумери заедно или можете да одберете ваши нумери
и да ја искористите „Брз микс“ наредбата во Проект менито.

<p>
<a name="a2"></a>
<font size=+1><b>
2. Можам ли да отстранам вокал од снимка?
</b></font>
<p>

Со некои стерео снимки можно е да се отстранат вокалите
заради начинот на кој се миксувани при снимањето во студио.
Ако вокалите се поставени токму во центар при снимањето,
а сите други инструменти се околу центарот со
одделување на десниот од левиот канал вокалот
комплетно се губи, оставајќи ги само другите инструменти.
<p>

<b>Ова функционира само на некои снимки!</b>
<p>
За да го направите ова во Audacity, внесете стеро нумера, потоа
кликнете на нумера менито (малата стрелка надолу веднаш до
името на нумерата) и одберте „Подели стерео нумера“.
Сега одберете ја долната нумера (десниот канал) и употребете го
"Обратно" ефектот (од Ефект менито).  Најпосле, користејќи
го нумера менито обата канали претворете ги во моно канали
и потоа измиксувајте ги користејќи „Брз микс“.  Ако сте среќни
вокалите ќе ги снема.

<p>
<a name="a3"></a>
<font size=+1><b>
3. Кога се обидувам да се снимам себе си и нумерата
              зошто двете нумери се несинхронизирани?
</b></font>
<p>
Тоа е нормално и се случува поради малото доцнење
откако Audacity ќе почне да свири и вистинското
јавување на вашите звучници.  Audacity не се обидува автоматски
да го корегира ова доцнење и вие морате рачно да го наместите.
<p>
За исправка на доцнењето користете ја
Временската алатка за лизгање на
една од нумерите по нејзината линија.  Забележете дека можете
да ја користите алатката додека свирите.

