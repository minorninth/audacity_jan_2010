<a name="a5"></a>
<font size=+1><b>
5. Можам ли да снимам RealAudio или друг стриминг?
</b></font>
</p><p>

Не автоматски.  Повеќето стриминзи вклучуваат користење на
RealOne Player, Windows Media Player и Quicktime, кои се специфично
дизајнирани да ве заштитат од снимање.
</p><p>
Сепак, скоро секогаш е можно да се сними скоро се што вашиот компјутер
може да отсвири преку спојување на излезот со влезот или микрофонот
на вашата звучна картичка.
</p><p>
Употребете 1/8 инчен стерео мини со 1/8 инчен стерео мини кабел.  Ставете го едниот
крај во излезот од вашиот компјутер или звучна картичка (каде што ги
вклучувате звучниците или слушалките - најчесто зелено обоен).  Внесете го другиот
крај во микрофонскиот или линискиот влез (најчесто обоен црвено).
</p><p>
Сега Audacity може да сними се што вашиот компјутер ќе отсвири.  Стиснете Снимај
во Audacity а потоа Пушти/Play во вашиот стриминг програм.
</p><p>

<a name="a6"></a>
<font size=+1><b>
6. Помош! Стиснав Снимај, но се што добив е тишина!
</b></font>
</p><p>
Audacity снима преку стандардно поставениот влезен уред во
вашиот оперативен систем.  Ако имате повеќе влезни уреди (како
вграден микрофон, надворешен микрофон, линиски влез и
аудио ЦД) тртеба да одберете еден од нив за да снимате.  Ако се
обидете да снимате од микрофон, на пример, но вашиот влезен уред е
поставен на „линиски влез/line in“, тогаш Audacity ќе сними само тишина.
</p><p>
Ако користите Windows, можете да го поставите влезниот уред со десен клик
на иконката за јачина во линијата за алатки (во десниот долен агол
на вашиот екран).
</p><p>
Ако користите Mac OS 9, употребете го Sound control panel.  На Mac OS X, употребете
го Sound panel во System Preferences (таму има Input јазиче ако имате
10.2 или понов).
</p><p>
Во Linux, употребете миксета како „xmixer“ или „kmix“.
</p><p>

<a name="a7"></a>
<font size=+1><b>
7. Како да поделам една во многу нумери?
</b></font>
</p><p>
Некогаш можеби ќе имате неколку нумери кои се снимени во
една.  Ако сакате да ги ставите нумерите на ЦД како одвоени
нумери, вие ќе мора да ја одделите секоја нумера посебно.
</p><p>
Има многу начини да се подели снименото на повеќе делови употребувајќи го
Audacity.  Еве неколку кои можете да ги испробате:
</p><p>
1 начин: Експорт селекција
</p><ul>
<li>Селектирајте регион кој одговара на првата нумера
</li><li>одберете „Експорт селекција како WAV“ од Датотека менито и снимете
    ја на дискот.
</li><li>Повторете за сите останати нумери.
</li></ul>
<p>
2 начин: Бриши и Врати
</p><ul>
<li>Избришете се <i>освен</i> првата нумера.
</li><li>Одберете „Експорт како WAV“ од Датотека менито, и снимете ја првата нумера
    на дискот.
</li><li>Одберете „Врати“ од Уреди менито.  Продолжете да враќате се додека не се врати
    се од вашето аудио.  (Audacity има неограничено Врати.)
</li><li>Повторете за секоја останата нумера.
</li></ul>
<p>
3 начин: Подели и Експортирај
</p><ul>
<li>Одберете што сакате да биде прва нумера.
</li><li>Одберете „Подели“ од Уреди менито, што ќе ја префрли селекцијата
    во одвоена Audacity нумера.
</li><li>Повторувајте додека не ги одвоите сите нумери.
    Не заборавајте на Врати ако каде било згрешите.
</li><li>Сега вратете се назад и одберете „Експорт селекција како WAV“ за експорт на
    секоја одвоена нумера.  За брза селекција на поединечна нумера кликнете на нејзиното
    име (каде било во полето лево од брановидната форма, блиску
    до насловот).
</li></ul>
