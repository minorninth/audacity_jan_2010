<a name="a4"></a>
<font size=+1><b>
4.Зошто Audacity не доаѓа со MP3 енкодер?
</b></font>
<p>

За жал, алгоритмот за енкодирање или креирање MP3 датотеки
е патентиран и <b>Audacity</b> нема да може да
вклучи MP3 експорт алгоритам без да плати
или да го прекрши законот во многу држави.
<p>
Како компромис, Audacity доаѓа со можност за користење
други MP3 енкодери, кои морате да ги симнете одвоено.
Така на вас останува дали ќе ги почитувате
сите лиценцни ограничувања со MP3 енкодерот.
<p>
За повеќе информации видете ја страната за експорт на MP3 датотеки
во Помошта.

