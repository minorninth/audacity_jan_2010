<font size=+1><b>
2. Кој го развива Audacity?<br>
</b></font>

<p>

Audacity е започнат 1999
од Dominic Mazzoni додека
студираше на Carnegie Mellon University во Pittsburgh, PA, USA.
Тој работеше на истражувачки проект со својот ментор, професорот
Roger Dannenberg и им требаше алатка која ќе им овозможи
алгоритам за визуелизација на аудио сигнал. Потоа, овој програм
се разви во генерален звукоуредувач и други луѓе почнаа
да помагаат.
<p>
Денес, Audacity се развива користејќи го Sourceforge, мрежна страна
која дозволува луѓе од цел свет да соработуваат на слободни
програмски пакети.  Види на <a href="http://www.sourceforge.net"
>sourceforge.net</a> за повеќе информации.
Дузини луѓе придонесуваат во
Audacity и неговиот развој постојано се забрзува.

