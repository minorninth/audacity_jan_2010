1. Дали Audacity е навистина слободен?  Зошто?<br>

</b></font>

<p>

Да, во скоро сите случаи.  Audacity не само што е ослободен од плаќање
туку вие можете слободно да правите што сакате со него.
<p>
Audacity се дистрибуира под правилата на
<a href="http://www.gnu.org/copyleft/gpl.html"
>Генералната јавна лиценца (GPL)</a>.
Слободно можете да го користите програмот за лични или комерцијални
потреби. Исто така слободно можете да го давате или продавате.
Изворниот код од програмот е слободно достапен на
мрежа и вие слободно можете да го модификувате за сопствени потреби,
а секако секоја измена исто така мора да ја дистрибуирате под GPL.
<p>
Audacity е создаден користејќи wxWindows, програмска библиотека
која подлежи под помалкурестриктивната LGPL.
За повеќе информации посетете ја страната
<a href="http://www.wxwindows.org">wxwindows.org</a>.
<p>
Авторите на Audacity одлучија да го издадат под GPL
од многу причини. Некои од нас работат од љубов.
Дел од нас поради морални причини, бидејќи мислиме дека
сите програми треба да се слободни, додека другиот дел од нас
веруваат дека во светот постои потреба и за слободни и за комерцијални
програми.
<p>
Една причина што Audacity е слободен е што така ќе биде и попопуларен.
Помило ни е да видиме милион среќни корисници на
Audacity за беспари отколку илјада што ќе ни платат.
Корисниците се попријателски кога нештото го добиваат слободно.

<p>
Друга причина е што така се поттикнува соработката. Ако Audacity
се плаќаше сигурно дузина луѓе од светот
немаше да придонесуваат со код, исправки на бубачки, документација
графики,...

