<p>
Audacity 1.1.1 fungerer langt bedre under Mac OS X, og er oversat til andre
sprog end engelsk. Hvis du bruger Mac OS X eller vil bruge Audacity på dit

eget sprog, bør bruge version 1.1.1. I øjeblikket understøttes disse sprog: bulgarsk, dansk, hollandsk, fransk, tysk, ungarnsk, italiensk, 
polsk, russisk, slovensk og spansk.
</p>

