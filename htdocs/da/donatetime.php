<li><b>Vær oversætter</b>
<p>
Hvis du er god til engelsk og et andet sprog, kan du hjælpe os andre, der
taler dit sprog. Vi har brug for folk til at oversætte programmenuerne, 
dialogboksene, dokumentationen og webstedet.<BR>
Vi har på sigt også brug for folk til at deltage i e-mail lister på deres
eget sprog, så de kan hjælpe brugere, der ikke taler så godt engelsk. Her
kan du hjælpe enten ved at besvare spørgsmålene direkte eller ved at 
oversætte dem til engelsk og videresende dem.
<p>
<a href="http://audacity.sourceforge.net/translation/"
>Klik her for at læse mere om oversættelse.</a>
<p>

<li><b>Rapportér fejl</b>
<p>
Hvis du finder en fejl i Audacity,
<a href="mailto:audacity-help@lists.sourceforge.net"
>vil vi gerne vide det</a>.
Det er os en stor hjælp hvis du tager tid til at gentage fejlen og skrive
en detaljeret beskrivelse af, hvordan du fremprovokerer den. Husk at 
fortælle, hvilket operativsystem, du bruger (f.eks. Windows 2000, Mac OS 9, etc.).
<p>


<li><b>Vær betatester</b>
<p>
Udover den seneste stabile udgave af Audacity har vi altid en betaudgave.
Denne udgave har mange nye faciliteter, der ikke er grundigt testede 
endnu. Hvis du har lyst til at give en hånd, kan du hente denne udgave
af Audacity og lege med den. Fortæl os, hvad du kan lide og hvad du ikke
kan lide, og fortæl os, hvad du synes skal forbedres. Kig specielt efter
<i>regressioner</i> - dvs. ting, der virkede i tidligere udgaver men ikke
i denne.
