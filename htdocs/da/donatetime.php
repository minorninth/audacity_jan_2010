og fortæl os, at du kan lide programmet. Det gør os glade at vide, at
programmet bliver værdsat. Og du er velkommen til at sende kommentarer
og forslag - vi forbedrer stadig Audacity, og vi sætter pris på dine 
bidrag.
<p>

<li><b>Vær med blandt Audacity-brugerne</b>
<p>
