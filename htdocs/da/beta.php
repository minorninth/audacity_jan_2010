<p>
Advarsel: denne udgave af Audacity er en beta-udgave!
Den har masser af nye faciliteter, blandt andeten ny værktøjslinje
automatisk resampling, brug af 32-bit sampling, Gain og Pan kontroller på hvert spor. Programmet er oversat til mange sprog. Programmet betragtes stadig som ufærdigt, dokumentationen er ukomplet, og der kan være alvorlige fejl endnu.
</p>
<p>
Hvis du har lyst til at eksperimentere med de nye faciliteter,
foreslår vi at du henter denne udgave og leger med den.
Og hvis du gør det, er du meget velkommen til at give os 
tilbagemeldinger. Fortæl os, hvad der virker, hvad der ikke gør,
og hvilke forbedringer, du gerne ville se i programmet.
Du kan have denne udgave og Audacity 1.0 (den stabile udgave)
installeret samtidig på din computer.
</p>
<p>
Bemærk:
Denne udgave kan ikke læse eller skrive projektfiler fra andre udgaver af Audacity, hverken 1.0, 1.1.1 eller 1.1.2. For at flytte eksisterende projekter til denne version må du eksportere alle lydsporene til et standardformat (WAV, AIFF) og derefter importere filerne til 1.1.3.
</p>
