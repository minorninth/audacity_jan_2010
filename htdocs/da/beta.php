<p>
Advarsel: denne udgave af Audacity er en beta-udgave!
Den har masser af nye faciliteter, blandt andeten ny værktøjslinje
automatisk resampling, brug af 32-bit sampling, men har sikkert også 
mange nye fejl.
</p>
<p>
Hvis du har lyst til at eksperimentere med de nye faciliteter,
foreslår vi at du henter denne udgave og leger med den.
Og hvis du gør det, er du meget velkommen til at give os 
tilbagemeldinger. Fortæl os, hvad der virker, hvad der ikke gør,
og hvilke forbedringer, du gerne ville se i programmet.
Du kan have denne udgave og Audacity 1.0 (den stabile udgave)
installeret samtidig på din computer.
</p>
<p>
Bemærk:
Denne udgave kan ikke læse eller skrive projekter, der kan bruges
af Audacity 1.0 eller tidligere, da den bruger et nyt XML-baseret
filformat.
</p>
