specielt Windows, Mac OS og Linux.
</p>

<p>
Version 1.1.0 er den første betaudgave af den næste version af Audacity,
der blandt nyhederne har understøttelse af endnu bedre lydkvalitet,
flere lydfil formater, flere effekter, flere redigeringsfunktioner, 
en forbedret brugerflade, og understøttelse af flere sprog.
Der er mange flere funktioner end i den stabile udgave, men det er vigtigt
at være klar over, at nogle af nyhederne stadig er under udvikling, 
ikke er testet så grundigt, og alle slags problemer kan eksistere.
</p>

<p>
