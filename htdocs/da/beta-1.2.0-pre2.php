<h1>Audacity 1.2.0-pre2</h1>

<p>
Audacity 1.2.0-pre2 er en offentlig testudgave af det gratis lydredigeringsprogram Audacity. Formålet med denne udgave er at vise og teste de nye faciliteter der vil være i vores næste stabile udgave 1.2.0.
</p>

<p>
Har du kommentarer eller fejlrapporter kan du sende dem til:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Din tilbagemelding vil hjælpe os med at gøre den endelige udgave stabil og fejlfri. Vi forventer at udgive den stabile udgave i oktober 2003.
</p>
