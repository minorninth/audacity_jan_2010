<p>
Audacity kan bruge VST udvidelse på Mactintosh- og Windows-baserde computere.
Du kan finde udvidelser der har alle former for filtre, equilizere, reverb,
kor-effekt og mere. Nedenfor er en liste over downloadsteder for VST udvidelser.
</p>

<p>
Audacitys brug af udvidelser bliver løbende forbedret. Hvis du finder en
VST-udvidelse, der ikke fungere i Audacity vil vi meget gerne have det at
vide, så vi kan løse problemet.
</p>

<p>
For at bruge VST udvidelser i Audacity skal du lave en mappe ved navn <b>VST</b>
i Audacitys mappe (den kan eksistere i forvejen) og placere udvidelserne der!
Næste gang, du starter Audacity, vil udvidelserne blive taget med i Effekter-menuen.
</p>

