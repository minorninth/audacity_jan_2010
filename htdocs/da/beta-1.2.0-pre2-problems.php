<h2>Kendte problemer</h2>

<p>
Her er nogle kendte fejl i Audacity 1.2.0-pre2:
</p>

<ul>
  <li>Audacity kan importere MIDI-filer, men de kan ikke spilles eller redigeres.
    <p>
  <li>Optagelse i fuld duplex (dvs. man afspiller og optager samtidig)
    kan få optagelsen til at lyde langsom på nogle Linux-systemer.
    <p>
  <li>Kun MacOS X: Audacity kan ikke arbejde med filer og mapper,
    der ligger indeni mapper med internationale tegn (æøå o. lign)
    i navnet. Bemærk, at filer og projektfiler med disse tegn fungerer.
    Det er kun filer inden i mapper med disse tegn, der giver problemer.
    <p>

  <li>Kun Windows: Audacity er inkompatibelt med visse professionelle
    lydkort og kan gå ned, hvis et af dem er valgt som standard, når
    Audacity åbner. Som en midlertidig løsning kan du vælge et andet
    lydkort som standard med Audacity. Vi vil meget gerne høre fra dig,
    hvis det sker for dig, det vil hjælpe os med at indkredse og løse
    problemet.
    <p>
    Bemærk, at Windows installationsprogrammet vil erstattet 1.0.0 
    installationerne, ikke 1.1.x beta-udgaverne.
    <p>
</ul>

