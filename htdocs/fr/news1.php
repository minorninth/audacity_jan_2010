<?php BoxTop('Le 5 juin 2002: lancement des versions 1.0.0 et 1.1.0 !'); ?>

<p>
L'équipe de développeurs d'Audacity est fière d'annoncer le lancement simultané des versions 1.0.0 et 1.1.0.</p>

<p>
La version 1.0.0 est le résultat final et stable des versions précédentes d'Audacity. C'est une application de traitement sonore simple et efficace qui convient à l'enregistrement, au mixage et au traitement de pistes audios de qualité   CD-audio sur plusieurs plateformes dont Windows, Mac OS, et Linux.
</p>

<p>
La version 1.1.0 est la première version bêta disponible de la prochaine mise à jour importante d'Audacity qui supporte une plus grande qualité audio, plus de formats de fichiers audio, et offre plus d'effets, plus de fonctions d'édition, une interface utilisateur améliorée et la possibilité de travailler dans la langue de son choix. Bien que cette version offre plusieurs nouvelles fonctions très utiles, certaines sont encore en développement et n'ont été testées que sommairement. Elles pourraient causer des problèmes.
</p>

<p>
Les deux versions d'Audacity peuvent être présentes en même temps sur votre ordinateur.
</p>

<p>
Notez que même si le site web est en français, Audacity 1.0 n'est disponible qu'en anglais.  Puisque Audacity 1.1 (version bêta) permet de travailler en plusieurs langues, c'est cette version que vous devez télécharger si vous désirez absolument avoir l'application en français.  Sinon, vous devrez travailler en anglais.
</p>

