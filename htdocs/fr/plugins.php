<p>
Audacity 1.2 intègre les modules <a href="http://www.ladspa.org/">LADSPA</a> at
<a href="nyquist.php">Nyquist</a> en mode natif, et les modules VST avec Audacity VST Enabler<a href="http://audacityteam.org/vst/"> optionnel </a>.
</p>
<p>
Audacity 1.2.0 intègre déjà quelques échantillons de modules LADSPA and Nyquist dans le dossier <b>Plug-ins</b>.  Placer le VST Enabler et autres modules dans ce répertoire pour pouvoir les utiliser avec Audacity.  A la prochaine ouverture d'Audacity, ces modules seront accessibles depuis le mebnu Effet (ou Générer ou Analyser).  Ces modules d'effets apparaissent toujours en bas des menus, alors que les effests internes se trouvent en haut.
</p>

<p>
La compatibilité d'Audacity avec les plugiciels s'améliore.  Veuillez nous aviser si vous avez un plugiciel VST qui ne fonctionne pas avec Audacity, nous tenterons d'apporter les corrections nécessaires.
</p>

