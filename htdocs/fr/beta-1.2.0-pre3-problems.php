<h2>Problèmes Connus</h2>

<p>
Voici les problèmes connus d'Audacity 1.2.0:
</p>

<ul>
  <li>Audacity peut importer et afficher des fichiers midi mais ceux-ci ne peuvent ni être lus, ni édités
    <p>
  <li>L'enregistrement en Full Duplex (lire des pistes existants pendant l'enregistrement)fait que le son est ralenti sous certains systêmes Linux
    <p>
  <li>MacOS X seulement: Audacity ne peut pas travailler avec des fichier ou des dossiers qui se trouvent à l'intérieur de dossiers dont le nom comporte des caractères internationaux (accents, etc.) dans leur nom. Notez que les fichiers avec des accents fonctionnent ainsi que les projets Audacity avec des accents. Seuls les fichiers se trouvant à l'intérieur de dossiers avec des caractères accentués vont poser problème.
  </p></li> 
  <li><p>Le changement de hauteur peut provoquer un crash s'il est utilisé sur une piste ayant une fréquence d'échantillonnage de 96 KHz.</p></li>
    <p>
</ul>
