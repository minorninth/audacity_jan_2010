Nous avons résolu énormément de bugs dans la dernière version publique et nous allons sortir Audacity 1.2.0-pre3 dans les prochains jours, avec l'Activateur VST, finalement. Dans la foulée, voici d'autres news:

<p>
Audacity a été nominé comme programme open-source possible qu'<a href="http://www.oreilly.com/">O'Reilly</a> pourrait envoyer au <a href="http://www.comdex.com/">COMDEX</a> a Las Vegas,
du 17 au 20 novembre.  <a href="http://www.oreillynet.com/contest/comdex/"
>Cliquez ici</a> pour plus d'informations et pour voter pour
Audacity (enregistrement gratuit sur O'Reilly requis pour voter).
Les votes clôturent au 31 octobre.
</p>

<p>
L'<a href="http://audacityteam.org/wiki/">Audacity Wiki</a> est un site web editable par le public pour tout ce qui concerne Audacity. Il inclut actuellement des trucs et astuces, de la documentation et des liens, plus une place pour les demandes de fonctions. La meilleure partie dans tout ça, c'est qu'il est facile pour n'importe qui de contribuer - juste cliquer sur un lien au fond de la n'importe quelle page pour l'éditer ou ajouter votre propre contenu. Nous espérons que vous trouverez celà utile et sympa.
</p>

<p>
Nous avons maintenant des <a href="http://audacityteam.org/tshirt.php"> T-Shirts Audacity</a> disponibles comme remerciements aux gens qui contribuent.
</p>

<p>
Le <a href="http://audacityteam.org/wiki/">Wiki</a> et l'<a href="http://audacityteam.org/tshirt.php">Offre T-Shirt</a>
sont tous deux hébergés sur un nouveau site web,
<a href="http://audacityteam.org/">AudacityTeam.org</a>,
que nous avons mis en place four le contenu supplémentaire d'Audacity et les propos relatifs à l'équipe de dévelopement d'Audacity, contrairement à Audacity en général.
</p>

<p>
Vérifiez bientôt la disponibilité d'Audacity 1.2.0-pre3.  Si vous n'avez pas utilisé récemment une version bêta ou pré-sortie d'Audacity, visitez notre galerie de <a href="http://audacity.sourceforge.net/screenshots.php"
>Captures d'écran</a> mise à jour. 
</p>

