<h2>Problèmes Connus</h2>

<p>
Les problèmes suivants sont connus avec Audacity 1.2.0-pre2:
</p>

<ul>
  <li>Audacity peut importer et afficher des fichiers MIDI, mais ils ne peuvent être joués ou édités.
    <p>
  <li>Enregistrer en Full-Duplex(lire des pistes existantes pendant l'enregistrement) peut, sur certains systêmes Linux faire que l'enregistrement soit ralenti.
       <p>
  <li>MacOS X suelement: Audacity ne peut fonctionner avec des fichiers ou des dossiers qui se trouvent dans un dossier avec des caractères internationaux    (accents, etc.) dans leur nom. Notez que les fichiers comportant des accents fonctionnent, ainsi que les fichiers Audacity avec des accents. Seuls les fichiers à l'intérieur de dossiers avec des accents dans leur nom vont causer problème. 
    <p>

  <li>Windows seulement: Audacity est incompatible avec certaines cartes son professionnelles et peut crasher si une de ces cartes est la carte par défaut au moment ou vous lancez Audacity. Comme alternative, utilisez une autre carte par défaut pour utiliser Audacity, mais SVP dites-nous si celà vous affecte, car ainsi nous pourrons mieux le cerner et solutionner ce problème.
    <p>
    Notez également que l'installeur Windows remplacera les installations 1.0 mais pas les installations 1.1.x .
   <p>
</ul>
