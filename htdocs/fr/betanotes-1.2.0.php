Améliorations de la version 1.2.0:

  * Nouveaux effets de fondu enchaîné.

  * Résoud le problème d'échantillons dessinés à la mauvaise place en zoom avant maxi. Ceci faisait déplacer à l'outil de dessin d'ondes un échantillon différent de celui situé sous le curseur.
   
  * N'utilise pas les tags id3v2.4, qui ne sont pas encore supportés par la plupart des lecteurs.  (Ceci avait été réglé dans la version 1.2.0-pre2, mais est accidentellement réapparu dans la 1.2.0-pre3.)

  * Traduction affichée correctement dans la barre d'état.

  * Quand le curseur est à l'écran, le bouton de zoom avant zoome désormais
    sur la zone du curseur.

  * Mac OS X: Problèmes audio du PowerMac G5 résolus.

  * Linux/ALSA: Travail  autour d'in bogue de l'émulation ALSA's OSS qui faisait se déplacer trop rapidement le curseur de lecture d'Audacity.

  * Microsoft Windows: Le code source Audacity peut maintenant compiler hors de la boîte de Windows.

  * Beaucoup de traductions nouvelles ou améliorées.

