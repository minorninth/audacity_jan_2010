<h1>Audacity 1.2.0-pre1</h1>

<p>
Audacity 1.2.0-pre1 est une sortie en test public de l'éditeur audio Audacity.
Le but de cette version est de permettre aux utilisateurs de tester les nouvelles fonctionnalités qui apparaitront dans notre prochaine version stable 1.2.0.
</p>

<p>
Pour signaler un bug ou faire un commentaire:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Vos rapports et suggestions nous aideront à rendre la version finale stable et sans bug! Nous espérons sortir la nouvelle version stable en Septembre 2003.
</p>
