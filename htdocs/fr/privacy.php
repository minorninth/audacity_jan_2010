<p><b>Règles de la liste d'annonces d'Audacity</b></p>

<p>(C'est la liste que vour rejoingnez quand vous entrez votre adresse de courriel dans la boite à gauche sur le site Web.)</p>

<p> 
Si vous désirez être informé(e) des mises à jour, veuillez inscrire votre adresse de courriel dans la boîte prévue à cet effet et cliquez sur "Inscrire". Vous serez inscrit(e) sur notre propre liste de diffusion que nous n'utilisons que pour envoyer occasionnellement de courts messages.
</p>

<p>
Au départ, ces messages seront rédigés uniquement en anglais, mais nous espérons offrir dans un proche avenir la possibilité de s'inscrire à une liste de diffusion dans la langue de votre choix.
</p>

<p>
Nous prenons votre confidentialité au sérieux.
</p>

<p>
A aucun moment les développeurs d'Audacity ou les personnes responsables des listes de diffusion ne partageront ces listes avec des tiers, pour quelque raison que ce soit. Nous ne conserverons aucune information à propos des personnes inscrites sur ces listes (pas même le nom!), et nous n'afficherons jamais ces listes publiquement.
</p>

<p>
Pour être rayé de la liste d'envoi, inscrivez votre adresse électronique (courriel) dans la case prévue à cet effet et cliquez sur "Rayer".
Cela effacera sans délai votre adresse électronique de la liste; aucune confirmation ne sera nécessaire et il n'y aura aucun  délai.  Dans le cas où vous n'arriveriez pas à rayer votre adresse électronique de la liste, écrivez à <a href="mailto:dominic@audacityteam.org">dominic@audacityteam.org</a> pour obtenir de l'aide.
</p>

<p><b>Règles pour les listes de diffusion d'Audacity</b></p>

<p>(Ceci s'applique aux liste de diffusions : 
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-help">audacity-help</a>,
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-users">audacity-users</a>,
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-devel">audacity-devel</a>, et
<a href="http://lists.sourceforge.net/lists/listinfo/audacity-translation">audacity-translation</a>.
</p>

<p>
Ces listes sont des <i>listes de discussion</i>.  Quand vous postez un message à l'une de ces listes, y compris <a href="http://lists.sourceforge.net/lists/listinfo/audacity-help">audacity-help</a>,
il sera envoyé a des centaines de personnes, y compris les développeurs d'Audacity et d'autres membres du public. Plusieurs sites web, y compris SourceForge, peuvent publier des archives de ces listes en ligne. Ces archives ne contiennent pas normalement votre adresse de courriel. Mais rien n'empèche une personne de s'inscrire à l'une de ces listes et d'y publier ces propres archives, y compris des adresses de courriels. Si vous êtes concerné par la confidentialité, vous devriez utiliser une adresse de courriel temporaire.</p>

<p>
Si vous avez des questions à propos de la confidentialité et que vous ne souhaitez pas poster votre message sur les listes de diffusion, envoyez un courriel privé au responsable des développeurs d'Audacity : <a href="mailto:dominic@audacityteam.org">dominic@audacityteam.org</a>.
</p>

<p>Dans ce site,le seul masculin est utilisé dans le but d'alléger le texte.
</p>

