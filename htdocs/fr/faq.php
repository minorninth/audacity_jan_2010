<font size=+1><b>
1. Windows: Que faire avec le fichier .exe?<br>
</b></font>

<p>Dans l'explorateur, sélectionnez l'option:"Exécuter à partir de l'emplacement actuel".  Cela active l'installateur d'Audacity qui installera les fichiers et ajoutera un item au menu Démarrer.
<p>
Si vous avez déjà téléchargé le fichier, il suffit de double-cliquer sur son icône pour lancer l'installateur.
<p>
Si vous préférez ne pas utiliser le programme d'installation, vous pouvez aussi télécharger Audacity en fichier ZIP que vous décompresserez avec WinZip.
<p>
<a name="i2"></a>
<font size=+1><b>
2. Windows: Comment désinstaller Audacity?<br>
</b></font>

<p><font size=+0>Audacity peut être désinstallé par le panneau de configuration Ajout/Suppression de programmes. Dans la liste, sélectionnez "Audacity" et cliquez sur "Ajouter/Supprimer...". Ceci lance le programme de désinstallation d'Audacity.
<p>
<a name="i3"></a> <font size=+1><b> 3. Mac: Que faire avec le fichier .sit?<br> </b></font>

<p>
Il vous faut une version récente de StuffIt Expander.  StuffIt Expander est inclus sur tous les Macintoshs et il est habituellement sélectionné par défaut sur tous les navigateurs pour Mac. Dans le cas où Audacity ne se décompresserait pas automatiquement, glissez le fichier "audacity.sit" sur l'icone de StuffIt Expander pour le décompresser.
<p>
Audacity ne requiert aucune installation particulière.  Il suffit de glisser la chemise Audacity dans la chemise Applications ou à tout autre endroit que vous jugez pratique.  Pour désinstaller, il suffit de glisser tout le dossier dans la corbeille.

<p>
<a name="i4"></a>
<font size=+1><b>
4. Unix: Pourquoi ai-je des "failed dependencies" lorsque j'installe à partir de RPM?
<br></b></font>

<p><font size=+0>D'abord, assurez-vous que wxGTK est installé; vous trouverez un lien pour le télécharger sur notre page pour Linux - http://audacity.sourceforge.net/linux.html. Si RPM
refuse de s'installer, this is an unresolved issue.  Installez simplement RPM
à l'aide du flag --nodeps.</font>

<p>
<a name="i5"></a>
<font size=+1><b>

5. Unix: J'obtiens un message d'erreur quand je charge la bibliothèque partagée libwx_gtk.so<br></b></font>

<p><font size=+0>Il y a deux possibilités:
<br>- Si vous avez installé wxWindows RPM, assurez-vous d'exécuter "ldconfig" (as
root) afin que la nouvelle bibliothèque soit reconnue par le système.

<br><br>- Si vous avez installé wxWindows
à partir de la source, il est peut-être bien installé dans in /usr/local, mais /usr/local/lib n'est peut-être pas dans le chemin d'accès de la bibliothèque. Ajoutez /usr/local/lib au fichier "/etc/ld.so.conf" et exécutez "ldconfig". Vous pouvez également ajouter "/usr/local/bin" au chemin d'accès pour que le programme puisse trouver l'utilitaire "wx-config".<br>

