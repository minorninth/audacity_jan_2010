<font size=+1><b>
4. J'ai trouvé un bogue!  Qu'est-ce que je fais?<br>
</b></font>
<p>

Lorsque vous rapportez un bogue, il est important d'être aussi précis que possible. Donnez-nous suffisamment d'informations pour que nous puissions le reproduire, sans quoi il nous sera difficile de le corriger. Envoyez vos rapports de bogues à 
<a href="mailto:audacity-help@lists.sourceforge.net">
<audacity-help@lists.sourceforge.net></a>.
<p>
N'oubliez pas de mentionner votre système d'exploitation (par exemple Windows 98, MacOS 9.1, RedHat Linux 7.1, etc.) ainsi que toute caractéristique de votre ordinateur que vous jugez pertinente.
<p>
Pouvez-vous le reproduire? S'il se présente de façon systématique, dites-nous exactement toutes les manipulations que vous faites pour le provoquer.  Si vous obtenez un message d'erreur, rapportez-nous le texte exact de ce message.
<p>

Nous voulons éliminer tous les bogues!  Nous vous sommes reconnaissants de nous aider à les identifier tous.
