<font size=+1><b>
1. Audacity est-il vraiment gratuit?  Pourquoi?<br>

</b></font>

<p>

Oui, et en plus d'être gratuit, vous êtes libre d'en faire ce que vous voulez.
<p>
Audacity est rendu disponible en vertu de la <a href="http://www.gnu.org/copyleft/gpl.html"
>License Publique Générale GNU</a> (GNU GPL).
Vous pouvez utiliser librement ce programme à des fins personnelles ou commerciales.  Vous pouvez également le distribuer et le vendre.
Le code source du logiciel est disponible gratuitement sur la toile, et vous pouvez le modifier selon vos besoins.  Cependant, tous les changements que vous apportez doivent également être distribués en vertu de la license GPL.
<p>
Audacity est développé à l'aide de wxWindows, une bibliothèque de programmes distribuée en vertu de la license LGPL, moins restrictive. Pour plus d'informations, veuillez voir le site 
<a href="http://www.wxwindows.org">wxwindows.org</a>.
<p>
Les créateurs d'Audacity ont décidé de distribuer le logiciel en vertu de la license GPL pour plusieurs raisons.  Certains le font par générosité. Certains le font par principe, parce qu'ils croient que tous les logiciels devraient être gratuits, alors que d'autres croient que les logiciels commerciaux et les logiciels gratuits ont tous leur raison d'être.
<p>
Un des bienfaits de la gratuité d'Audacity, c'est qu'il sera ainsi plus populaire. Plusieurs d'entre nous préfèrent que le logiciel soit utilisé gratuitement par un million d'utilisateurs heureux plutôt qu'acheté par seulement quelques milliers d'utilisateurs.  Et la plupart des utilisateurs sont plus avenants lorsqu'ils obtiennent quelque chose gratuitement.

<p>
Une autre  bonne raison, c'est que ça encourage la coopération.  Si Audacity était un partagiciel, il est peu probable que des dizaines de personnes à travers le monde s'impliquent en programmant, en corrigeant des bogues, en rédigeant la documentation et en créant les graphismes.

