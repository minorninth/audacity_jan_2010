<a name="g2"></a>
<font size=+1><b>
2. Qui a développé Audacity?<br>
</b></font>

 <p>

Dominic Mazzoni a créé Audacity à l'automne 1999 alors qu'il était étudiant en doctorat à l'université Carnegie Mellon à Pittsburgh aux États-Unis. Il travaillait sur un projet avec son directeur de recherche, le professeur Roger Dannenberg, et ils avaient besoin d'un outil qui leur permettrait de visualiser les algorithmes d'analyse audio.  Avec le temps, ce programme est devenu un logiciel général de traitement sonore et d'autres personnes se sont impliquées.

<p>
Maintenant, Audacity est développé avec l'aide de Sourceforge, un site internet
qui permet aux gens des quatre coins du monde de collaborer à des projets de logiciels gratuits.  Pour plus d'informations, visitez le site <a href="http://www.sourceforge.net"
>sourceforge.net</a>. 
Des dizaines de personnes ont participé au développement d'Audacity et le projet évolue de plus en plus rapidement.

