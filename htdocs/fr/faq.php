<font size=+1><b>

1. Comment combiner deux pistes?
</b></font>
<p>

Audacity combine les pistes automatiquement.  Il suffit d'importer les deux pistes dans le même projet, ce qui peut être fait en utilisant l'item "Importation audio" du menu Projet ou simplement en glissant les deux fichiers audio dans la fenêtre Audacity.  Aussi, en appuyant sur le bouton d'enregistrement, Audacity génère automatiquement une nouvelle piste.
<p>

Pour sauvegarder le résultat, vous pouvez soit Exporter le projet, ce qui combinera automatiquement toutes les pistes, soit sélectionner les pistes en question et utiliser l'item "Quick Mix" du menu Projet.

<p>
<a name="a2"></a>
<font size=+1><b>
2. Est-il possible d'éliminer la voix d'un enregistrement?
</b></font>
<p>

Il est possible d'éliminer la voix de certains enregistrements stéréo selon la façon dont le mixage  a été fait en studio. Souvent, la partie vocale est positionnée exactement au centre alors que les instruments sont sur les côtés.  En soustrayant le canal droit du canal gauche, la voix se trouve annulée, ne laissant que les instruments.
<p>

<b>Ceci ne fonctionne que sur certains enregistrements!</b>
<p>Pour le faire avec Audacity, ouvrez un enregistrement stéréo. Dans le menu déroulant de la piste (cliquez sur la petite flèche après le nom de la piste pour le faire apparaître), sélectionnez "Split Stereo Track".  Sélectionnez ensuite la piste du bas (le canal droit) et utilisez l'effet Inversion du menu Effet. Enfin, convertissez chaque piste en Mono à l'aide du  menu déroulant devant chacune et combinez-les avec Quick Mix. Si vous êtes chanceux, la voix aura disparu. 

<p>
<a name="a3"></a>
<font size=+1><b>
3. Pourquoi les pistes sont-elles décalées lorsque j'essaie d'enregistrer des harmonisations avec moi-même?
</b></font>
<p>
Ce phénomène est normal et est causé par le petit délai entre le moment où Audacity joue un son et le moment où ce son atteint les hauts-parleurs. Audacity ne corrige pas automatiquement ce délai, il faut donc le corriger manuellement.
<p>
Pour le corriger, utilisez l'outil de décallage temporel pour glisser une des pistes jusqu'à ce qu'elle soit alignée avec les autres.  Notez qu'il est possible d'utiliser l'outil pendant l'écoute de l'enregistrement.

<p>

<a name="a4"></a>
<font size=+1><b>
4. Pourquoi Audacity n'inclut-il pas avec un encodeur MP3?
</b></font>
<p>

Malheureusement, l'algorithme pour créer des fichiers MP3 est breveté, et inclure un tel encodeur dans <b>Audacity</b> serait impossible sans ajouter des frais ou sans enfreindre la loi de plusieurs pays.
<p>
En guise de compromis, Audacity a la capacité d'utiliser d'autres encodeurs MP3 téléchargeables séparément.  Il est de votre devoir de respecter les licences de ces encodeurs MP3.
<p>
Pour plus d'informations, rendez-vous à la page Exporter des fichers MP3 dans l'Aide en ligne.
</p>

