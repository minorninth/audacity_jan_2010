<h1>Audacity 1.2.0-pre3</h1>

<p>
Audacity 1.2.0-pre3 est une sortie en test public de l'éditeur audio Audacity.
Le but de cette sortie est de permettre aux utilisateurs de voir et de tester les nouvelles fonctions qui apparaitront dans notre prochaine version stable 1.2.0.
</p>

<p>
SVP reportez tous les bugs ou commentaires à:
<a href="mailto:audacity-help@lists.sourceforge.net">
audacity-help@lists.sourceforge.net</a>
</p>

<p>
Vos commentaires nous permettront de faire une version finale stable et sans bug!
Nous espérons sortir la nouvelle version finale en novembre 2003.
</p>