<?php BoxTop('August  2, 2003: Audacity 1.2.0-pre1 est sorti'); ?>

<p>
Audacity 1.2.0-pre1 est une sortie en test public de l'éditeur audio Audacity.
Le but de cette version est de permettre aux utilisateurs de tester les nouvelles fonctionnalités qui apparaitront dans notre prochaine version stable 1.2.0, que nous espérons sortir le mois prochain.
</p>

<p>
Cette version ajoute un traitement audio de qualité professionnelle, et de nouvelles fonctionnalités majeurs comme la possibilité d'accélérer, de ralentir ou d'altérer la hauteur de note d'une piste. C'est la première version qui peut ouvrir des projets enregistrés dans l'ancien format Audacity 1.0 aussi bien que le nouveau format XML utilisé par Audacity 1.1 et supérieur. Audacity 1.2.0-pre1 corrige plusieurs bugs de la dernière version bêta.

</p>

<p>
La page <a href="beta.php?<?php print "$langLinkStr"; ?>">Beta Versions</a> comporte des notes complètes et les liens de téléchargement.
</p>

<p>
Note: Le chargement des plugin VST a été désactivé dans Audacity 1.2.0-pre1 car nous travaillons sur des problèmes de licence potentiels. Nous espèrons résoudre tous les conflits à temps pour inclure le support VST dans la version finale 1.2.0 d'Audacity.
</p>

